    private static string GenerateSource(DtoClassInfo classInfo, bool hasFluentValidation)
    {
        var sb = new StringBuilder();
        sb.AppendLine("// <auto-generated />");
        sb.AppendLine($"// Generated by ZibStack.NET.Dto from [{classInfo.Kind}Dto] on {classInfo.ClassName}");
        sb.AppendLine("#nullable enable");
        sb.AppendLine();
        sb.AppendLine("using System;");
        sb.AppendLine("using System.Collections.Generic;");
        sb.AppendLine("using ZibStack.NET.Dto;");
        sb.AppendLine();

        if (classInfo.Namespace is not null)
        {
            sb.AppendLine($"namespace {classInfo.Namespace};");
            sb.AppendLine();
        }

        switch (classInfo.Kind)
        {
            case DtoKind.Create:
                GenerateCreateRequestClass(sb, classInfo);
                if (hasFluentValidation)
                {
                    sb.AppendLine();
                    GenerateFluentBaseValidator(sb, classInfo, isCreate: true, combined: false);
                }
                break;

            case DtoKind.Update:
                GenerateUpdateRequestClass(sb, classInfo);
                if (hasFluentValidation)
                {
                    sb.AppendLine();
                    GenerateFluentBaseValidator(sb, classInfo, isCreate: false, combined: false);
                }
                break;

            case DtoKind.Combined:
                GenerateCombinedRequestClass(sb, classInfo);
                if (hasFluentValidation)
                {
                    sb.AppendLine();
                    GenerateFluentBaseValidator(sb, classInfo, isCreate: true, combined: true);
                    sb.AppendLine();
                    GenerateFluentBaseValidator(sb, classInfo, isCreate: false, combined: true);
                }
                break;
        }

        return sb.ToString();
    }

    private static void GenerateCreateRequestClass(StringBuilder sb, DtoClassInfo classInfo)
    {
        var props = classInfo.Properties.Where(p => !p.IsUpdateOnly).ToList();

        sb.AppendLine($"public record {classInfo.RequestName} : ZibStack.NET.Dto.ICanCreate<{classInfo.ClassName}>, ZibStack.NET.Dto.ICanValidate");
        sb.AppendLine("{");

        foreach (var prop in props)
        {
            EmitValidationAttributes(sb, prop.ValidationAttributes);
            sb.AppendLine($"    public PatchField<{GetPropTypeName(prop, isCreate: true)}> {prop.PropertyName} {{ get; init; }}");
        }

        sb.AppendLine();

        // Validate()
        sb.AppendLine("    public DtoValidationResult Validate()");
        sb.AppendLine("    {");

        if (classInfo.CreateValidatorTypeName is not null)
        {
            sb.AppendLine($"        return new {classInfo.CreateValidatorTypeName}().Validate(this);");
        }
        else
        {
            sb.AppendLine("        var errors = new DtoValidationResult();");
            foreach (var prop in props)
            {
                if (prop.IsRequired)
                {
                    sb.AppendLine($"        if (!{prop.PropertyName}.HasValue)");
                    sb.AppendLine($"            errors.AddError(\"{prop.JsonName}\", \"is required.\");");

                    if (NeedsNullCheck(prop))
                    {
                        sb.AppendLine($"        else if ({prop.PropertyName}.Value is null)");
                        sb.AppendLine($"            errors.AddError(\"{prop.JsonName}\", \"cannot be null.\");");
                    }
                }
                else if (NeedsNullCheck(prop))
                {
                    sb.AppendLine($"        if ({prop.PropertyName}.HasValue && {prop.PropertyName}.Value is null)");
                    sb.AppendLine($"            errors.AddError(\"{prop.JsonName}\", \"cannot be null.\");");
                }
                EmitValidationRules(sb, prop);
            }
            sb.AppendLine("        return errors;");
        }

        sb.AppendLine("    }");
        sb.AppendLine();

        // ToEntity()
        GenerateToEntityMethod(sb, props, classInfo.ClassName);
        sb.AppendLine();

        sb.AppendLine("}");
    }

