    /// <summary>Non-nullable reference types need null validation.</summary>
    private static void EmitAutoNestedDto(StringBuilder sb, DtoClassInfo nested)
    {
        var nestedSource = GenerateSource(nested, false);
        var lines = nestedSource.Split('\n');
        var skip = true;
        foreach (var line in lines)
        {
            if (skip && (line.TrimStart().StartsWith("//") || line.TrimStart().StartsWith("#") ||
                line.TrimStart().StartsWith("using ") || line.TrimStart().StartsWith("namespace ") ||
                string.IsNullOrWhiteSpace(line)))
                continue;
            skip = false;
            sb.AppendLine(line.TrimEnd());
        }
    }

    private static void EmitValidationAttributes(StringBuilder sb, List<string> attrs)
    {
        foreach (var attr in attrs)
            sb.AppendLine($"    {attr}");
    }

    private static void EmitValidationRules(StringBuilder sb, DtoPropertyInfo prop)
    {
        if (prop.ValidationRules.Count == 0) return;

        var name = prop.PropertyName;
        var json = prop.JsonName;
        var isString = prop.DisplayTypeName is "string" or "string?";

        foreach (var rule in prop.ValidationRules)
        {
            var guard = prop.IsValueType && !prop.IsNullable
                ? $"{name}.HasValue"
                : $"{name}.HasValue && {name}.Value is not null";

            var msg = rule.Message;

            switch (rule.Kind)
            {
                case ValidationRuleKind.MinLength when rule.IntParam1 is { } minLen:
                    msg ??= $"must be at least {minLen} characters.";
                    sb.AppendLine($"        if ({guard} && {name}.Value.Length < {minLen})");
                    sb.AppendLine($"            errors.AddError(\"{json}\", \"{EscapeString(msg)}\");");
                    break;

                case ValidationRuleKind.MaxLength when rule.IntParam1 is { } maxLen:
                    msg ??= $"must be at most {maxLen} characters.";
                    sb.AppendLine($"        if ({guard} && {name}.Value.Length > {maxLen})");
                    sb.AppendLine($"            errors.AddError(\"{json}\", \"{EscapeString(msg)}\");");
                    break;

                case ValidationRuleKind.StringLength when rule.IntParam1 is { } sMax:
                {
                    msg ??= rule.IntParam2 is { } sMin
                        ? $"must be between {sMin} and {sMax} characters."
                        : $"must be at most {sMax} characters.";
                    var lenCheck = rule.IntParam2 is { } sMin2
                        ? $"({name}.Value.Length < {sMin2} || {name}.Value.Length > {sMax})"
                        : $"{name}.Value.Length > {sMax}";
                    sb.AppendLine($"        if ({guard} && {lenCheck})");
                    sb.AppendLine($"            errors.AddError(\"{json}\", \"{EscapeString(msg)}\");");
                    break;
                }

                case ValidationRuleKind.Range when rule.DoubleParam1 is { } rMin && rule.DoubleParam2 is { } rMax:
                {
                    msg ??= $"must be between {FormatDouble(rMin)} and {FormatDouble(rMax)}.";
                    var cast = prop.IsValueType ? $"(double){name}.Value" : $"(double){name}.Value";
                    sb.AppendLine($"        if ({guard} && ({cast} < {FormatDouble(rMin)} || {cast} > {FormatDouble(rMax)}))");
                    sb.AppendLine($"            errors.AddError(\"{json}\", \"{EscapeString(msg)}\");");
                    break;
                }

                case ValidationRuleKind.Email:
                    msg ??= $"is not a valid email address.";
                    sb.AppendLine($"        if ({guard} && !System.Text.RegularExpressions.Regex.IsMatch({name}.Value, @\"^[^@\\s]+@[^@\\s]+\\.[^@\\s]+$\"))");
                    sb.AppendLine($"            errors.AddError(\"{json}\", \"{EscapeString(msg)}\");");
                    break;

                case ValidationRuleKind.Url:
                    msg ??= $"is not a valid URL.";
                    sb.AppendLine($"        if ({guard} && !System.Uri.TryCreate({name}.Value, System.UriKind.Absolute, out _))");
                    sb.AppendLine($"            errors.AddError(\"{json}\", \"{EscapeString(msg)}\");");
                    break;

                case ValidationRuleKind.Regex when rule.StringParam is { } pattern:
                    msg ??= $"does not match the required pattern.";
                    sb.AppendLine($"        if ({guard} && !System.Text.RegularExpressions.Regex.IsMatch({name}.Value, @\"{EscapeVerbatim(pattern)}\"))");
                    sb.AppendLine($"            errors.AddError(\"{json}\", \"{EscapeString(msg)}\");");
                    break;

                case ValidationRuleKind.NotEmpty:
                    msg ??= $"must not be empty.";
                    if (isString)
                    {
                        sb.AppendLine($"        if ({name}.HasValue && string.IsNullOrWhiteSpace({name}.Value))");
                    }
                    else
                    {
                        sb.AppendLine($"        if ({guard} && ((System.Collections.ICollection){name}.Value).Count == 0)");
                    }
                    sb.AppendLine($"            errors.AddError(\"{json}\", \"{EscapeString(msg)}\");");
                    break;

                case ValidationRuleKind.Phone:
                    msg ??= $"is not a valid phone number.";
                    sb.AppendLine($"        if ({guard} && !System.Text.RegularExpressions.Regex.IsMatch({name}.Value, @\"^\\+?[\\d\\s\\-\\(\\)]{7,15}$\"))");
                    sb.AppendLine($"            errors.AddError(\"{json}\", \"{EscapeString(msg)}\");");
                    break;
            }
        }
    }

    private static string FormatDouble(double d)
    {
        if (d == (int)d) return ((int)d).ToString();
        return d.ToString(System.Globalization.CultureInfo.InvariantCulture);
    }

    private static string EscapeString(string s)
        => s.Replace("\\", "\\\\").Replace("\"", "\\\"");

    private static string EscapeVerbatim(string s)
        => s.Replace("\"", "\"\"");

    private static string GetPropTypeName(DtoPropertyInfo prop, bool isCreate)
    {
        var nestedName = isCreate ? prop.NestedCreateDtoName : prop.NestedUpdateDtoName;
        if (nestedName is not null)
        {
            return prop.IsNullable ? $"{nestedName}?" : nestedName;
        }
        return prop.DisplayTypeName;
    }

    private static bool IsValueTypeName(string typeName)
    {
        return typeName is "int" or "long" or "short" or "byte" or "float" or "double" or "decimal"
            or "bool" or "char" or "uint" or "ulong" or "ushort" or "sbyte"
            or "System.Int32" or "System.Int64" or "System.Int16" or "System.Byte"
            or "System.Single" or "System.Double" or "System.Decimal" or "System.Boolean"
            or "System.Char" or "System.DateTime" or "System.DateTimeOffset"
            or "System.DateOnly" or "System.TimeOnly" or "System.TimeSpan" or "System.Guid"
            || typeName.EndsWith("?");
    }

    private static bool NeedsNullCheck(DtoPropertyInfo prop)
        => !prop.IsValueType && !prop.IsNullable;

    /// <summary>Non-nullable reference types need ! when assigning from PatchField.Value.</summary>
    private static bool NeedsNullForgiving(DtoPropertyInfo prop)
        => !prop.IsValueType && !prop.IsNullable;

    private static void GenerateToEntityMethod(StringBuilder sb, List<DtoPropertyInfo> props, string typeName)
    {
        var normalProps = props.Where(p => !p.IsFlattened).ToList();
        var flatProps = props.Where(p => p.IsFlattened).ToList();

        sb.AppendLine($"    public {typeName} ToEntity()");
        sb.AppendLine("    {");
        sb.AppendLine($"        var entity = new {typeName}");
        sb.AppendLine("        {");
        foreach (var prop in normalProps)
        {
            if (prop.NestedCreateDtoName is null)
            {
                var defaultVal = prop.IsValueType ? "default" : (prop.IsNullable ? "default" : "default!");
                sb.AppendLine($"            {prop.SourcePropertyName} = {prop.PropertyName}.HasValue ? {prop.PropertyName}.Value{(NeedsNullForgiving(prop) ? "!" : "")} : {defaultVal},");
                continue;
            }

            if (prop.IsNullable)
                sb.AppendLine($"            {prop.SourcePropertyName} = {prop.PropertyName}.HasValue && {prop.PropertyName}.Value is not null ? {prop.PropertyName}.Value.ToEntity() : default,");
            else
                sb.AppendLine($"            {prop.SourcePropertyName} = {prop.PropertyName}.HasValue ? {prop.PropertyName}.Value!.ToEntity() : default!,");
        }
        sb.AppendLine("        };");

        // Apply flattened properties
        if (flatProps.Count > 0)
        {
            // Group by top-level property (first segment of FlattenEntityPath)
            var groups = flatProps.GroupBy(p => p.FlattenEntityPath!.Split('.')[0]);
            foreach (var g in groups)
            {
                sb.AppendLine($"        if (entity.{g.Key} is not null)");
                sb.AppendLine("        {");
                foreach (var fp in g)
                {
                    sb.AppendLine($"            if ({fp.PropertyName}.HasValue) entity.{fp.FlattenEntityPath} = {fp.PropertyName}.Value{(NeedsNullForgiving(fp) ? "!" : "")};");
                }
                sb.AppendLine("        }");
            }
        }

        sb.AppendLine("        return entity;");
        sb.AppendLine("    }");
    }

    private static void GenerateApplyToMethod(StringBuilder sb, List<DtoPropertyInfo> props, string typeName)
    {
        sb.AppendLine($"    public void ApplyTo({typeName} target)");
        sb.AppendLine("    {");
        foreach (var prop in props)
        {
            if (prop.IsImmutable) continue;

            if (prop.IsFlattened)
            {
                sb.AppendLine($"        if ({prop.PropertyName}.HasValue && target.{prop.FlattenEntityPath!.Split('.')[0]} is not null) target.{prop.FlattenEntityPath} = {prop.PropertyName}.Value{(NeedsNullForgiving(prop) ? "!" : "")};");
                continue;
            }

            if (prop.NestedUpdateDtoName is null)
            {
                sb.AppendLine($"        if ({prop.PropertyName}.HasValue) target.{prop.SourcePropertyName} = {prop.PropertyName}.Value{(NeedsNullForgiving(prop) ? "!" : "")};");
                continue;
            }

            sb.AppendLine($"        if ({prop.PropertyName}.HasValue)");
            sb.AppendLine("        {");
            if (prop.IsNullable)
            {
                sb.AppendLine($"            if ({prop.PropertyName}.Value is null)");
                sb.AppendLine($"                target.{prop.SourcePropertyName} = null;");
                sb.AppendLine("            else");
                sb.AppendLine("            {");
                if (prop.NestedCreateDtoName is not null)
                {
                    // Has ToEntity — can create from scratch
                    sb.AppendLine($"                if (target.{prop.SourcePropertyName} is null) target.{prop.SourcePropertyName} = {prop.PropertyName}.Value.ToEntity();");
                }
                else
                {
                    sb.AppendLine($"                if (target.{prop.SourcePropertyName} is not null)");
                }
                sb.AppendLine($"                {(prop.NestedCreateDtoName is not null ? "else " : "    ")}{prop.PropertyName}.Value.ApplyTo(target.{prop.SourcePropertyName});");
                sb.AppendLine("            }");
            }
            else
            {
                sb.AppendLine($"            if (target.{prop.SourcePropertyName} is not null) {prop.PropertyName}.Value!.ApplyTo(target.{prop.SourcePropertyName});");
            }
            sb.AppendLine("        }");
        }
        sb.AppendLine("    }");
    }

    private static void GenerateDiffMethod(StringBuilder sb, List<DtoPropertyInfo> props, string typeName)
    {
        sb.AppendLine($"    public List<string> Diff({typeName} entity)");
        sb.AppendLine("    {");
        sb.AppendLine("        var changes = new List<string>();");
        foreach (var prop in props)
        {
            // Skip nested DTO props — can't compare different types
            if (prop.NestedUpdateDtoName is not null || prop.NestedCreateDtoName is not null)
            {
                sb.AppendLine($"        if ({prop.PropertyName}.HasValue)");
                sb.AppendLine($"            changes.Add(\"{prop.JsonName}\");");
                continue;
            }
            sb.AppendLine($"        if ({prop.PropertyName}.HasValue && !System.Collections.Generic.EqualityComparer<{prop.DisplayTypeName}>.Default.Equals({prop.PropertyName}.Value, entity.{prop.SourcePropertyName}))");
            sb.AppendLine($"            changes.Add(\"{prop.JsonName}\");");
        }
        sb.AppendLine("        return changes;");
        sb.AppendLine("    }");
    }

    private static string GenerateResponseDtoSource(ResponseDtoInfo info)
    {
        var sb = new StringBuilder();
        sb.AppendLine("// <auto-generated />");
        sb.AppendLine($"// Generated by ZibStack.NET.Dto from [ResponseDto] on {info.ClassName}");
        sb.AppendLine("#nullable enable");
        sb.AppendLine();
        sb.AppendLine("using System;");
        sb.AppendLine("using System.Linq;");
        sb.AppendLine();

        if (info.Namespace is not null)
        {
            sb.AppendLine($"namespace {info.Namespace};");
            sb.AppendLine();
        }

        sb.AppendLine($"public record {info.ResponseName}");
        sb.AppendLine("{");

        // Properties — plain, no PatchField
        foreach (var prop in info.Properties)
        {
            EmitValidationAttributes(sb, prop.ValidationAttributes);
            var needsDefault = !prop.DisplayTypeName.EndsWith("?") && !IsValueTypeName(prop.DisplayTypeName);
            sb.AppendLine($"    public {prop.DisplayTypeName} {prop.PropertyName} {{ get; init; }}{(needsDefault ? " = default!;" : "")}");
        }

        sb.AppendLine();

        // FromEntity
        sb.AppendLine($"    public static {info.ResponseName} FromEntity({info.ClassName} entity)");
        sb.AppendLine("    {");
        sb.AppendLine($"        return new {info.ResponseName}");
        sb.AppendLine("        {");
        foreach (var prop in info.Properties)
        {
            if (prop.IsFlattened)
            {
                sb.AppendLine($"            {prop.PropertyName} = {prop.FlattenSource},");
                continue;
            }

            if (!prop.IsNestedResponse)
            {
                sb.AppendLine($"            {prop.PropertyName} = entity.{prop.PropertyName},");
                continue;
            }

            if (prop.IsNullable)
                sb.AppendLine($"            {prop.PropertyName} = entity.{prop.PropertyName} is not null ? {prop.NestedResponseName}.FromEntity(entity.{prop.PropertyName}) : null,");
            else
                sb.AppendLine($"            {prop.PropertyName} = {prop.NestedResponseName}.FromEntity(entity.{prop.PropertyName}),");
        }
        sb.AppendLine("        };");
        sb.AppendLine("    }");
        sb.AppendLine();

        // ProjectFrom (IQueryable)
        sb.AppendLine($"    public static IQueryable<{info.ResponseName}> ProjectFrom(IQueryable<{info.ClassName}> query)");
        sb.AppendLine("    {");
        sb.AppendLine($"        return query.Select(x => new {info.ResponseName}");
        sb.AppendLine("        {");
        foreach (var prop in info.Properties)
        {
            if (prop.IsFlattened)
            {
                sb.AppendLine($"            {prop.PropertyName} = {prop.FlattenProjection},");
                continue;
            }

            if (!prop.IsNestedResponse)
                sb.AppendLine($"            {prop.PropertyName} = x.{prop.PropertyName},");
        }
        sb.AppendLine("        });");
        sb.AppendLine("    }");

        sb.AppendLine("}");
        return sb.ToString();
    }

    private static string GenerateQueryDtoSource(QueryDtoInfo info)
    {
        var sb = new StringBuilder();
        sb.AppendLine("// <auto-generated />");
        sb.AppendLine($"// Generated by ZibStack.NET.Dto from [QueryDto] on {info.ClassName}");
        sb.AppendLine("#nullable enable");
        sb.AppendLine();
        sb.AppendLine("using System;");
        sb.AppendLine("using System.Linq;");
        if (info.Sortable)
            sb.AppendLine("using ZibStack.NET.Dto;");
        sb.AppendLine();

        if (info.Namespace is not null)
        {
            sb.AppendLine($"namespace {info.Namespace};");
            sb.AppendLine();
        }

        sb.AppendLine($"public record {info.QueryName}");
        sb.AppendLine("{");

        // All properties nullable
        foreach (var prop in info.Properties)
        {
            sb.AppendLine($"    public {prop.NullableTypeName} {prop.PropertyName} {{ get; init; }}");
        }

        // Sorting properties
        if (info.Sortable)
        {
            sb.AppendLine();
            sb.AppendLine("    /// <summary>Property name to sort by. Case-insensitive.</summary>");
            sb.AppendLine("    public string? SortBy { get; init; }");
            sb.AppendLine("    /// <summary>Sort direction. Defaults to Asc.</summary>");
            sb.AppendLine("    public SortDirection? SortDirection { get; init; }");
        }

        sb.AppendLine();

        // ApplyFilter
        sb.AppendLine($"    public IQueryable<{info.ClassName}> ApplyFilter(IQueryable<{info.ClassName}> query)");
        sb.AppendLine("    {");
        foreach (var prop in info.Properties)
        {
            sb.AppendLine($"        if ({prop.PropertyName} is not null)");
            if (prop.IsValueType)
                sb.AppendLine($"            query = query.Where(x => x.{prop.PropertyName} == {prop.PropertyName}.Value);");
            else
                sb.AppendLine($"            query = query.Where(x => x.{prop.PropertyName} == {prop.PropertyName});");
        }
        sb.AppendLine("        return query;");
        sb.AppendLine("    }");

        // ApplySort
        if (info.Sortable)
        {
            sb.AppendLine();
            sb.AppendLine($"    public IQueryable<{info.ClassName}> ApplySort(IQueryable<{info.ClassName}> query)");
            sb.AppendLine("    {");

            var defaultSortStr = info.DefaultSort is not null ? $"\"{info.DefaultSort}\"" : "null";
            var defaultDirStr = info.DefaultSortDirection == 1 ? "ZibStack.NET.Dto.SortDirection.Desc" : "ZibStack.NET.Dto.SortDirection.Asc";
            sb.AppendLine($"        var sortBy = SortBy ?? {defaultSortStr};");
            sb.AppendLine($"        var direction = SortDirection ?? {defaultDirStr};");
            sb.AppendLine();
            sb.AppendLine("        if (sortBy is null) return query;");
            sb.AppendLine();
            sb.AppendLine("        return (sortBy.ToLowerInvariant(), direction) switch");
            sb.AppendLine("        {");
            foreach (var prop in info.Properties)
            {
                var lower = prop.PropertyName.ToLowerInvariant();
                sb.AppendLine($"            (\"{lower}\", ZibStack.NET.Dto.SortDirection.Asc) => query.OrderBy(x => x.{prop.PropertyName}),");
                sb.AppendLine($"            (\"{lower}\", ZibStack.NET.Dto.SortDirection.Desc) => query.OrderByDescending(x => x.{prop.PropertyName}),");
            }
            sb.AppendLine("            _ => query,");
            sb.AppendLine("        };");
            sb.AppendLine("    }");

            // Convenience: ApplyFilterAndSort
            sb.AppendLine();
            if (info.HasQueryDsl)
            {
                sb.AppendLine($"    /// <summary>Applies typed params or DSL filter/sort. DSL takes priority when provided.</summary>");
                sb.AppendLine($"    public IQueryable<{info.ClassName}> Apply(IQueryable<{info.ClassName}> query, string? filter = null, string? sort = null)");
                sb.AppendLine("    {");
                sb.AppendLine("        if (filter is not null || sort is not null)");
                sb.AppendLine("        {");
                sb.AppendLine($"            query = ApplyDslFilter(query, filter);");
                sb.AppendLine($"            query = ApplyDslSort(query, sort);");
                sb.AppendLine("        }");
                sb.AppendLine("        else");
                sb.AppendLine("        {");
                sb.AppendLine("            query = ApplyFilter(query);");
                sb.AppendLine("            query = ApplySort(query);");
                sb.AppendLine("        }");
                sb.AppendLine("        return query;");
                sb.AppendLine("    }");
            }
            else
            {
                sb.AppendLine($"    public IQueryable<{info.ClassName}> Apply(IQueryable<{info.ClassName}> query)");
                sb.AppendLine("    {");
                sb.AppendLine("        query = ApplyFilter(query);");
                sb.AppendLine("        query = ApplySort(query);");
                sb.AppendLine("        return query;");
                sb.AppendLine("    }");
            }
        }

        // DSL filter/sort methods (when ZibStack.NET.Query is referenced)
        if (info.HasQueryDsl)
        {
            sb.AppendLine();
            sb.AppendLine($"    /// <summary>Applies a filter DSL string with AND, OR, grouping, and IN support.</summary>");
            sb.AppendLine($"    public static IQueryable<{info.ClassName}> ApplyDslFilter(IQueryable<{info.ClassName}> query, string? filter)");
            sb.AppendLine("    {");
            sb.AppendLine("        if (string.IsNullOrWhiteSpace(filter)) return query;");
            sb.AppendLine("        var expr = ZibStack.NET.Query.FilterParser.ParseExpression(filter);");
            sb.AppendLine($"        return ZibStack.NET.Query.FilterApplier.ApplyTree(query, expr, clause => clause.Field.ToLowerInvariant() switch");
            sb.AppendLine("        {");
            foreach (var prop in info.Properties)
            {
                var lower = prop.PropertyName.ToLowerInvariant();
                sb.AppendLine($"            \"{lower}\" => ZibStack.NET.Query.FilterApplier.BuildPredicate<{info.ClassName}, {prop.OriginalTypeName}>(x => x.{prop.PropertyName}, clause),");
            }
            foreach (var nav in info.NavigationPaths)
            {
                sb.AppendLine($"            \"{nav.DotPath}\" => ZibStack.NET.Query.FilterApplier.BuildPredicate<{info.ClassName}, {nav.OriginalTypeName}>(x => x.{nav.ExpressionPath}, clause),");
            }
            sb.AppendLine("            _ => null,");
            sb.AppendLine("        });");
            sb.AppendLine("    }");

            sb.AppendLine();
            sb.AppendLine($"    /// <summary>Applies a sort string (e.g. \"-Name\" or \"Level desc\").</summary>");
            sb.AppendLine($"    public static IQueryable<{info.ClassName}> ApplyDslSort(IQueryable<{info.ClassName}> query, string? sort)");
            sb.AppendLine("    {");
            sb.AppendLine("        if (string.IsNullOrWhiteSpace(sort)) return query;");
            sb.AppendLine("        var clauses = ZibStack.NET.Query.SortParser.Parse(sort);");
            sb.AppendLine("        var first = true;");
            sb.AppendLine("        foreach (var clause in clauses)");
            sb.AppendLine("        {");
            sb.AppendLine("            query = (clause.Field.ToLowerInvariant(), clause.Descending, first) switch");
            sb.AppendLine("            {");
            foreach (var prop in info.Properties)
            {
                var lower = prop.PropertyName.ToLowerInvariant();
                sb.AppendLine($"                (\"{lower}\", false, true)  => query.OrderBy(x => x.{prop.PropertyName}),");
                sb.AppendLine($"                (\"{lower}\", true,  true)  => query.OrderByDescending(x => x.{prop.PropertyName}),");
                sb.AppendLine($"                (\"{lower}\", false, false) => ((IOrderedQueryable<{info.ClassName}>)query).ThenBy(x => x.{prop.PropertyName}),");
                sb.AppendLine($"                (\"{lower}\", true,  false) => ((IOrderedQueryable<{info.ClassName}>)query).ThenByDescending(x => x.{prop.PropertyName}),");
            }
            // Navigation property paths for sorting
            foreach (var nav in info.NavigationPaths)
            {
                sb.AppendLine($"                (\"{nav.DotPath}\", false, true)  => query.OrderBy(x => x.{nav.ExpressionPath}),");
                sb.AppendLine($"                (\"{nav.DotPath}\", true,  true)  => query.OrderByDescending(x => x.{nav.ExpressionPath}),");
                sb.AppendLine($"                (\"{nav.DotPath}\", false, false) => ((IOrderedQueryable<{info.ClassName}>)query).ThenBy(x => x.{nav.ExpressionPath}),");
                sb.AppendLine($"                (\"{nav.DotPath}\", true,  false) => ((IOrderedQueryable<{info.ClassName}>)query).ThenByDescending(x => x.{nav.ExpressionPath}),");
            }
            sb.AppendLine("                _ => query,");
            sb.AppendLine("            };");
            sb.AppendLine("            first = false;");
            sb.AppendLine("        }");
            sb.AppendLine("        return query;");
            sb.AppendLine("    }");

            // ─── ProjectFields: compile-time field projection ────────────
            sb.AppendLine();
            sb.AppendLine($"    /// <summary>Projects entity fields to a dictionary based on selected field names. No reflection.</summary>");
            sb.AppendLine($"    public static System.Collections.Generic.Dictionary<string, object?> ProjectFields({info.ClassName} entity, System.Collections.Generic.HashSet<string> fields)");
            sb.AppendLine("    {");
            sb.AppendLine("        var result = new System.Collections.Generic.Dictionary<string, object?>();");
            foreach (var prop in info.Properties)
            {
                var lower = prop.PropertyName.ToLowerInvariant();
                var jsonName = prop.JsonName;
                sb.AppendLine($"        if (fields.Contains(\"{lower}\")) result[\"{jsonName}\"] = entity.{prop.PropertyName};");
            }
            // Navigation sub-properties
            var navGroups = new Dictionary<string, List<QueryNavigationPath>>();
            foreach (var nav in info.NavigationPaths)
            {
                var dot = nav.DotPath.IndexOf('.');
                var prefix = nav.DotPath.Substring(0, dot);
                if (!navGroups.ContainsKey(prefix))
                    navGroups[prefix] = new List<QueryNavigationPath>();
                navGroups[prefix].Add(nav);
            }
            foreach (var kv in navGroups)
            {
                var navName = info.NavigationNames.FirstOrDefault(n => n.ToLowerInvariant() == kv.Key) ?? kv.Key;
                var jsonNavName = char.ToLowerInvariant(navName[0]) + navName.Substring(1);
                sb.AppendLine($"        if (fields.Any(f => f.StartsWith(\"{kv.Key}.\")))");
                sb.AppendLine("        {");
                sb.AppendLine($"            var nav = new System.Collections.Generic.Dictionary<string, object?>();");
                foreach (var nav in kv.Value)
                {
                    var subField = nav.DotPath.Substring(kv.Key.Length + 1);
                    var subExpr = nav.ExpressionPath.Substring(nav.ExpressionPath.IndexOf('.') + 1);
                    var jsonSubName = char.ToLowerInvariant(subExpr[0]) + subExpr.Substring(1);
                    sb.AppendLine($"            if (fields.Contains(\"{nav.DotPath}\")) nav[\"{jsonSubName}\"] = entity.{nav.ExpressionPath};");
                }
                sb.AppendLine($"            result[\"{jsonNavName}\"] = nav;");
                sb.AppendLine("        }");
            }
            sb.AppendLine("        return result;");
            sb.AppendLine("    }");

            // ─── ApplyIncludes: EF Core Include for navigation fields ────
            sb.AppendLine();
            sb.AppendLine($"    /// <summary>Adds EF Core Include() calls for navigation properties referenced in the select fields.</summary>");
            sb.AppendLine($"    public static IQueryable<{info.ClassName}> ApplyIncludes(IQueryable<{info.ClassName}> query, System.Collections.Generic.HashSet<string> fields)");
            sb.AppendLine("    {");
            if (info.HasEfCore)
            {
                foreach (var navName in info.NavigationNames)
                {
                    var lower = navName.ToLowerInvariant();
                    sb.AppendLine($"        if (fields.Any(f => f.StartsWith(\"{lower}.\")))");
                    sb.AppendLine($"            query = Microsoft.EntityFrameworkCore.EntityFrameworkQueryableExtensions.Include(query, x => x.{navName});");
                }
            }
            sb.AppendLine("        return query;");
            sb.AppendLine("    }");
        }

        sb.AppendLine("}");
        return sb.ToString();
    }

    private static void GenerateApplyWithChangesMethod(StringBuilder sb, List<DtoPropertyInfo> props, string typeName)
    {
        sb.AppendLine($"    public (System.Collections.Generic.List<string> ChangedFields, {typeName} Entity) ApplyWithChanges({typeName} target)");
        sb.AppendLine("    {");
        sb.AppendLine("        var changed = new System.Collections.Generic.List<string>();");
        foreach (var prop in props)
        {
            if (prop.IsImmutable) continue;

            if (prop.NestedUpdateDtoName is null)
            {
                sb.AppendLine($"        if ({prop.PropertyName}.HasValue)");
                sb.AppendLine("        {");
                sb.AppendLine($"            if (!System.Collections.Generic.EqualityComparer<{prop.DisplayTypeName}>.Default.Equals({prop.PropertyName}.Value, target.{prop.SourcePropertyName}))");
                sb.AppendLine($"                changed.Add(\"{prop.JsonName}\");");
                sb.AppendLine($"            target.{prop.SourcePropertyName} = {prop.PropertyName}.Value{(NeedsNullForgiving(prop) ? "!" : "")};");
                sb.AppendLine("        }");
                continue;
            }

            // Nested — just track that it was sent, delegate to ApplyTo
            sb.AppendLine($"        if ({prop.PropertyName}.HasValue)");
            sb.AppendLine("        {");
            sb.AppendLine($"            changed.Add(\"{prop.JsonName}\");");
            if (prop.IsNullable)
            {
                sb.AppendLine($"            if ({prop.PropertyName}.Value is null)");
                sb.AppendLine($"                target.{prop.SourcePropertyName} = null;");
                sb.AppendLine("            else");
                sb.AppendLine("            {");
                if (prop.NestedCreateDtoName is not null)
                {
                    sb.AppendLine($"                if (target.{prop.SourcePropertyName} is null) target.{prop.SourcePropertyName} = {prop.PropertyName}.Value.ToEntity();");
                    sb.AppendLine($"                else {prop.PropertyName}.Value.ApplyTo(target.{prop.SourcePropertyName});");
                }
                else
                {
                    sb.AppendLine($"                if (target.{prop.SourcePropertyName} is not null) {prop.PropertyName}.Value.ApplyTo(target.{prop.SourcePropertyName});");
                }
                sb.AppendLine("            }");
            }
            else
            {
                sb.AppendLine($"            {prop.PropertyName}.Value!.ApplyTo(target.{prop.SourcePropertyName});");
            }
            sb.AppendLine("        }");
        }
        sb.AppendLine("        return (changed, target);");
        sb.AppendLine("    }");
    }

    private static void GenerateFluentBaseValidator(StringBuilder sb, DtoClassInfo classInfo, bool isCreate, bool combined)
    {
        var requestName = combined
            ? classInfo.RequestName
            : (isCreate ? classInfo.RequestName : classInfo.RequestName);
        var validatorSuffix = isCreate ? "CreateBaseValidator" : "UpdateBaseValidator";
        var props = isCreate
            ? classInfo.Properties.Where(p => !p.IsUpdateOnly).ToList()
            : classInfo.Properties.Where(p => !p.IsCreateOnly).ToList();

        sb.AppendLine($"public class {requestName}{validatorSuffix} : ZibStack.NET.Dto.FluentDtoValidator<{requestName}>");
        sb.AppendLine("{");
        sb.AppendLine($"    public {requestName}{validatorSuffix}()");
        sb.AppendLine("    {");

        foreach (var prop in props)
        {
            var jsonName = prop.JsonName;

            if (isCreate && prop.IsRequired)
            {
                sb.AppendLine($"        RuleFor(x => x.{prop.PropertyName}).Must(f => f.HasValue).WithMessage(\"Property '{jsonName}' is required.\");");

                if (NeedsNullCheck(prop))
                {
                    sb.AppendLine($"        RuleFor(x => x.{prop.PropertyName}).Must(f => !f.HasValue || f.Value is not null).WithMessage(\"Property '{jsonName}' cannot be null.\");");
                }
            }
            else if (NeedsNullCheck(prop))
            {
                sb.AppendLine($"        RuleFor(x => x.{prop.PropertyName}).Must(f => !f.HasValue || f.Value is not null).WithMessage(\"Property '{jsonName}' cannot be null.\");");
            }
        }

        sb.AppendLine("    }");
        sb.AppendLine("}");
    }

