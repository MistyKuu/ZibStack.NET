    private const string ETagSource = @"// <auto-generated />
#nullable enable

namespace ZibStack.NET.Dto
{
    /// <summary>Weak-ETag helpers used by generated CRUD endpoints with [CrudApi(Concurrency = true)].</summary>
    public static class ETag
    {
        /// <summary>Formats a row version as a weak ETag: <c>W/""42""</c>.</summary>
        public static string Format(long version) => $""W/\""{version}\"""";

        /// <summary>
        /// Returns true when the If-Match header value matches the current version.
        /// Accepts <c>*</c>, <c>W/""n""</c>, <c>""n""</c> and bare <c>n</c>; comma-separated lists
        /// match when any entry matches (RFC 9110 If-Match semantics).
        /// </summary>
        public static bool Matches(string? ifMatch, long version)
        {
            if (string.IsNullOrWhiteSpace(ifMatch)) return false;
            foreach (var raw in ifMatch!.Split(','))
            {
                var tag = raw.Trim();
                if (tag == ""*"") return true;
                if (tag.StartsWith(""W/"", System.StringComparison.OrdinalIgnoreCase)) tag = tag.Substring(2);
                tag = tag.Trim('""');
                if (long.TryParse(tag, out var v) && v == version) return true;
            }
            return false;
        }
    }
}
