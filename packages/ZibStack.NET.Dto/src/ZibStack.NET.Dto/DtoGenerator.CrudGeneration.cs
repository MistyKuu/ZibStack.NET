    private static void ScanForCrudApi(INamespaceSymbol ns, List<CrudTestInfo> results)
    {
        foreach (var type in ns.GetTypeMembers())
        {
            foreach (var attr in type.GetAttributes())
            {
                var attrName = attr.AttributeClass?.Name;
                if (attrName != "CrudApiAttribute") continue;

                var info = new CrudTestInfo
                {
                    ClassName = type.Name,
                    Namespace = type.ContainingNamespace.IsGlobalNamespace ? null : type.ContainingNamespace.ToDisplayString(),
                };

                foreach (var arg in attr.NamedArguments)
                {
                    switch (arg.Key)
                    {
                        case "Route": info.Route = arg.Value.Value as string ?? ""; break;
                        case "Operations": if (arg.Value.Value is int ops) info.Operations = ops; break;
                        case "SoftDelete": if (arg.Value.Value is true) info.SoftDelete = true; break;
                        case "KeyProperty":
                            var kp = arg.Value.Value as string ?? "Id";
                            var prop = type.GetMembers(kp).OfType<IPropertySymbol>().FirstOrDefault();
                            if (prop is not null) info.KeyType = prop.Type.ToDisplayString();
                            break;
                    }
                }

                if (string.IsNullOrEmpty(info.Route))
                    info.Route = "api/" + Pluralize(CamelCase(type.Name));

                // Resolve key type from default "Id" property if not set via KeyProperty
                if (info.KeyType == "int")
                {
                    var idProp = type.GetMembers("Id").OfType<IPropertySymbol>().FirstOrDefault();
                    if (idProp is not null) info.KeyType = idProp.Type.ToDisplayString();
                }

                // Collect public instance properties for test data generation
                foreach (var member in type.GetMembers().OfType<IPropertySymbol>())
                {
                    if (member.IsStatic || member.DeclaredAccessibility != Accessibility.Public) continue;
                    if (member.GetMethod is null) continue;
                    var isKey = member.Name == info.KeyPropertyName;
                    var hasSetter = member.SetMethod is not null;
                    var isComputed = !hasSetter || member.IsReadOnly;
                    var typeName = member.Type.ToDisplayString();
                    var isNullable = member.Type.NullableAnnotation == NullableAnnotation.Annotated
                                 || typeName.EndsWith("?");

                    var propInfo = new CrudTestPropertyInfo
                    {
                        Name = member.Name,
                        CSharpType = typeName.TrimEnd('?'),
                        IsNullable = isNullable,
                        IsKey = isKey,
                        IsComputed = isComputed,
                    };

                    // Read validation attributes for smart Faker mapping
                    foreach (var pa in member.GetAttributes())
                    {
                        var valAttrName = pa.AttributeClass?.Name;
                        if (valAttrName is "ZEmailAttribute" or "EmailAddressAttribute")
                            propInfo.HasEmailAttr = true;
                        else if (valAttrName is "ZUrlAttribute" or "UrlAttribute")
                            propInfo.HasUrlAttr = true;
                        else if (valAttrName is "ZRangeAttribute" or "RangeAttribute")
                        {
                            if (pa.ConstructorArguments.Length >= 2)
                            {
                                if (pa.ConstructorArguments[0].Value is int rmin) propInfo.RangeMin = rmin;
                                if (pa.ConstructorArguments[1].Value is int rmax) propInfo.RangeMax = rmax;
                            }
                        }
                        else if (valAttrName is "ZMinLengthAttribute" or "MinLengthAttribute")
                        {
                            if (pa.ConstructorArguments.Length >= 1 && pa.ConstructorArguments[0].Value is int ml)
                                propInfo.MinLength = ml;
                        }
                        else if (valAttrName is "ZMaxLengthAttribute" or "MaxLengthAttribute" or "StringLengthAttribute")
                        {
                            if (pa.ConstructorArguments.Length >= 1 && pa.ConstructorArguments[0].Value is int xl)
                                propInfo.MaxLength = xl;
                        }
                    }

                    info.Properties.Add(propInfo);
                }

                results.Add(info);
            }
        }
        foreach (var sub in ns.GetNamespaceMembers())
            ScanForCrudApi(sub, results);
    }

