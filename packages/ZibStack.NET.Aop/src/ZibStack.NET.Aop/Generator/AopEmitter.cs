using System.Collections.Generic;
using System.Linq;
using System.Text;

namespace ZibStack.NET.Aop.Generator;

public static class AopEmitter
{
    public static string Emit(
        InterceptedClassModel classModel,
        IReadOnlyList<CallSiteModel> callSites,
        IReadOnlyDictionary<string, IAspectEmitter> emitters)
    {
        var sb = new StringBuilder();
        sb.AppendLine("// <auto-generated/>");
        sb.AppendLine("#nullable enable");
        sb.AppendLine("#pragma warning disable CS8603");
        sb.AppendLine();

        // Collect usings from all emitters used
        var usings = new HashSet<string>
        {
            "System", "System.Diagnostics", "System.Runtime.CompilerServices"
        };
        foreach (var method in classModel.Methods)
        {
            foreach (var aspect in method.Aspects)
            {
                if (emitters.TryGetValue(aspect.AttributeFullName, out var emitter))
                {
                    foreach (var u in emitter.RequiredUsings)
                        usings.Add(u);
                }
            }
            // Runtime handlers need Aop namespace
            if (method.Aspects.Any(a => a.HandlerTypeName != null))
                usings.Add("ZibStack.NET.Aop");
        }

        foreach (var u in usings.OrderBy(x => x))
            sb.AppendLine($"using {u};");
        sb.AppendLine();

        if (!string.IsNullOrEmpty(classModel.Namespace))
        {
            sb.AppendLine($"using {classModel.Namespace};");
            sb.AppendLine();
        }

        // InterceptsLocation attribute
        sb.AppendLine("namespace System.Runtime.CompilerServices");
        sb.AppendLine("{");
        sb.AppendLine("    [global::System.AttributeUsage(global::System.AttributeTargets.Method, AllowMultiple = true)]");
        sb.AppendLine("    file sealed class InterceptsLocationAttribute : global::System.Attribute");
        sb.AppendLine("    {");
        sb.AppendLine("        public InterceptsLocationAttribute(int version, string data) { }");
        sb.AppendLine("    }");
        sb.AppendLine("}");
        sb.AppendLine();

        sb.AppendLine("namespace ZibStack.Generated");
        sb.AppendLine("{");
        sb.AppendLine();
        sb.AppendLine($"    [global::System.ComponentModel.EditorBrowsable(global::System.ComponentModel.EditorBrowsableState.Never)]");
        sb.AppendLine($"    internal static class __{classModel.ClassName}_Aop");
        sb.AppendLine("    {");

        // Per-method: class members from emitters
        foreach (var method in classModel.Methods)
        {
            foreach (var aspect in method.Aspects)
            {
                if (emitters.TryGetValue(aspect.AttributeFullName, out var emitter))
                    emitter.EmitClassMembers(sb, classModel, method, aspect, "        ");
            }
            sb.AppendLine();
        }

        // Per-method: interceptor methods
        foreach (var method in classModel.Methods)
        {
            var methodCallSites = callSites
                .Where(cs => cs.MethodName == method.MethodName
                    && cs.ContainingClassName == classModel.ClassName)
                .ToList();

            if (methodCallSites.Count == 0)
                continue;

            EmitInterceptorMethod(sb, classModel, method, methodCallSites, emitters);
            sb.AppendLine();
        }

        sb.AppendLine("    }");
        sb.AppendLine("}");

        return sb.ToString();
    }

    private static void EmitInterceptorMethod(
        StringBuilder sb,
        InterceptedClassModel classModel,
        InterceptedMethodModel method,
        List<CallSiteModel> callSites,
        IReadOnlyDictionary<string, IAspectEmitter> emitters)
    {
        // InterceptsLocation attributes
        foreach (var cs in callSites)
            sb.AppendLine($"        {cs.InterceptsLocationAttributeSyntax}");

        // Method signature
        var asyncKeyword = method.IsAsync ? "async " : "";
        var paramList = string.Join(", ",
            method.Parameters.Select(p => $"{p.FullyQualifiedType} {p.Name}"));
        var fullParamList = paramList.Length > 0
            ? $"this {classModel.ClassName} @this, {paramList}"
            : $"this {classModel.ClassName} @this";

        sb.AppendLine($"        internal static {asyncKeyword}{method.ReturnType} {method.MethodName}_Aop({fullParamList})");
        sb.AppendLine("        {");

        var indent = "            ";

        // Separate aspects into before/after handlers and around handlers
        var beforeAfterAspects = method.Aspects.Where(a => !a.IsAroundHandler).ToList();
        var aroundAspects = method.Aspects.Where(a => a.IsAroundHandler).ToList();

        // Emit OnBefore for non-around aspects (ascending order)
        foreach (var aspect in beforeAfterAspects)
        {
            if (emitters.TryGetValue(aspect.AttributeFullName, out var emitter))
                emitter.EmitBefore(sb, classModel, method, aspect, indent);
            else if (aspect.HandlerTypeName != null)
                EmitRuntimeHandlerBefore(sb, classModel, method, aspect, indent);
        }

        sb.AppendLine();
        sb.AppendLine($"{indent}var __sw = global::System.Diagnostics.Stopwatch.StartNew();");

        // Build the proceed chain for around handlers
        var callArgs = string.Join(", ", method.Parameters.Select(p => p.Name));
        var originalCall = $"@this.{method.MethodName}({callArgs})";

        if (aroundAspects.Count > 0)
        {
            // Build nested Around calls: outermost around wraps inner, innermost wraps original call
            if (method.IsAsync)
            {
                // Async: Func<ValueTask<object?>>
                sb.AppendLine($"{indent}global::System.Func<global::System.Threading.Tasks.ValueTask<object?>> __proceed = async () =>");
                sb.AppendLine($"{indent}{{");
                if (method.ReturnsVoid)
                {
                    sb.AppendLine($"{indent}    await {originalCall}.ConfigureAwait(false);");
                    sb.AppendLine($"{indent}    return null;");
                }
                else
                    sb.AppendLine($"{indent}    return await {originalCall}.ConfigureAwait(false);");
                sb.AppendLine($"{indent}}};");

                // Wrap with each around handler (innermost first, outermost last)
                foreach (var aspect in aroundAspects)
                {
                    var handlerVar = GetHandlerVarName(aspect);
                    var ctxVar = GetContextVarName(aspect);
                    sb.AppendLine($"{indent}var __prevProceed_{handlerVar} = __proceed;");
                    if (aspect.IsAsyncAroundHandler)
                        sb.AppendLine($"{indent}__proceed = () => {handlerVar}.AroundAsync({ctxVar}, __prevProceed_{handlerVar});");
                    else
                        sb.AppendLine($"{indent}__proceed = () => new global::System.Threading.Tasks.ValueTask<object?>({handlerVar}.Around({ctxVar}, () => __prevProceed_{handlerVar}().GetAwaiter().GetResult()));");
                }

                // Execute the chain
                sb.AppendLine($"{indent}try");
                sb.AppendLine($"{indent}{{");
                if (method.ReturnsVoid)
                    sb.AppendLine($"{indent}    await __proceed().ConfigureAwait(false);");
                else
                    sb.AppendLine($"{indent}    var __result = ({method.ReturnType})await __proceed().ConfigureAwait(false);");
            }
            else
            {
                // Sync: Func<object?>
                sb.AppendLine($"{indent}global::System.Func<object?> __proceed = () =>");
                sb.AppendLine($"{indent}{{");
                if (method.ReturnsVoid)
                {
                    sb.AppendLine($"{indent}    {originalCall};");
                    sb.AppendLine($"{indent}    return null;");
                }
                else
                    sb.AppendLine($"{indent}    return {originalCall};");
                sb.AppendLine($"{indent}}};");

                foreach (var aspect in aroundAspects)
                {
                    var handlerVar = GetHandlerVarName(aspect);
                    var ctxVar = GetContextVarName(aspect);
                    sb.AppendLine($"{indent}var __prevProceed_{handlerVar} = __proceed;");
                    sb.AppendLine($"{indent}__proceed = () => {handlerVar}.Around({ctxVar}, __prevProceed_{handlerVar});");
                }

                sb.AppendLine($"{indent}try");
                sb.AppendLine($"{indent}{{");
                if (method.ReturnsVoid)
                    sb.AppendLine($"{indent}    __proceed();");
                else
                    sb.AppendLine($"{indent}    var __result = ({method.ReturnType})__proceed()!;");
            }
        }
        else
        {
            // No around handlers — direct call (original behavior)
            sb.AppendLine($"{indent}try");
            sb.AppendLine($"{indent}{{");

            if (method.IsAsync)
                originalCall = $"await {originalCall}.ConfigureAwait(false)";

            if (method.ReturnsVoid)
                sb.AppendLine($"{indent}    {originalCall};");
            else
                sb.AppendLine($"{indent}    var __result = {originalCall};");
        }

        sb.AppendLine($"{indent}    __sw.Stop();");

        // OnAfter for non-around aspects (reversed)
        foreach (var aspect in beforeAfterAspects.AsEnumerable().Reverse())
        {
            if (emitters.TryGetValue(aspect.AttributeFullName, out var emitter))
                emitter.EmitAfter(sb, classModel, method, aspect, indent + "    ");
            else if (aspect.HandlerTypeName != null)
                EmitRuntimeHandlerAfter(sb, method, aspect, indent + "    ");
        }

        if (!method.ReturnsVoid)
            sb.AppendLine($"{indent}    return __result;");

        sb.AppendLine($"{indent}}}");
        sb.AppendLine($"{indent}catch (global::System.Exception __ex)");
        sb.AppendLine($"{indent}{{");
        sb.AppendLine($"{indent}    __sw.Stop();");

        // OnException for non-around aspects (reversed)
        foreach (var aspect in beforeAfterAspects.AsEnumerable().Reverse())
        {
            if (emitters.TryGetValue(aspect.AttributeFullName, out var emitter))
                emitter.EmitOnException(sb, classModel, method, aspect, indent + "    ");
            else if (aspect.HandlerTypeName != null)
                EmitRuntimeHandlerOnException(sb, method, aspect, indent + "    ");
        }

        sb.AppendLine($"{indent}    throw;");
        sb.AppendLine($"{indent}}}");
        sb.AppendLine("        }");
    }

    // === Runtime handler emission (for [AspectHandler]-based aspects) ===

    private static string GetHandlerVarName(AspectInfo aspect)
    {
        var safeName = aspect.AttributeFullName.Replace(".", "_").Replace("+", "_");
        return $"__handler_{safeName}";
    }

    private static string GetContextVarName(AspectInfo aspect)
    {
        var safeName = aspect.AttributeFullName.Replace(".", "_").Replace("+", "_");
        return $"__ctx_{safeName}";
    }

    private static void EmitRuntimeHandlerBefore(StringBuilder sb, InterceptedClassModel classModel, InterceptedMethodModel method, AspectInfo aspect, string indent)
    {
        var handlerVar = GetHandlerVarName(aspect);
        var ctxVar = GetContextVarName(aspect);

        sb.AppendLine($"{indent}var {handlerVar} = new {aspect.HandlerTypeName}();");
        sb.AppendLine($"{indent}var {ctxVar} = new global::ZibStack.NET.Aop.AspectContext");
        sb.AppendLine($"{indent}{{");
        sb.AppendLine($"{indent}    ClassName = \"{classModel.ClassName}\",");
        sb.AppendLine($"{indent}    MethodName = \"{method.MethodName}\",");

        // Build parameters array
        sb.AppendLine($"{indent}    Parameters = new global::ZibStack.NET.Aop.AspectParameterInfo[]");
        sb.AppendLine($"{indent}    {{");
        foreach (var p in method.Parameters)
        {
            var sensitive = p.IsSensitive ? "true" : "false";
            var noLog = p.IsNoLog ? "true" : "false";
            var value = p.IsNoLog ? "null" : p.Name;
            sb.AppendLine($"{indent}        new() {{ Name = \"{p.Name}\", Value = {value}, IsSensitive = {sensitive}, IsNoLog = {noLog} }},");
        }
        sb.AppendLine($"{indent}    }}");
        sb.AppendLine($"{indent}}};");
        if (aspect.IsAsyncHandler)
        {
            if (method.IsAsync)
                sb.AppendLine($"{indent}await {handlerVar}.OnBeforeAsync({ctxVar}).ConfigureAwait(false);");
            else
                sb.AppendLine($"{indent}// ERROR: IAsyncAspectHandler cannot be used on sync method. Make the method async or use IAspectHandler.");
        }
        else
            sb.AppendLine($"{indent}{handlerVar}.OnBefore({ctxVar});");
    }

    private static void EmitRuntimeHandlerAfter(StringBuilder sb, InterceptedMethodModel method, AspectInfo aspect, string indent)
    {
        var handlerVar = GetHandlerVarName(aspect);
        var ctxVar = GetContextVarName(aspect);

        sb.AppendLine($"{indent}{ctxVar}.ElapsedMilliseconds = __sw.ElapsedMilliseconds;");
        if (!method.ReturnsVoid)
            sb.AppendLine($"{indent}{ctxVar}.ReturnValue = __result;");
        if (aspect.IsAsyncHandler)
        {
            if (method.IsAsync)
                sb.AppendLine($"{indent}await {handlerVar}.OnAfterAsync({ctxVar}).ConfigureAwait(false);");
            else
                sb.AppendLine($"{indent}// ERROR: IAsyncAspectHandler cannot be used on sync method.");
        }
        else
            sb.AppendLine($"{indent}{handlerVar}.OnAfter({ctxVar});");
    }

    private static void EmitRuntimeHandlerOnException(StringBuilder sb, InterceptedMethodModel method, AspectInfo aspect, string indent)
    {
        var handlerVar = GetHandlerVarName(aspect);
        var ctxVar = GetContextVarName(aspect);

        sb.AppendLine($"{indent}{ctxVar}.ElapsedMilliseconds = __sw.ElapsedMilliseconds;");
        if (aspect.IsAsyncHandler)
        {
            if (method.IsAsync)
                sb.AppendLine($"{indent}await {handlerVar}.OnExceptionAsync({ctxVar}, __ex).ConfigureAwait(false);");
            else
                sb.AppendLine($"{indent}// ERROR: IAsyncAspectHandler cannot be used on sync method.");
        }
        else
            sb.AppendLine($"{indent}{handlerVar}.OnException({ctxVar}, __ex);");
    }
}
