using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Text.RegularExpressions;

namespace ZibStack.NET.TypeGen.Generator;

internal static class GraphQLEmitter
{
    public static IReadOnlyList<EmittedFile> Emit(SchemaModel model, GlobalSettings settings)
    {
        var gql = settings.GraphQL;
        var files = new List<EmittedFile>();

        if (gql.SingleFile)
        {
            var sb = new StringBuilder();
            if (gql.EmitGeneratedBanner)
                sb.AppendLine("# @generated by ZibStack.NET.TypeGen — do not edit");

            foreach (var en in model.Enums)
            {
                if ((en.Targets & TypeTarget.GraphQL) == 0) continue;
                sb.AppendLine();
                EmitEnum(sb, en);
            }

            foreach (var cls in model.Classes)
            {
                if (cls.TsIgnore || (cls.Targets & TypeTarget.GraphQL) == 0) continue;
                sb.AppendLine();
                EmitType(sb, cls, model);
            }

            files.Add(new EmittedFile(
                Target: TypeTarget.GraphQL,
                OutputDir: ResolveOutputDir(gql.OutputDir, model),
                FileName: gql.SingleFileName,
                Content: sb.ToString()));
        }
        else
        {
            var globalGqlDir = gql.OutputDir;
            foreach (var en in model.Enums)
            {
                if ((en.Targets & TypeTarget.GraphQL) == 0) continue;
                var sb = new StringBuilder();
                if (gql.EmitGeneratedBanner)
                    sb.AppendLine("# @generated by ZibStack.NET.TypeGen — do not edit");
                sb.AppendLine();
                EmitEnum(sb, en);
                files.Add(new EmittedFile(
                    Target: TypeTarget.GraphQL,
                    OutputDir: en.HasExplicitOutputDir ? en.OutputDir : !string.IsNullOrEmpty(globalGqlDir) ? globalGqlDir : en.OutputDir,
                    FileName: (en.EmittedName ?? en.SourceName) + ".graphql",
                    Content: sb.ToString()));
            }

            foreach (var cls in model.Classes)
            {
                if (cls.TsIgnore || (cls.Targets & TypeTarget.GraphQL) == 0) continue;
                var sb = new StringBuilder();
                if (gql.EmitGeneratedBanner)
                    sb.AppendLine("# @generated by ZibStack.NET.TypeGen — do not edit");
                sb.AppendLine();
                EmitType(sb, cls, model);
                files.Add(new EmittedFile(
                    Target: TypeTarget.GraphQL,
                    OutputDir: cls.HasExplicitOutputDir ? cls.OutputDir : !string.IsNullOrEmpty(globalGqlDir) ? globalGqlDir : cls.OutputDir,
                    FileName: (cls.EmittedName ?? cls.SourceName) + ".graphql",
                    Content: sb.ToString()));
            }
        }

        return files;
    }

    private static void EmitType(StringBuilder sb, SchemaClass cls, SchemaModel model)
    {
        var name = cls.EmittedName ?? cls.SourceName;
        var keyword = cls.IsInterface ? "interface" : "type";

        // Inheritance
        var implements = "";
        if (cls.BaseClassFullName is not null)
        {
            var baseName = model.Classes
                .FirstOrDefault(c => c.CSharpFullName == cls.BaseClassFullName)
                ?.EmittedName;
            if (baseName is not null)
                implements = $" implements {baseName}";
        }

        sb.AppendLine($"{keyword} {name}{implements} {{");

        foreach (var prop in cls.Properties)
        {
            if (prop.TsIgnore || prop.OpenApiIgnore) continue;
            var gqlType = MapType(prop.CSharpTypeFullName, prop.IsNullable, prop.IsExplicitlyRequired, model);
            var fieldName = ToCamelCase(prop.SourceName);
            sb.AppendLine($"  {fieldName}: {gqlType}");
        }

        sb.AppendLine("}");
    }

    private static void EmitEnum(StringBuilder sb, SchemaEnum en)
    {
        var name = en.EmittedName ?? en.SourceName;
        sb.AppendLine($"enum {name} {{");
        foreach (var m in en.Members)
            sb.AppendLine($"  {ToScreamingSnake(m.Name)}");
        sb.AppendLine("}");
    }

    private static string MapType(string csharpType, bool isNullable, bool isExplicitlyRequired, SchemaModel model)
    {
        var t = csharpType.TrimEnd('?');
        var bang = (!isNullable || isExplicitlyRequired) ? "!" : "";

        // Collections
        var listInner = ExtractGeneric(t, "List", "IList", "ICollection", "IEnumerable",
            "IReadOnlyList", "IReadOnlyCollection");
        if (listInner is not null)
            return $"[{MapType(listInner, false, false, model)}{(isNullable ? "" : "!")}]{bang}";
        if (t.EndsWith("[]"))
            return $"[{MapType(t.Substring(0, t.Length - 2), false, false, model)}!]{bang}";

        // Dictionary → JSON scalar (GraphQL doesn't have maps natively)
        if (t.StartsWith("Dictionary<") || t.StartsWith("IDictionary<") || t.StartsWith("IReadOnlyDictionary<"))
            return "JSON" + bang;

        // Primitives
        var mapped = t switch
        {
            "int" or "System.Int32" or "short" or "System.Int16" or "byte" or "System.Byte" => "Int",
            "long" or "System.Int64" => "Int",
            "float" or "System.Single" or "double" or "System.Double" or "decimal" or "System.Decimal" => "Float",
            "string" or "System.String" => "String",
            "bool" or "System.Boolean" => "Boolean",
            "System.Guid" => "ID",
            "System.DateTime" or "System.DateTimeOffset" => "DateTime",
            "System.DateOnly" => "Date",
            _ => null,
        };
        if (mapped is not null) return mapped + bang;

        // User DTO / enum reference
        var known = model.Classes.FirstOrDefault(c => c.CSharpFullName == t)?.EmittedName
                 ?? model.Enums.FirstOrDefault(e => e.CSharpFullName == t)?.EmittedName;
        if (known is not null) return known + bang;

        return "String" + bang; // fallback
    }

    private static string ToCamelCase(string s) =>
        string.IsNullOrEmpty(s) ? s : char.ToLowerInvariant(s[0]) + s.Substring(1);

    private static string ToScreamingSnake(string s) =>
        Regex.Replace(s, "(?<!^)([A-Z])", "_$1").ToUpperInvariant();

    private static string? ExtractGeneric(string type, params string[] names)
    {
        foreach (var name in names)
        {
            var prefix = name + "<";
            if (type.StartsWith(prefix) && type.EndsWith(">"))
                return type.Substring(prefix.Length, type.Length - prefix.Length - 1);
        }
        return null;
    }

    private static string ResolveOutputDir(string? settingsDir, SchemaModel model)
    {
        if (settingsDir is not null) return settingsDir;
        var first = model.Classes.FirstOrDefault(c => (c.Targets & TypeTarget.GraphQL) != 0);
        return first?.OutputDir ?? ".";
    }
}
