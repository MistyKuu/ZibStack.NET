    private static string ResolveOutputDir(string? queryDir, string? typeScriptDir, SchemaModel model)
    {
        if (!string.IsNullOrEmpty(queryDir)) return queryDir!;
        if (!string.IsNullOrEmpty(typeScriptDir)) return typeScriptDir!;
        var first = model.Classes.FirstOrDefault(c => (c.Targets & (TypeTarget.TypeScript | TypeTarget.TanStackQuery)) != 0);
        return first?.OutputDir ?? ".";
    }

    private static string ResolveModelOutputDir(string? typeScriptDir, SchemaModel model)
    {
        if (!string.IsNullOrEmpty(typeScriptDir)) return typeScriptDir!;
        var firstClass = model.Classes.FirstOrDefault(c =>
            !c.TsIgnore && (c.Targets & (TypeTarget.TypeScript | TypeTarget.TanStackQuery)) != 0);
        if (firstClass is not null) return firstClass.OutputDir;
        var firstEnum = model.Enums.FirstOrDefault(e =>
            !e.TsIgnore && (e.Targets & (TypeTarget.TypeScript | TypeTarget.TanStackQuery)) != 0);
        return firstEnum?.OutputDir ?? ".";
    }

    private static Dictionary<string, string> BuildModelDirLookup(SchemaModel model, TypeScriptSettings ts)
    {
        var lookup = new Dictionary<string, string>(System.StringComparer.Ordinal);
        foreach (var c in model.Classes)
        {
            if (c.TsIgnore || (c.Targets & (TypeTarget.TypeScript | TypeTarget.TanStackQuery)) == 0) continue;
            lookup[TypeScriptName(c)] = !string.IsNullOrEmpty(ts.OutputDir) ? ts.OutputDir! : c.OutputDir;
        }
        foreach (var e in model.Enums)
        {
            if (e.TsIgnore || (e.Targets & (TypeTarget.TypeScript | TypeTarget.TanStackQuery)) == 0) continue;
            lookup[TypeScriptName(e)] = !string.IsNullOrEmpty(ts.OutputDir) ? ts.OutputDir! : e.OutputDir;
        }
        return lookup;
    }

    private static string StripTsExtension(string fileName) =>
        fileName.EndsWith(".ts", System.StringComparison.OrdinalIgnoreCase)
            ? fileName.Substring(0, fileName.Length - 3)
            : fileName;

    private static string? ExtractGeneric(string typeName, params string[] names)
    {
        foreach (var n in names)
        {
            var idx = typeName.IndexOf(n + "<", System.StringComparison.Ordinal);
            if (idx < 0) continue;
            if (idx > 0 && (char.IsLetterOrDigit(typeName[idx - 1]) || typeName[idx - 1] == '_')) continue;
            var open = idx + n.Length;
            var inner = ExtractBalanced(typeName, open);
            if (inner != null) return inner;
        }
        return null;
    }

    private static (string K, string V)? ExtractTwoGenericArgs(string typeName, params string[] names)
    {
        var inner = ExtractGeneric(typeName, names);
        if (inner is null) return null;
        int depth = 0;
        for (int i = 0; i < inner.Length; i++)
        {
            if (inner[i] == '<') depth++;
            else if (inner[i] == '>') depth--;
            else if (inner[i] == ',' && depth == 0)
                return (inner.Substring(0, i).Trim(), inner.Substring(i + 1).Trim());
        }
        return null;
    }

    private static string? ExtractBalanced(string s, int openAngleBracketIndex)
    {
        if (openAngleBracketIndex >= s.Length || s[openAngleBracketIndex] != '<') return null;
        int depth = 1;
        for (int i = openAngleBracketIndex + 1; i < s.Length; i++)
        {
            if (s[i] == '<') depth++;
            else if (s[i] == '>')
            {
                depth--;
                if (depth == 0) return s.Substring(openAngleBracketIndex + 1, i - openAngleBracketIndex - 1);
            }
        }
        return null;
    }

    private static string CleanRouteParameterName(string raw)
    {
        var s = raw.Trim();
        while (s.StartsWith("*", System.StringComparison.Ordinal)) s = s.Substring(1);
        var colon = s.IndexOf(':');
        if (colon >= 0) s = s.Substring(0, colon);
        var equals = s.IndexOf('=');
        if (equals >= 0) s = s.Substring(0, equals);
        var optional = s.IndexOf('?');
        if (optional >= 0) s = s.Substring(0, optional);
        return s.Trim();
    }

    private static IEnumerable<(string Name, string TypeExpression)> ExtractRouteTemplateParameters(string pattern)
    {
        for (var i = 0; i < pattern.Length; i++)
        {
            if (pattern[i] != '{') continue;
            var close = pattern.IndexOf('}', i + 1);
            if (close < 0) yield break;

            var raw = pattern.Substring(i + 1, close - i - 1);
            var name = CleanRouteParameterName(raw);
            if (name.Length > 0)
                yield return (name, InferRouteTypeExpression(raw));
            i = close;
        }
    }

    private static string InferRouteTypeExpression(string raw)
    {
        var colon = raw.IndexOf(':');
        if (colon < 0 || colon + 1 >= raw.Length) return "string";

        var constraintStart = colon + 1;
        var constraintEnd = FirstNonNegative(
            raw.IndexOf(':', constraintStart),
            raw.IndexOf('=', constraintStart),
            raw.IndexOf('?', constraintStart));
        var constraint = constraintEnd >= 0
            ? raw.Substring(constraintStart, constraintEnd - constraintStart)
            : raw.Substring(constraintStart);

        var csharpType = constraint.Trim().ToLowerInvariant() switch
        {
            "bool" or "boolean" => "bool",
            "datetime" => "System.DateTime",
            "decimal" => "decimal",
            "double" => "double",
            "float" => "float",
            "guid" => "System.Guid",
            "int" => "int",
            "long" => "long",
            "short" => "short",
            _ => "string",
        };

        return TypeScriptNaming.TryMapPrimitive(csharpType, out var primitive) ? primitive : "string";
    }

    private static int FirstNonNegative(params int[] values)
    {
        var best = -1;
        foreach (var value in values)
        {
            if (value < 0) continue;
            if (best < 0 || value < best) best = value;
        }
        return best;
    }

    private static bool LooksLikeTypeName(string name, out string shortName)
    {
        shortName = name;
        if (string.IsNullOrEmpty(name)) return false;
        if (name.IndexOf('<') >= 0) return false;
        var dot = name.LastIndexOf('.');
        if (dot >= 0) shortName = name.Substring(dot + 1);
        if (shortName.Length == 0 || !char.IsUpper(shortName[0])) return false;
        foreach (var ch in shortName)
            if (!char.IsLetterOrDigit(ch) && ch != '_') return false;
        return true;
    }

    private static string TagName(string? tag) =>
        string.IsNullOrWhiteSpace(tag) ? "Api" : ToPascalIdentifier(tag!);

    private static string KeyFactoryName(string tag) => ToCamelIdentifier(tag) + "Keys";

    private static string InvalidateHelperName(string tag) => "invalidate" + ToPascalIdentifier(tag) + "Queries";

    private static string MakeUnique(string name, HashSet<string> used)
    {
        var baseName = string.IsNullOrEmpty(name) ? "operation" : name;
        var candidate = baseName;
        var index = 2;
        while (!used.Add(candidate))
            candidate = baseName + index++;
        return candidate;
    }

    private static string ToCamelIdentifier(string value)
    {
        var pascal = ToPascalIdentifier(value);
        return string.IsNullOrEmpty(pascal) ? "operation" : char.ToLowerInvariant(pascal[0]) + pascal.Substring(1);
    }

    private static string ToPascalIdentifier(string value)
    {
        if (string.IsNullOrWhiteSpace(value)) return "Operation";
        var sb = new StringBuilder();
        var upperNext = true;
        foreach (var ch in value)
        {
            if (char.IsLetterOrDigit(ch) || ch == '_')
            {
                if (sb.Length == 0 && char.IsDigit(ch)) sb.Append('_');
                sb.Append(upperNext ? char.ToUpperInvariant(ch) : ch);
                upperNext = false;
            }
            else
            {
                upperNext = true;
            }
        }
        return sb.Length == 0 ? "Operation" : sb.ToString();
    }

    private static string ToKebabCase(string name)
    {
        var separated = ToSeparated(name, '-');
        return string.IsNullOrEmpty(separated) ? "api" : separated;
    }

    private static string ToSeparated(string name, char sep)
    {
        var sb = new StringBuilder();
        for (int i = 0; i < name.Length; i++)
        {
            var ch = name[i];
            if (!char.IsLetterOrDigit(ch))
            {
                if (sb.Length > 0 && sb[sb.Length - 1] != sep) sb.Append(sep);
                continue;
            }
            if (i > 0 && char.IsUpper(ch) && sb.Length > 0 && sb[sb.Length - 1] != sep)
                sb.Append(sep);
            sb.Append(char.ToLowerInvariant(ch));
        }
        return sb.ToString().Trim(sep);
    }

    private static string RemoveTypeSyntax(string typeExpression)
    {
        var sb = new StringBuilder();
        foreach (var ch in typeExpression)
        {
            if (char.IsLetterOrDigit(ch) || ch == '_') sb.Append(ch);
        }
        return sb.Length == 0 ? "Value" : sb.ToString();
    }

    private static bool IsIdentifier(string value)
    {
        if (string.IsNullOrEmpty(value)) return false;
        if (!(char.IsLetter(value[0]) || value[0] == '_' || value[0] == '$')) return false;
        for (int i = 1; i < value.Length; i++)
        {
            var ch = value[i];
            if (!(char.IsLetterOrDigit(ch) || ch == '_' || ch == '$')) return false;
        }
        return true;
    }

    private static string TsString(string value) =>
        "'" + value.Replace("\\", "\\\\").Replace("'", "\\'") + "'";

