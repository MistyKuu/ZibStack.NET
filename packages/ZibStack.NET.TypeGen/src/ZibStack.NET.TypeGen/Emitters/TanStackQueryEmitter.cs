using System.Collections.Generic;
using System.Linq;
using System.Text;

namespace ZibStack.NET.TypeGen.Generator;

/// <summary>
/// Emits typed TanStack Query helpers from the endpoint model populated by
/// <see cref="EndpointDiscovery"/>. The output is TypeScript source only:
/// fetch functions, query keys, options factories, React hooks, and cache
/// helpers. DTO/model definitions remain the TypeScript emitter's job.
/// </summary>
internal static class TanStackQueryEmitter
{
    public static IReadOnlyList<EmittedFile> Emit(SchemaModel model, GlobalSettings settings)
    {
        EndpointDiscovery.SynthesizeFromCrudApi(model);
        if (model.Endpoints.Count == 0) return System.Array.Empty<EmittedFile>();

        var query = settings.TanStackQuery;
        var outputDir = ResolveOutputDir(query.OutputDir, settings.TypeScript.OutputDir, model);
        if (!model.TypeScriptNamesResolved)
        {
            TypeScriptNaming.ApplyEmittedNames(model, settings.TypeScript, TypeTarget.TypeScript | TypeTarget.TanStackQuery);
            model.TypeScriptNamesResolved = true;
        }
        var nameLookup = BuildTypeNameLookup(model);

        if (query.FileLayout == QueryFileLayout.SplitByTag)
        {
            var files = new List<EmittedFile>();
            foreach (var group in model.Endpoints
                .GroupBy(e => TagName(e.Tag))
                .OrderBy(g => g.Key, System.StringComparer.Ordinal))
            {
                var fileName = ToKebabCase(group.Key) + ".gen.ts";
                var content = EmitFile(group.ToList(), outputDir, settings, model, nameLookup);
                files.Add(new EmittedFile(TypeTarget.TanStackQuery, outputDir, fileName, content));
            }
            return files;
        }

        return new[]
        {
            new EmittedFile(
                TypeTarget.TanStackQuery,
                outputDir,
                string.IsNullOrWhiteSpace(query.SingleFileName) ? "api.gen.ts" : query.SingleFileName,
                EmitFile(model.Endpoints, outputDir, settings, model, nameLookup))
        };
    }

    private static string EmitFile(
        IReadOnlyList<EndpointInfo> endpoints,
        string queryOutputDir,
        GlobalSettings settings,
        SchemaModel model,
        IReadOnlyDictionary<string, string> nameLookup)
    {
        var query = settings.TanStackQuery;
        var ops = BuildOperations(endpoints, settings, nameLookup);
        var sb = new StringBuilder();

        if (query.EmitGeneratedBanner)
            sb.AppendLine("// @generated by ZibStack.NET.TypeGen - do not edit");

        EmitImports(sb, ops, queryOutputDir, settings, model);

        if (string.IsNullOrEmpty(query.ApiClientImportPath))
        {
            EmitDefaultClient(sb, query);
        }

        EmitPaginatedAliases(sb, ops);

        foreach (var group in ops
            .GroupBy(o => o.Tag)
            .OrderBy(g => g.Key, System.StringComparer.Ordinal))
        {
            EmitKeyFactory(sb, group.Key, group.Where(o => o.IsQuery).ToList());
        }

        foreach (var op in ops.OrderBy(o => o.Name, System.StringComparer.Ordinal))
        {
            EmitInputType(sb, op);
            EmitFetchFunction(sb, op, query);
            if (op.IsQuery && query.EmitQueryOptions)
                EmitQueryOptions(sb, op);
            if (!op.IsQuery && query.EmitMutationOptions)
                EmitMutationOptions(sb, op);
            if (query.EmitHooks)
                EmitHook(sb, op, query);
            if (op.IsQuery && query.EmitCacheHelpers && query.EmitQueryOptions)
                EmitPrefetchHelper(sb, op);
        }

        if (query.EmitCacheHelpers)
        {
            foreach (var tag in ops.Select(o => o.Tag).Distinct(System.StringComparer.Ordinal).OrderBy(t => t, System.StringComparer.Ordinal))
                EmitInvalidateHelper(sb, tag);
        }

        return sb.ToString();
    }

    private static List<OperationModel> BuildOperations(
        IReadOnlyList<EndpointInfo> endpoints,
        GlobalSettings settings,
        IReadOnlyDictionary<string, string> nameLookup)
    {
        var usedNames = new HashSet<string>(System.StringComparer.Ordinal);
        var operations = new List<OperationModel>();

        foreach (var ep in endpoints.OrderBy(e => e.Tag ?? "", System.StringComparer.Ordinal)
            .ThenBy(e => e.Pattern, System.StringComparer.Ordinal)
            .ThenBy(e => e.Verb, System.StringComparer.Ordinal))
        {
            var tag = TagName(ep.Tag);
            var operationName = MakeUnique(ToCamelIdentifier(ep.OperationId), usedNames);
            var inputTypeName = ToPascalIdentifier(operationName) + "Input";
            var inputMembers = BuildInputMembers(ep, settings, nameLookup);
            var response = ResolveResponseType(ep, nameLookup);

            operations.Add(new OperationModel
            {
                Endpoint = ep,
                Tag = tag,
                Name = operationName,
                InputTypeName = inputTypeName,
                InputMembers = inputMembers,
                ResponseType = response.TypeExpression,
                TypeImports = response.TypeImports,
                PaginatedAliases = response.PaginatedAliases,
                HasInput = inputMembers.Count > 0,
                HasRequiredInput = inputMembers.Any(m => m.Required),
                IsQuery = string.Equals(ep.Verb, "get", System.StringComparison.OrdinalIgnoreCase),
            });
        }

        return operations;
    }

    private static List<InputMember> BuildInputMembers(
        EndpointInfo ep,
        GlobalSettings settings,
        IReadOnlyDictionary<string, string> nameLookup)
    {
        var members = new List<InputMember>();
        var seen = new HashSet<string>(System.StringComparer.Ordinal);

        foreach (var p in GetWireParameters(ep, settings))
        {
            if (p.Location == ParamLocation.Body) continue;
            if (!seen.Add(p.Name)) continue;
            var type = ResolveType(p.CSharpType, nameLookup);
            members.Add(new InputMember
            {
                WireName = p.Name,
                PropertyName = p.Name,
                TypeExpression = type.TypeExpression,
                Required = p.Required || p.Location == ParamLocation.Route,
                Location = p.Location,
                TypeImports = type.TypeImports,
                PaginatedAliases = type.PaginatedAliases,
            });
        }

        AddMissingRouteInputMembers(ep, members, seen);

        if (ep.RequestBodyCSharpType is not null)
        {
            var body = ResolveType(ep.RequestBodyCSharpType, nameLookup);
            members.Add(new InputMember
            {
                WireName = "body",
                PropertyName = "body",
                TypeExpression = body.TypeExpression,
                Required = true,
                Location = ParamLocation.Body,
                TypeImports = body.TypeImports,
                PaginatedAliases = body.PaginatedAliases,
            });
        }
        else if (ep.RequestBodyArrayItemCSharpType is not null)
        {
            var body = ResolveType(ep.RequestBodyArrayItemCSharpType, nameLookup);
            members.Add(new InputMember
            {
                WireName = "body",
                PropertyName = "body",
                TypeExpression = body.TypeExpression + "[]",
                Required = true,
                Location = ParamLocation.Body,
                TypeImports = body.TypeImports,
                PaginatedAliases = body.PaginatedAliases,
            });
        }

        return members;
    }

    private static void AddMissingRouteInputMembers(EndpointInfo ep, List<InputMember> members, HashSet<string> seen)
    {
        foreach (var routeParam in ExtractRouteTemplateParameters(ep.Pattern))
        {
            if (members.Any(m => m.Location == ParamLocation.Route
                    && string.Equals(m.WireName, routeParam.Name, System.StringComparison.Ordinal)))
                continue;
            if (!seen.Add(routeParam.Name)) continue;

            members.Add(new InputMember
            {
                WireName = routeParam.Name,
                PropertyName = routeParam.Name,
                TypeExpression = routeParam.TypeExpression,
                Required = true,
                Location = ParamLocation.Route,
            });
        }
    }

    private static IEnumerable<EndpointParameter> GetWireParameters(EndpointInfo ep, GlobalSettings settings)
    {
        foreach (var p in ep.Parameters)
            yield return p;

        if (!ep.IsListEndpoint) yield break;

        foreach (var p in ListEndpointParameters(settings.HasQueryDsl))
            yield return p;
    }

    private static IEnumerable<EndpointParameter> ListEndpointParameters(bool hasQueryDsl)
    {
        yield return QueryParam("page", "int");
        yield return QueryParam("pageSize", "int");
        if (!hasQueryDsl) yield break;
        yield return QueryParam("filter", "string");
        yield return QueryParam("sort", "string");
        yield return QueryParam("select", "string");
        yield return QueryParam("count", "bool");
    }

    private static EndpointParameter QueryParam(string name, string type) =>
        new() { Name = name, CSharpType = type, Location = ParamLocation.Query, Required = false };

    private static TypeResolution ResolveResponseType(EndpointInfo ep, IReadOnlyDictionary<string, string> nameLookup)
    {
        if (ep.ResponseCSharpType is not null)
            return ResolveType(ep.ResponseCSharpType, nameLookup);
        if (ep.ResponseArrayItemCSharpType is not null)
        {
            var item = ResolveType(ep.ResponseArrayItemCSharpType, nameLookup);
            item.TypeExpression += "[]";
            return item;
        }
        return new TypeResolution("void");
    }

    private static TypeResolution ResolveType(string cSharpType, IReadOnlyDictionary<string, string> nameLookup)
    {
        var t = cSharpType.Trim().TrimEnd('?');
        var result = new TypeResolution("unknown");

        var patchInner = ExtractGeneric(t, "PatchField");
        if (patchInner is not null) return ResolveType(patchInner, nameLookup);

        var nullableInner = ExtractGeneric(t, "Nullable", "System.Nullable");
        if (nullableInner is not null) return ResolveType(nullableInner, nameLookup);

        var pagedInner = ExtractGeneric(t, "PaginatedResponse");
        if (pagedInner is not null)
        {
            var inner = ResolveType(pagedInner, nameLookup);
            var aliasName = "PaginatedResponseOf" + ToPascalIdentifier(RemoveTypeSyntax(inner.TypeExpression));
            result.TypeExpression = aliasName;
            result.TypeImports.UnionWith(inner.TypeImports);
            result.PaginatedAliases[aliasName] = inner.TypeExpression;
            return result;
        }

        if (nameLookup.TryGetValue(t, out var mapped))
            return TypeResolution.WithImport(mapped, mapped);

        if (t.EndsWith("[]", System.StringComparison.Ordinal))
        {
            var inner = ResolveType(t.Substring(0, t.Length - 2), nameLookup);
            inner.TypeExpression += "[]";
            return inner;
        }

        var listInner = ExtractGeneric(t,
            "List", "IList", "ICollection", "IEnumerable", "IReadOnlyList", "IReadOnlyCollection",
            "HashSet", "ISet", "IReadOnlySet",
            "System.Collections.Generic.List", "System.Collections.Generic.IList", "System.Collections.Generic.ICollection",
            "System.Collections.Generic.IEnumerable", "System.Collections.Generic.IReadOnlyList",
            "System.Collections.Generic.IReadOnlyCollection", "System.Collections.Generic.HashSet",
            "System.Collections.Generic.ISet", "System.Collections.Generic.IReadOnlySet");
        if (listInner is not null)
        {
            var inner = ResolveType(listInner, nameLookup);
            inner.TypeExpression += "[]";
            return inner;
        }

        var dict = ExtractTwoGenericArgs(t,
            "Dictionary", "IDictionary", "IReadOnlyDictionary",
            "System.Collections.Generic.Dictionary", "System.Collections.Generic.IDictionary",
            "System.Collections.Generic.IReadOnlyDictionary");
        if (dict is not null)
        {
            var key = ResolveType(dict.Value.K, nameLookup);
            var val = ResolveType(dict.Value.V, nameLookup);
            result.TypeExpression = $"Record<{key.TypeExpression}, {val.TypeExpression}>";
            result.TypeImports.UnionWith(key.TypeImports);
            result.TypeImports.UnionWith(val.TypeImports);
            foreach (var kvp in key.PaginatedAliases) result.PaginatedAliases[kvp.Key] = kvp.Value;
            foreach (var kvp in val.PaginatedAliases) result.PaginatedAliases[kvp.Key] = kvp.Value;
            return result;
        }

        if (TypeScriptNaming.TryMapPrimitive(t, out var primitive)) return new TypeResolution(primitive);

        if (LooksLikeTypeName(t, out var shortName))
            return TypeResolution.WithImport(shortName, shortName);

        return result;
    }

    private static void EmitImports(StringBuilder sb, IReadOnlyList<OperationModel> ops, string queryOutputDir, GlobalSettings settings, SchemaModel model)
    {
        var query = settings.TanStackQuery;
        var queryOps = ops.Where(o => o.IsQuery).ToList();
        var mutationOps = ops.Where(o => !o.IsQuery).ToList();

        var tanstackImports = new List<string>();
        var tanstackTypeImports = new List<string>();
        if (queryOps.Count > 0 && query.EmitQueryOptions) tanstackImports.Add("queryOptions");
        if (mutationOps.Count > 0 && query.EmitMutationOptions) tanstackImports.Add("mutationOptions");
        if (query.EmitHooks)
        {
            if (queryOps.Count > 0) tanstackImports.Add("useQuery");
            if (mutationOps.Count > 0) tanstackImports.Add("useMutation");
            if (mutationOps.Count > 0 && query.EmitCacheHelpers) tanstackImports.Add("useQueryClient");
        }
        if (query.EmitCacheHelpers) tanstackTypeImports.Add("QueryClient");

        if (tanstackImports.Count > 0 || tanstackTypeImports.Count > 0)
        {
            var parts = new List<string>();
            parts.AddRange(tanstackImports.Distinct(System.StringComparer.Ordinal).OrderBy(x => x, System.StringComparer.Ordinal));
            if (tanstackTypeImports.Count > 0)
                parts.AddRange(tanstackTypeImports.Distinct(System.StringComparer.Ordinal).OrderBy(x => x, System.StringComparer.Ordinal).Select(x => "type " + x));
            sb.AppendLine($"import {{ {string.Join(", ", parts)} }} from '@tanstack/react-query';");
        }

        if (!string.IsNullOrEmpty(query.ApiClientImportPath))
            sb.AppendLine($"import {{ {query.ApiClientName} }} from '{query.ApiClientImportPath}';");

        var modelImports = CollectModelImports(ops, queryOutputDir, settings, model);
        foreach (var kvp in modelImports.OrderBy(k => k.Key, System.StringComparer.Ordinal))
        {
            var names = string.Join(", ", kvp.Value.OrderBy(n => n, System.StringComparer.Ordinal));
            sb.AppendLine($"import type {{ {names} }} from '{kvp.Key}';");
        }

        if (tanstackImports.Count > 0 || tanstackTypeImports.Count > 0 || !string.IsNullOrEmpty(query.ApiClientImportPath) || modelImports.Count > 0 || query.EmitGeneratedBanner)
            sb.AppendLine();
    }

    private static Dictionary<string, HashSet<string>> CollectModelImports(
        IReadOnlyList<OperationModel> ops,
        string queryOutputDir,
        GlobalSettings settings,
        SchemaModel model)
    {
        var allTypes = new HashSet<string>(System.StringComparer.Ordinal);
        foreach (var op in ops)
        {
            allTypes.UnionWith(op.TypeImports);
            foreach (var member in op.InputMembers)
                allTypes.UnionWith(member.TypeImports);
        }

        foreach (var alias in ops.SelectMany(o => o.PaginatedAliases.Keys))
            allTypes.Remove(alias);

        var byPath = new Dictionary<string, HashSet<string>>(System.StringComparer.Ordinal);
        if (allTypes.Count == 0) return byPath;

        var query = settings.TanStackQuery;
        if (!string.IsNullOrEmpty(query.ModelsImportPath))
        {
            byPath[query.ModelsImportPath!] = allTypes;
            return byPath;
        }

        var ts = settings.TypeScript;
        var modelDir = ResolveModelOutputDir(ts.OutputDir, model);
        if (ts.FileLayout == TypeScriptFileLayout.SingleFile)
        {
            var fileName = StripTsExtension(string.IsNullOrWhiteSpace(ts.SingleFileName) ? "models.ts" : ts.SingleFileName);
            var path = SchemaParser.ComputeRelativeImport(queryOutputDir, modelDir, fileName);
            byPath[path] = allTypes;
            return byPath;
        }

        var dirByTypeName = BuildModelDirLookup(model, ts);
        foreach (var typeName in allTypes)
        {
            var targetDir = dirByTypeName.TryGetValue(typeName, out var perTypeDir) ? perTypeDir : modelDir;
            var path = SchemaParser.ComputeRelativeImport(queryOutputDir, targetDir, typeName);
            if (!byPath.TryGetValue(path, out var names))
                byPath[path] = names = new HashSet<string>(System.StringComparer.Ordinal);
            names.Add(typeName);
        }

        return byPath;
    }

    private static void EmitDefaultClient(StringBuilder sb, TanStackQuerySettings query)
    {
        sb.AppendLine("export type ApiFetchOptions = {");
        sb.AppendLine("    method?: string;");
        sb.AppendLine("    query?: Record<string, unknown>;");
        sb.AppendLine("    headers?: Record<string, string | undefined>;");
        sb.AppendLine("    body?: unknown;");
        sb.AppendLine("    signal?: AbortSignal;");
        sb.AppendLine("};");
        sb.AppendLine();
        sb.AppendLine("export class ApiError extends Error {");
        sb.AppendLine("    constructor(public readonly status: number, public readonly body: unknown, message: string) {");
        sb.AppendLine("        super(message);");
        sb.AppendLine("        this.name = 'ApiError';");
        sb.AppendLine("    }");
        sb.AppendLine("}");
        sb.AppendLine();
        sb.AppendLine($"export async function {query.ApiClientName}<T>(path: string, options: ApiFetchOptions = {{}}): Promise<T> {{");
        sb.AppendLine("    const configuredBaseUrl = (() => {");
        sb.AppendLine($"        try {{ return {query.BaseUrlExpression}; }} catch {{ return undefined; }}");
        sb.AppendLine("    })();");
        sb.AppendLine("    const baseUrl = configuredBaseUrl || (typeof window !== 'undefined' ? window.location.origin : 'http://localhost');");
        sb.AppendLine("    const url = new URL(path, baseUrl);");
        sb.AppendLine("    if (options.query) {");
        sb.AppendLine("        for (const [key, value] of Object.entries(options.query)) {");
        sb.AppendLine("            if (value === undefined || value === null) continue;");
        sb.AppendLine("            if (Array.isArray(value)) {");
        sb.AppendLine("                for (const item of value) url.searchParams.append(key, String(item));");
        sb.AppendLine("            } else {");
        sb.AppendLine("                url.searchParams.set(key, String(value));");
        sb.AppendLine("            }");
        sb.AppendLine("        }");
        sb.AppendLine("    }");
        sb.AppendLine("    const headers: Record<string, string> = {};");
        sb.AppendLine("    if (options.body !== undefined) headers['content-type'] = 'application/json';");
        sb.AppendLine("    if (options.headers) {");
        sb.AppendLine("        for (const [key, value] of Object.entries(options.headers)) {");
        sb.AppendLine("            if (value !== undefined) headers[key] = value;");
        sb.AppendLine("        }");
        sb.AppendLine("    }");
        sb.AppendLine("    const response = await fetch(url.toString(), {");
        sb.AppendLine("        method: options.method ?? 'GET',");
        sb.AppendLine("        headers,");
        sb.AppendLine("        body: options.body === undefined ? undefined : JSON.stringify(options.body),");
        sb.AppendLine("        signal: options.signal,");
        sb.AppendLine("    });");
        sb.AppendLine("    if (!response.ok) {");
        sb.AppendLine("        const errorText = await response.text();");
        sb.AppendLine("        let errorBody: unknown = errorText;");
        sb.AppendLine("        try { errorBody = errorText ? JSON.parse(errorText) : undefined; } catch { }");
        sb.AppendLine("        throw new ApiError(response.status, errorBody, response.statusText || 'Request failed');");
        sb.AppendLine("    }");
        sb.AppendLine("    if (response.status === 204) return undefined as T;");
        sb.AppendLine("    const text = await response.text();");
        sb.AppendLine("    if (!text) return undefined as T;");
        sb.AppendLine("    const contentType = response.headers.get('content-type') ?? '';");
        sb.AppendLine("    return (contentType.includes('application/json') ? JSON.parse(text) : text) as T;");
        sb.AppendLine("}");
        sb.AppendLine();
    }

    private static void EmitPaginatedAliases(StringBuilder sb, IReadOnlyList<OperationModel> ops)
    {
        var aliases = new Dictionary<string, string>(System.StringComparer.Ordinal);
        foreach (var op in ops)
        {
            foreach (var kvp in op.PaginatedAliases)
                aliases[kvp.Key] = kvp.Value;
            foreach (var member in op.InputMembers)
                foreach (var kvp in member.PaginatedAliases)
                    aliases[kvp.Key] = kvp.Value;
        }

        foreach (var kvp in aliases.OrderBy(k => k.Key, System.StringComparer.Ordinal))
        {
            sb.AppendLine($"export type {kvp.Key} = {{");
            sb.AppendLine($"    items: {kvp.Value}[];");
            sb.AppendLine("    totalCount: number;");
            sb.AppendLine("    page: number;");
            sb.AppendLine("    pageSize: number;");
            sb.AppendLine("    totalPages: number;");
            sb.AppendLine("    hasNextPage: boolean;");
            sb.AppendLine("    hasPreviousPage: boolean;");
            sb.AppendLine("};");
            sb.AppendLine();
        }
    }

    private static void EmitKeyFactory(StringBuilder sb, string tag, IReadOnlyList<OperationModel> queryOps)
    {
        var keyName = KeyFactoryName(tag);
        sb.AppendLine($"export const {keyName} = {{");
        sb.AppendLine($"    all: ['{ToCamelIdentifier(tag)}'] as const,");
        foreach (var op in queryOps.OrderBy(o => o.Name, System.StringComparer.Ordinal))
        {
            if (!op.HasInput)
                sb.AppendLine($"    {op.Name}: () => [...{keyName}.all, '{op.Name}'] as const,");
            else if (op.HasRequiredInput)
                sb.AppendLine($"    {op.Name}: (input: {op.InputTypeName}) => [...{keyName}.all, '{op.Name}', input] as const,");
            else
                sb.AppendLine($"    {op.Name}: (input: {op.InputTypeName} = {{}}) => [...{keyName}.all, '{op.Name}', input] as const,");
        }
        sb.AppendLine("};");
        sb.AppendLine();
    }

    private static void EmitInputType(StringBuilder sb, OperationModel op)
    {
        if (!op.HasInput) return;
        sb.AppendLine($"export type {op.InputTypeName} = {{");
        foreach (var member in op.InputMembers)
        {
            var optional = member.Required ? "" : "?";
            sb.AppendLine($"    {MemberDeclarationName(member.PropertyName)}{optional}: {member.TypeExpression};");
        }
        sb.AppendLine("};");
        sb.AppendLine();
    }

    private static void EmitFetchFunction(StringBuilder sb, OperationModel op, TanStackQuerySettings query)
    {
        sb.Append($"export function {op.Name}(");
        if (op.HasInput)
            sb.Append(op.HasRequiredInput ? $"input: {op.InputTypeName}" : $"input: {op.InputTypeName} = {{}}");
        if (op.HasInput)
            sb.Append(", signal?: AbortSignal");
        else
            sb.Append("signal?: AbortSignal");
        sb.AppendLine($"): Promise<{op.ResponseType}> {{");

        sb.AppendLine($"    return {query.ApiClientName}<{op.ResponseType}>({BuildPathExpression(op)}, {{");
        sb.AppendLine($"        method: '{op.Endpoint.Verb.ToUpperInvariant()}',");
        EmitRequestOptionObject(sb, "query", op.InputMembers.Where(m => m.Location == ParamLocation.Query).ToList());
        EmitRequestOptionObject(sb, "headers", op.InputMembers.Where(m => m.Location == ParamLocation.Header).ToList());
        var body = op.InputMembers.FirstOrDefault(m => m.Location == ParamLocation.Body);
        if (body is not null)
            sb.AppendLine($"        body: {InputAccess(body)},");
        sb.AppendLine("        signal,");
        sb.AppendLine("    });");
        sb.AppendLine("}");
        sb.AppendLine();
    }

    private static void EmitRequestOptionObject(StringBuilder sb, string optionName, IReadOnlyList<InputMember> members)
    {
        if (members.Count == 0) return;
        sb.AppendLine($"        {optionName}: {{");
        foreach (var member in members)
            sb.AppendLine($"            {ObjectKey(member.WireName)}: {InputAccess(member)},");
        sb.AppendLine("        },");
    }

    private static void EmitQueryOptions(StringBuilder sb, OperationModel op)
    {
        sb.Append($"export function {op.Name}Options(");
        if (op.HasInput)
            sb.Append(op.HasRequiredInput ? $"input: {op.InputTypeName}" : $"input: {op.InputTypeName} = {{}}");
        sb.AppendLine(") {");
        var keyCall = BuildKeyCall(op);
        var fetchCall = op.HasInput ? $"{op.Name}(input, signal)" : $"{op.Name}(signal)";
        sb.AppendLine("    return queryOptions({");
        sb.AppendLine($"        queryKey: {keyCall},");
        sb.AppendLine($"        queryFn: ({{ signal }}) => {fetchCall},");
        sb.AppendLine("    });");
        sb.AppendLine("}");
        sb.AppendLine();
    }

    private static void EmitMutationOptions(StringBuilder sb, OperationModel op)
    {
        sb.AppendLine($"export function {op.Name}MutationOptions() {{");
        sb.AppendLine("    return mutationOptions({");
        sb.AppendLine($"        mutationFn: {BuildMutationFn(op)},");
        sb.AppendLine("    });");
        sb.AppendLine("}");
        sb.AppendLine();
    }

    private static void EmitHook(StringBuilder sb, OperationModel op, TanStackQuerySettings query)
    {
        var hookName = "use" + ToPascalIdentifier(op.Name);
        if (op.IsQuery)
        {
            sb.Append($"export function {hookName}(");
            if (op.HasInput)
                sb.Append(op.HasRequiredInput ? $"input: {op.InputTypeName}" : $"input: {op.InputTypeName} = {{}}");
            sb.AppendLine(") {");
            if (query.EmitQueryOptions)
                sb.AppendLine(op.HasInput
                    ? $"    return useQuery({op.Name}Options(input));"
                    : $"    return useQuery({op.Name}Options());");
            else
            {
                var keyCall = BuildKeyCall(op);
                var fetchCall = op.HasInput ? $"{op.Name}(input, signal)" : $"{op.Name}(signal)";
                sb.AppendLine("    return useQuery({");
                sb.AppendLine($"        queryKey: {keyCall},");
                sb.AppendLine($"        queryFn: ({{ signal }}) => {fetchCall},");
                sb.AppendLine("    });");
            }
            sb.AppendLine("}");
            sb.AppendLine();
            return;
        }

        sb.AppendLine($"export function {hookName}() {{");
        if (query.EmitCacheHelpers)
        {
            sb.AppendLine("    const queryClient = useQueryClient();");
            sb.AppendLine("    return useMutation({");
            if (query.EmitMutationOptions)
                sb.AppendLine($"        ...{op.Name}MutationOptions(),");
            else
                sb.AppendLine($"        mutationFn: {BuildMutationFn(op)},");
            sb.AppendLine("        onSuccess: async () => {");
            sb.AppendLine($"            await {InvalidateHelperName(op.Tag)}(queryClient);");
            sb.AppendLine("        },");
            sb.AppendLine("    });");
        }
        else if (query.EmitMutationOptions)
        {
            sb.AppendLine($"    return useMutation({op.Name}MutationOptions());");
        }
        else
        {
            sb.AppendLine("    return useMutation({");
            sb.AppendLine($"        mutationFn: {BuildMutationFn(op)},");
            sb.AppendLine("    });");
        }
        sb.AppendLine("}");
        sb.AppendLine();
    }

    private static string BuildMutationFn(OperationModel op) =>
        op.HasInput
            ? $"(input: {op.InputTypeName}) => {op.Name}(input)"
            : $"() => {op.Name}()";

    private static void EmitPrefetchHelper(StringBuilder sb, OperationModel op)
    {
        var name = "prefetch" + ToPascalIdentifier(op.Name);
        sb.Append($"export function {name}(queryClient: QueryClient");
        if (op.HasInput)
            sb.Append(op.HasRequiredInput ? $", input: {op.InputTypeName}" : $", input: {op.InputTypeName} = {{}}");
        sb.AppendLine(") {");
        sb.AppendLine(op.HasInput
            ? $"    return queryClient.prefetchQuery({op.Name}Options(input));"
            : $"    return queryClient.prefetchQuery({op.Name}Options());");
        sb.AppendLine("}");
        sb.AppendLine();
    }

    private static void EmitInvalidateHelper(StringBuilder sb, string tag)
    {
        sb.AppendLine($"export function {InvalidateHelperName(tag)}(queryClient: QueryClient) {{");
        sb.AppendLine($"    return queryClient.invalidateQueries({{ queryKey: {KeyFactoryName(tag)}.all }});");
        sb.AppendLine("}");
        sb.AppendLine();
    }

    private static string BuildPathExpression(OperationModel op)
    {
        var pattern = op.Endpoint.Pattern;
        var sb = new StringBuilder();
        sb.Append('`');
        for (int i = 0; i < pattern.Length; i++)
        {
            var ch = pattern[i];
            if (ch == '`') { sb.Append("\\`"); continue; }
            if (ch == '$') { sb.Append("\\$"); continue; }
            if (ch != '{') { sb.Append(ch); continue; }

            var close = pattern.IndexOf('}', i + 1);
            if (close < 0) { sb.Append(ch); continue; }
            var raw = pattern.Substring(i + 1, close - i - 1);
            var name = CleanRouteParameterName(raw);
            var member = op.InputMembers.FirstOrDefault(m =>
                m.Location == ParamLocation.Route &&
                string.Equals(m.WireName, name, System.StringComparison.Ordinal));
            sb.Append("${encodeURIComponent(String(");
            sb.Append(member is null ? MissingRouteParameterExpression(name) : InputAccess(member));
            sb.Append("))}");
            i = close;
        }
        sb.Append('`');
        return sb.ToString();
    }

    private static string MissingRouteParameterExpression(string name) =>
        "(() => { throw new Error(" + TsString("Missing required route parameter '" + name + "'.") + "); })()";

    private static string BuildKeyCall(OperationModel op)
    {
        var keyName = KeyFactoryName(op.Tag);
        return op.HasInput ? $"{keyName}.{op.Name}(input)" : $"{keyName}.{op.Name}()";
    }

    private static string InputAccess(InputMember member) =>
        IsIdentifier(member.PropertyName) ? "input." + member.PropertyName : $"input[{TsString(member.PropertyName)}]";

    private static string MemberDeclarationName(string name) =>
        IsIdentifier(name) ? name : TsString(name);

    private static string ObjectKey(string name) =>
        IsIdentifier(name) ? name : TsString(name);

    private static Dictionary<string, string> BuildTypeNameLookup(SchemaModel model)
    {
        var lookup = new Dictionary<string, string>(System.StringComparer.Ordinal);
        foreach (var c in model.Classes)
            if (!c.TsIgnore && (c.Targets & (TypeTarget.TypeScript | TypeTarget.TanStackQuery)) != 0)
                lookup[c.CSharpFullName] = TypeScriptName(c);
        foreach (var e in model.Enums)
            if (!e.TsIgnore && (e.Targets & (TypeTarget.TypeScript | TypeTarget.TanStackQuery)) != 0)
                lookup[e.CSharpFullName] = TypeScriptName(e);
        return lookup;
    }

