using System.Collections.Generic;
using System.Linq;
using System.Text;

namespace ZibStack.NET.TypeGen.Generator;

/// <summary>
/// Produces Python model classes from a <see cref="SchemaModel"/>. Default style
/// is Pydantic v2 — emitted classes subclass <c>BaseModel</c>, property names
/// are snake_case per PEP 8, and original C# names are preserved via
/// <c>Field(alias=...)</c> so JSON wire format stays compatible.
/// <para>
/// Alternate <see cref="PythonStyle.Dataclass"/> emits plain <c>@dataclass</c>
/// with no runtime deps — type hints only, no validation.
/// </para>
/// </summary>
internal static class PythonEmitter
{
    public static IReadOnlyList<EmittedFile> Emit(SchemaModel model, GlobalSettings settings)
    {
        var files = new List<EmittedFile>();
        var py = settings.Python;

        // Build C#-fullname → Python-name lookup so property references resolve.
        var nameByCSharp = new Dictionary<string, string>();
        foreach (var c in model.Classes)
            if (!c.OpenApiIgnore && (c.Targets & TypeTarget.Python) != 0)
            {
                c.EmittedName = c.SourceName;   // Python reuses PascalCase class names as-is
                nameByCSharp[c.CSharpFullName] = c.EmittedName;
            }
        foreach (var e in model.Enums)
            if (!e.OpenApiIgnore && (e.Targets & TypeTarget.Python) != 0)
            {
                e.EmittedName = e.SourceName;
                nameByCSharp[e.CSharpFullName] = e.EmittedName;
            }

        if (py.FileLayout == PythonFileLayout.SingleFile)
        {
            var sb = new StringBuilder();
            EmitHeader(sb, py, singleFile: true);
            // Enums first so classes can reference them without forward-declaration dance.
            foreach (var en in model.Enums)
                if ((en.Targets & TypeTarget.Python) != 0)
                    EmitEnum(sb, en);
            foreach (var cls in model.Classes)
                if ((cls.Targets & TypeTarget.Python) != 0)
                    EmitClass(sb, cls, py, nameByCSharp, importsInBody: false);
            files.Add(new EmittedFile(TypeTarget.Python, ResolveOutputDir(py.OutputDir, model), py.SingleFileName, sb.ToString()));
        }
        else
        {
            foreach (var cls in model.Classes)
            {
                if ((cls.Targets & TypeTarget.Python) == 0) continue;
                var sb = new StringBuilder();
                EmitHeader(sb, py, singleFile: false);
                EmitImports(sb, cls, nameByCSharp);
                EmitClass(sb, cls, py, nameByCSharp, importsInBody: true);
                files.Add(new EmittedFile(TypeTarget.Python, cls.OutputDir, ToSnakeCase(cls.EmittedName) + ".py", sb.ToString()));
            }
            foreach (var en in model.Enums)
            {
                if ((en.Targets & TypeTarget.Python) == 0) continue;
                var sb = new StringBuilder();
                EmitHeader(sb, py, singleFile: false);
                sb.AppendLine(en.IsStringSerialized
                    ? "from enum import Enum"
                    : "from enum import IntEnum");
                sb.AppendLine();
                EmitEnum(sb, en);
                files.Add(new EmittedFile(TypeTarget.Python, en.OutputDir, ToSnakeCase(en.EmittedName) + ".py", sb.ToString()));
            }
        }

        return files;
    }

    // ── header + imports ────────────────────────────────────────────────────

    private static void EmitHeader(StringBuilder sb, PythonSettings py, bool singleFile)
    {
        if (py.EmitGeneratedBanner)
        {
            sb.AppendLine("# @generated by ZibStack.NET.TypeGen — do not edit");
            sb.AppendLine();
        }
        sb.AppendLine("from __future__ import annotations");
        // Union types and Optional emitted inline (PEP 604 X | None) — modern Python 3.10+.
        // Dataclasses / Pydantic imports added by EmitImports (FilePerClass) or inline below.
        if (singleFile)
        {
            sb.AppendLine("from enum import Enum, IntEnum");
            if (py.Style == PythonStyle.Pydantic)
                sb.AppendLine("from pydantic import BaseModel, Field");
            else
                sb.AppendLine("from dataclasses import dataclass, field");
            sb.AppendLine("from typing import Any");
            sb.AppendLine("from uuid import UUID");
            sb.AppendLine("from datetime import datetime, date, time");
            sb.AppendLine();
        }
    }

    private static void EmitImports(StringBuilder sb, SchemaClass cls, IReadOnlyDictionary<string, string> nameLookup)
    {
        // Framework imports — always needed.
        sb.AppendLine("from pydantic import BaseModel, Field");
        sb.AppendLine("from typing import Any");
        sb.AppendLine("from uuid import UUID");
        sb.AppendLine("from datetime import datetime, date, time");

        // Cross-class refs: import each referenced user-DTO from its own module.
        var refs = CollectRefs(cls, nameLookup);
        refs.Remove(cls.EmittedName);
        if (refs.Count > 0)
        {
            sb.AppendLine();
            // Absolute imports — work both in a package (with __init__.py) and when
            // the emit dir is just on sys.path. Relative imports would force users
            // to create a package even for a flat folder of models.
            foreach (var r in refs.OrderBy(r => r, System.StringComparer.Ordinal))
                sb.AppendLine($"from {ToSnakeCase(r)} import {r}");
        }
        sb.AppendLine();
    }

    private static HashSet<string> CollectRefs(SchemaClass cls, IReadOnlyDictionary<string, string> nameLookup)
    {
        var acc = new HashSet<string>();
        // Base class needs an import so the `class Child(Parent)` declaration resolves.
        if (cls.BaseClassFullName is { } bfn && nameLookup.TryGetValue(bfn, out var bn))
            acc.Add(bn);
        foreach (var prop in cls.Properties)
        {
            if (prop.TsIgnore) continue;
            // `[UseType<T>]` points at a different type than the C# declaration.
            // Use T's FQN for the import so Python's `from t import T` resolves.
            if (prop.TargetTypeCSharpFqn is { } useTypeFqn
                && nameLookup.TryGetValue(useTypeFqn, out var useTypeName))
                acc.Add(useTypeName);
            else
                CollectRefsRecursive(prop.CSharpTypeFullName, nameLookup, acc);
        }
        return acc;
    }

    private static void CollectRefsRecursive(string cSharpType, IReadOnlyDictionary<string, string> nameLookup, HashSet<string> acc)
    {
        var t = cSharpType.TrimEnd('?');
        // Unwrap PatchField<T> — Dto's tri-state wrapper is transparent in Python.
        var patchInner = ExtractGeneric(t, "PatchField");
        if (patchInner != null) { CollectRefsRecursive(patchInner, nameLookup, acc); return; }

        if (nameLookup.TryGetValue(t, out var mapped)) { acc.Add(mapped); return; }

        if (t.EndsWith("[]")) { CollectRefsRecursive(t.Substring(0, t.Length - 2), nameLookup, acc); return; }
        var listInner = ExtractGeneric(t, "List", "IList", "ICollection", "IEnumerable", "IReadOnlyList", "IReadOnlyCollection");
        if (listInner != null) { CollectRefsRecursive(listInner, nameLookup, acc); return; }
        var dict = ExtractTwoGenericArgs(t, "Dictionary", "IDictionary", "IReadOnlyDictionary");
        if (dict != null) { CollectRefsRecursive(dict.Value.K, nameLookup, acc); CollectRefsRecursive(dict.Value.V, nameLookup, acc); }
    }

    // ── class / enum emission ──────────────────────────────────────────────

    private static void EmitClass(StringBuilder sb, SchemaClass cls, PythonSettings py, IReadOnlyDictionary<string, string> nameLookup, bool importsInBody)
    {
        var baseType = cls.BaseClassFullName is { } bfn && nameLookup.TryGetValue(bfn, out var bn) ? bn : null;

        if (py.Style == PythonStyle.Pydantic)
        {
            var parent = baseType ?? "BaseModel";
            sb.AppendLine($"class {cls.EmittedName}({parent}):");
        }
        else
        {
            sb.AppendLine("@dataclass");
            sb.AppendLine(baseType is null ? $"class {cls.EmittedName}:" : $"class {cls.EmittedName}({baseType}):");
        }

        var emittedProps = cls.Properties.Where(p => !p.TsIgnore).ToList();
        if (emittedProps.Count == 0)
        {
            sb.AppendLine("    pass");
            sb.AppendLine();
            return;
        }

        foreach (var prop in emittedProps)
        {
            var srcName = prop.SourceName;
            var pyName = py.SnakeCaseProperties ? ToSnakeCase(srcName) : srcName;
            // Read-only ⇒ treat as optional at the Pydantic level too: mirrors the
            // TS `?` and the OpenAPI `readOnly` — single model serves both read
            // and write, so server-computed fields don't need a value client-side.
            // The `frozen=True` still blocks reassignment after construction, so
            // you can't accidentally mutate a value parsed from a server response.
            var pyOptional = prop.IsNullable || prop.IsReadOnly;
            // `[UseType<T>]` short-circuit: emit T's Python name directly when T
            // is in the model (CollectRefsRecursive picked it up for imports).
            // Nullable wrapping still applies. External targets (not in model)
            // fall through to the usual C# → Python mapping.
            string typeExpr;
            if (prop.TargetTypeCSharpFqn is { } useTypeFqn
                && nameLookup.TryGetValue(useTypeFqn, out var useTypePy))
            {
                typeExpr = pyOptional ? useTypePy + " | None" : useTypePy;
            }
            else
            {
                typeExpr = MapCSharpToPy(prop.CSharpTypeFullName, pyOptional, nameLookup);
            }
            var optional = pyOptional;

            if (py.Style == PythonStyle.Pydantic)
            {
                // Pydantic v2 `Field(frozen=True)` blocks reassignment after
                // construction — a per-field mirror of C#'s getter-only / private-set
                // semantics. Without frozen, the default `model.field = x` would
                // silently succeed client-side even though the server rejects it.
                // Merged with alias/default via a single Field() call so the arg
                // list reads naturally.
                var fieldArgs = new List<string>();
                if (optional) fieldArgs.Add("default=None");
                if (pyName != srcName) fieldArgs.Add($"alias=\"{srcName}\"");
                if (prop.IsReadOnly) fieldArgs.Add("frozen=True");

                if (fieldArgs.Count == 0)
                {
                    sb.AppendLine($"    {pyName}: {typeExpr}");
                }
                else if (fieldArgs.Count == 1 && fieldArgs[0] == "default=None")
                {
                    sb.AppendLine($"    {pyName}: {typeExpr} = None");
                }
                else
                {
                    sb.AppendLine($"    {pyName}: {typeExpr} = Field({string.Join(", ", fieldArgs)})");
                }
            }
            else
            {
                sb.AppendLine(optional
                    ? $"    {pyName}: {typeExpr} = None"
                    : $"    {pyName}: {typeExpr}");
            }
        }
        sb.AppendLine();
    }

    private static void EmitEnum(StringBuilder sb, SchemaEnum en)
    {
        // Numeric enums use IntEnum (preserves C# int values); if all values are
        // sequential 0..N-1 the choice is cosmetic, but explicit IntEnum lets
        // Python code compare against ints naturally.
        //
        // String-serialised enums ([JsonStringEnumConverter]) inherit from
        // (str, Enum) — the portable idiom across Python 3.8+ (StrEnum only arrived
        // in 3.11). Member values are the original C# member names so round-trips
        // match the wire format.
        var bases = en.IsStringSerialized ? "str, Enum" : "IntEnum";
        sb.AppendLine($"class {en.EmittedName}({bases}):");
        if (en.Members.Count == 0)
        {
            sb.AppendLine("    pass");
            sb.AppendLine();
            return;
        }
        foreach (var m in en.Members)
        {
            var value = en.IsStringSerialized ? $"\"{m.Name}\"" : m.Value.ToString();
            sb.AppendLine($"    {ToScreamingSnakeCase(m.Name)} = {value}");
        }
        sb.AppendLine();
    }

    // ── type mapping ───────────────────────────────────────────────────────

    /// <summary>
    /// Maps a C# type display string to a Python type annotation. Unknown types
    /// fall back to <c>Any</c>. Unwraps <c>PatchField&lt;T&gt;</c> so Dto-generated
    /// Create/Update DTOs render as plain fields.
    /// </summary>
    private static string MapCSharpToPy(string cSharpType, bool isNullable, IReadOnlyDictionary<string, string> nameLookup)
    {
        var t = cSharpType.TrimEnd('?');

        var patchInner = ExtractGeneric(t, "PatchField");
        if (patchInner != null) return MapCSharpToPy(patchInner, isNullable, nameLookup);

        if (nameLookup.TryGetValue(t, out var mapped))
            return isNullable ? $"{mapped} | None" : mapped;

        if (t.EndsWith("[]"))
        {
            var inner = MapCSharpToPy(t.Substring(0, t.Length - 2), isNullable: false, nameLookup);
            return Wrap(isNullable, $"list[{inner}]");
        }
        var listInner = ExtractGeneric(t, "List", "IList", "ICollection", "IEnumerable", "IReadOnlyList", "IReadOnlyCollection");
        if (listInner != null)
            return Wrap(isNullable, $"list[{MapCSharpToPy(listInner, isNullable: false, nameLookup)}]");

        var dict = ExtractTwoGenericArgs(t, "Dictionary", "IDictionary", "IReadOnlyDictionary");
        if (dict != null)
            return Wrap(isNullable, $"dict[{MapCSharpToPy(dict.Value.K, false, nameLookup)}, {MapCSharpToPy(dict.Value.V, false, nameLookup)}]");

        var primitive = t switch
        {
            "string" => "str",
            "bool" or "System.Boolean" => "bool",
            "byte" or "sbyte" or "short" or "ushort" or "int" or "uint" or "long" or "ulong"
                or "System.Int32" or "System.Int64" => "int",
            "float" or "double" or "System.Single" or "System.Double" => "float",
            "decimal" or "System.Decimal" => "str",   // same reason as TS: precision preservation
            "System.Guid" or "Guid" => "UUID",
            "System.DateTime" or "DateTime" or "System.DateTimeOffset" or "DateTimeOffset" => "datetime",
            "System.DateOnly" or "DateOnly" => "date",
            "System.TimeOnly" or "TimeOnly" or "System.TimeSpan" or "TimeSpan" => "time",
            "object" => "Any",
            _ => "Any",
        };
        return Wrap(isNullable, primitive);
    }

    private static string Wrap(bool nullable, string inner) => nullable ? $"{inner} | None" : inner;

    // ── helpers (generic-arg parsing shared by mapper + ref collector) ─────

    private static string? ExtractGeneric(string typeName, params string[] names)
    {
        foreach (var n in names)
        {
            var idx = typeName.IndexOf(n + "<", System.StringComparison.Ordinal);
            if (idx < 0) continue;
            if (idx > 0 && char.IsLetterOrDigit(typeName[idx - 1])) continue;
            int depth = 1;
            for (int i = idx + n.Length + 1; i < typeName.Length; i++)
            {
                if (typeName[i] == '<') depth++;
                else if (typeName[i] == '>') { depth--; if (depth == 0) return typeName.Substring(idx + n.Length + 1, i - idx - n.Length - 1); }
            }
        }
        return null;
    }

    private static (string K, string V)? ExtractTwoGenericArgs(string typeName, params string[] names)
    {
        var inner = ExtractGeneric(typeName, names);
        if (inner is null) return null;
        int depth = 0;
        for (int i = 0; i < inner.Length; i++)
        {
            if (inner[i] == '<') depth++;
            else if (inner[i] == '>') depth--;
            else if (inner[i] == ',' && depth == 0)
                return (inner.Substring(0, i).Trim(), inner.Substring(i + 1).Trim());
        }
        return null;
    }

    // ── name conversion (PascalCase → snake_case, SCREAMING_SNAKE) ─────────

    private static string ToSnakeCase(string name)
    {
        if (string.IsNullOrEmpty(name)) return name;
        var sb = new StringBuilder();
        for (int i = 0; i < name.Length; i++)
        {
            if (i > 0 && char.IsUpper(name[i]) && (char.IsLower(name[i - 1]) || (i + 1 < name.Length && char.IsLower(name[i + 1]))))
                sb.Append('_');
            sb.Append(char.ToLowerInvariant(name[i]));
        }
        return sb.ToString();
    }

    private static string ToScreamingSnakeCase(string name) => ToSnakeCase(name).ToUpperInvariant();

    private static string ResolveOutputDir(string? globalDir, SchemaModel model)
    {
        if (!string.IsNullOrEmpty(globalDir)) return globalDir!;
        var first = model.Classes.FirstOrDefault();
        return first?.OutputDir ?? ".";
    }
}
