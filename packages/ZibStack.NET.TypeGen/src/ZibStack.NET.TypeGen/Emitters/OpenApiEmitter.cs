    private static void EmitKeyParamYaml(StringBuilder sb, string keyName, (string TypeKey, string? FmtKey) keyType)
    {
        sb.AppendLine("      parameters:");
        sb.AppendLine($"        - name: {LowerFirst(keyName)}");
        sb.AppendLine("          in: path");
        sb.AppendLine("          required: true");
        sb.AppendLine("          schema:");
        sb.AppendLine($"            type: {keyType.TypeKey}");
        if (keyType.FmtKey != null) sb.AppendLine($"            format: {keyType.FmtKey}");
    }

    /// <summary>
    /// Resolves the route per <c>[CrudApi]</c> conventions: explicit <c>Route</c>
    /// wins; otherwise <c>api/{prefix}/{pluralized-class-name}</c>. Pluralization
    /// is naive (append <c>s</c>) — irregular nouns (Bus → Buses, Person → People)
    /// must use the explicit <c>Route</c> override.
    /// </summary>
    private static string ResolveRoute(SchemaClass cls)
    {
        var crud = cls.Crud!;
        if (!string.IsNullOrEmpty(crud.Route)) return crud.Route!;
        var prefix = crud.RoutePrefix?.Trim('/');
        var middle = string.IsNullOrEmpty(prefix) ? "" : prefix + "/";
        return $"api/{middle}{Pluralize(cls.SourceName).ToLowerInvariant()}";
    }

    private static string Pluralize(string name) => name + "s";

    private static string LowerFirst(string s) =>
        string.IsNullOrEmpty(s) ? s : char.ToLowerInvariant(s[0]) + s.Substring(1);

    /// <summary>
    /// Looks up the property matching <c>Crud.KeyProperty</c> and translates its
    /// C# type to OpenAPI type/format. Falls back to <c>integer/int32</c> when
    /// the property isn't found — same default as <c>[CrudApi]</c> assumes.
    /// </summary>
    private static (string TypeKey, string? FmtKey) ResolveKeyParamType(SchemaClass cls)
    {
        var key = cls.Properties.FirstOrDefault(p =>
            string.Equals(p.SourceName, cls.Crud!.KeyProperty, System.StringComparison.Ordinal));
        if (key is null) return ("integer", "int32");
        var mapped = MapCSharpToOpenApi(key.CSharpTypeFullName, new Dictionary<string, string>());
        return (mapped.TypeKey, mapped.FmtKey);
    }

    private static void EmitClassYaml(StringBuilder sb, SchemaClass cls, IReadOnlyDictionary<string, string> nameByCSharp, string indent)
    {
        // Polymorphic base → `oneOf` with `discriminator.mapping`. Variants emit
        // as their own schemas (via ordinary EmitClassYaml on the variant); this
        // base schema just points at them.
        if (cls.PolymorphicVariants.Count > 0 && cls.PolymorphicDiscriminator is not null)
        {
            var variantRefs = cls.PolymorphicVariants
                .Select(v => (v, name: nameByCSharp.TryGetValue(v.CSharpFullName, out var n) ? n : null))
                .Where(x => x.name is not null)
                .ToList();
            if (variantRefs.Count > 0)
            {
                sb.AppendLine($"{indent}{cls.EmittedName}:");
                sb.AppendLine($"{indent}  oneOf:");
                foreach (var (_, name) in variantRefs)
                    sb.AppendLine($"{indent}    - $ref: '#/components/schemas/{name}'");
                sb.AppendLine($"{indent}  discriminator:");
                sb.AppendLine($"{indent}    propertyName: {cls.PolymorphicDiscriminator}");
                sb.AppendLine($"{indent}    mapping:");
                foreach (var (v, name) in variantRefs)
                    sb.AppendLine($"{indent}      {v.DiscriminatorValue}: '#/components/schemas/{name}'");
                return;
            }
        }

        // Base in the emit set → allOf [{ $ref: base }, { type: object, properties: new-only }].
        // BUT: if our base is a polymorphic union, DON'T allOf into it — the base
        // is an abstract oneOf, not a concrete object. We still stamp the
        // discriminator literal via the normal props path (see EmitPropertyYaml).
        var baseRef = cls.BaseClassFullName is { } bfn
            && nameByCSharp.TryGetValue(bfn, out var bn)
            && cls.PolymorphicDiscriminatorValue is null
            ? bn : null;
        if (baseRef is not null)
        {
            sb.AppendLine($"{indent}{cls.EmittedName}:");
            sb.AppendLine($"{indent}  allOf:");
            sb.AppendLine($"{indent}    - $ref: '#/components/schemas/{baseRef}'");
            sb.AppendLine($"{indent}    - type: object");
            EmitClassBodyYaml(sb, cls, nameByCSharp, indent + "      ");
            return;
        }
        sb.AppendLine($"{indent}{cls.EmittedName}:");
        sb.AppendLine($"{indent}  type: object");
        EmitClassBodyYaml(sb, cls, nameByCSharp, indent + "  ");
    }

    private static void EmitClassBodyYaml(StringBuilder sb, SchemaClass cls, IReadOnlyDictionary<string, string> nameByCSharp, string indent)
    {
        var emittedProps = cls.Properties.Where(p => !p.OpenApiIgnore).ToList();
        var required = emittedProps.Where(p => !IsEffectivelyNullable(p)).ToList();
        // Polymorphic variant: discriminator is always present + required.
        var hasDiscProp = cls.PolymorphicDiscriminatorValue is not null
            && cls.PolymorphicDiscriminatorPropertyOnVariant is not null;

        if (required.Count > 0 || hasDiscProp)
        {
            sb.AppendLine($"{indent}required:");
            if (hasDiscProp) sb.AppendLine($"{indent}  - {cls.PolymorphicDiscriminatorPropertyOnVariant}");
            foreach (var p in required)
                sb.AppendLine($"{indent}  - {p.OpenApiNameOverride ?? p.SourceName}");
        }

        if (emittedProps.Count > 0 || hasDiscProp)
        {
            sb.AppendLine($"{indent}properties:");
            if (hasDiscProp)
            {
                // Discriminator as a string with single-value enum — OpenAPI 3.0
                // idiom for pinning a literal.
                sb.AppendLine($"{indent}  {cls.PolymorphicDiscriminatorPropertyOnVariant}:");
                sb.AppendLine($"{indent}    type: string");
                sb.AppendLine($"{indent}    enum:");
                sb.AppendLine($"{indent}      - {cls.PolymorphicDiscriminatorValue}");
            }
            foreach (var p in emittedProps)
                EmitPropertyYaml(sb, p, nameByCSharp, indent + "  ");
        }

        // [JsonExtensionData] property → additionalProperties on the parent schema
        // itself. Permissive (`true`) when the dictionary value is object / JsonElement,
        // typed schema otherwise.
        if (cls.AllowsAdditionalProperties)
        {
            if (cls.AdditionalPropertiesValueCSharpType is null)
            {
                sb.AppendLine($"{indent}additionalProperties: true");
            }
            else
            {
                var inner = MapCSharpToOpenApi(cls.AdditionalPropertiesValueCSharpType, nameByCSharp);
                sb.AppendLine($"{indent}additionalProperties:");
                if (inner.RefTarget is not null)
                    sb.AppendLine($"{indent}  $ref: '#/components/schemas/{inner.RefTarget}'");
                else
                {
                    sb.AppendLine($"{indent}  type: {inner.TypeKey}");
                    if (inner.FmtKey is not null) sb.AppendLine($"{indent}  format: {inner.FmtKey}");
                }
            }
        }
    }

    private static void EmitPropertyYaml(StringBuilder sb, SchemaProperty prop, IReadOnlyDictionary<string, string> nameByCSharp, string indent)
    {
        var name = prop.OpenApiNameOverride ?? prop.SourceName;
        sb.AppendLine($"{indent}{name}:");
        var (typeKey, fmtKey, refTarget, items) = ResolvePropertyShape(prop, nameByCSharp);
        if (refTarget != null)
        {
            // OpenAPI 3.0: nullable $ref needs allOf wrapping — a bare $ref can't carry siblings.
            if (IsEffectivelyNullable(prop))
            {
                sb.AppendLine($"{indent}  nullable: true");
                sb.AppendLine($"{indent}  allOf:");
                sb.AppendLine($"{indent}    - $ref: '#/components/schemas/{refTarget}'");
            }
            else
                sb.AppendLine($"{indent}  $ref: '#/components/schemas/{refTarget}'");
        }
        else
        {
            if (typeKey == "array")
            {
                sb.AppendLine($"{indent}  type: array");
                if (IsEffectivelyNullable(prop))
                    sb.AppendLine($"{indent}  nullable: true");
                sb.AppendLine($"{indent}  items:");
                if (items?.RefTarget != null)
                    sb.AppendLine($"{indent}    $ref: '#/components/schemas/{items.RefTarget}'");
                else
                {
                    sb.AppendLine($"{indent}    type: {items?.TypeKey ?? "object"}");
                    if (items?.FmtKey != null) sb.AppendLine($"{indent}    format: {items.FmtKey}");
                }
            }
            else if (typeKey == "dict")
            {
                sb.AppendLine($"{indent}  type: object");
                if (IsEffectivelyNullable(prop))
                    sb.AppendLine($"{indent}  nullable: true");
                sb.AppendLine($"{indent}  additionalProperties:");
                if (items?.RefTarget != null)
                    sb.AppendLine($"{indent}    $ref: '#/components/schemas/{items.RefTarget}'");
                else
                {
                    sb.AppendLine($"{indent}    type: {items?.TypeKey ?? "object"}");
                    if (items?.FmtKey != null) sb.AppendLine($"{indent}    format: {items.FmtKey}");
                }
            }
            else
            {
                sb.AppendLine($"{indent}  type: {typeKey}");
                if (fmtKey != null) sb.AppendLine($"{indent}  format: {fmtKey}");
                if (IsEffectivelyNullable(prop))
                    sb.AppendLine($"{indent}  nullable: true");
            }
        }

        // OpenApiProperty extras override / supplement the inferred shape.
        if (prop.OpenApiFormat != null)
            sb.AppendLine($"{indent}  format: {prop.OpenApiFormat}");
        if (prop.OpenApiDescription != null)
            sb.AppendLine($"{indent}  description: {YamlString(prop.OpenApiDescription)}");
        if (prop.OpenApiExample != null)
            sb.AppendLine($"{indent}  example: {YamlScalar(prop.OpenApiExample)}");

        // Validation-derived constraints — safe to emit even when not applicable
        // to the type (OpenAPI readers ignore minLength on integers, etc.).
        if (prop.MinLength is int minLen) sb.AppendLine($"{indent}  minLength: {minLen}");
        if (prop.MaxLength is int maxLen) sb.AppendLine($"{indent}  maxLength: {maxLen}");
        if (prop.Minimum is double minVal) sb.AppendLine($"{indent}  minimum: {FormatNumber(minVal)}");
        if (prop.Maximum is double maxVal) sb.AppendLine($"{indent}  maximum: {FormatNumber(maxVal)}");
        // Regex patterns commonly contain YAML-significant chars (|, *, ?, [, ~, &).
        // Always double-quote them — safer than relying on YamlString's heuristic.
        if (prop.Pattern is string pat)
            sb.AppendLine($"{indent}  pattern: \"{pat.Replace("\\", "\\\\").Replace("\"", "\\\"")}\"");
    }

    /// <summary>Trim ".0" from whole-double values to keep YAML tidy (e.g. 100, not 100.0).</summary>
    private static string FormatNumber(double v) =>
        v == System.Math.Floor(v) && !double.IsInfinity(v)
            ? ((long)v).ToString(CultureInfo.InvariantCulture)
            : v.ToString(CultureInfo.InvariantCulture);

    private static void EmitEnumYaml(StringBuilder sb, SchemaEnum en, string indent)
    {
        sb.AppendLine($"{indent}{en.EmittedName}:");
        sb.AppendLine($"{indent}  type: string");
        sb.AppendLine($"{indent}  enum:");
        foreach (var m in en.Members)
            sb.AppendLine($"{indent}    - {m.Name}");
    }

    // ── JSON emitter (when OutputPath ends with .json) ──────────────────────

    private static string EmitJson(SchemaModel model, OpenApiSettings oa, IReadOnlyDictionary<string, string> nameByCSharp)
    {
        // Minimal hand-rolled JSON to avoid a Newtonsoft / System.Text.Json dependency in
        // the analyzer DLL. Generator analyzers run inside the compiler — pulling in big
        // libraries here is risky, so we serialize manually for the small surface area.
        var sb = new StringBuilder();
        sb.AppendLine("{");
        sb.AppendLine($"  \"openapi\": {JsonString(oa.OpenApiVersion)},");
        sb.AppendLine("  \"info\": {");
        sb.AppendLine($"    \"title\": {JsonString(oa.Title)},");
        sb.Append($"    \"version\": {JsonString(oa.Version)}");
        if (!string.IsNullOrEmpty(oa.Description))
        {
            sb.AppendLine(",");
            sb.AppendLine($"    \"description\": {JsonString(oa.Description!)}");
        }
        else sb.AppendLine();
        sb.AppendLine("  },");
        EmitPathsJson(sb, model);
        sb.AppendLine("  \"components\": {");
        sb.AppendLine("    \"schemas\": {");

        var first = true;
        foreach (var cls in model.Classes)
        {
            if (cls.OpenApiIgnore || (cls.Targets & TypeTarget.OpenApi) == 0) continue;
            if (!first) sb.AppendLine(",");
            first = false;
            EmitClassJson(sb, cls, nameByCSharp, "      ");
        }
        foreach (var en in model.Enums)
        {
            if (en.OpenApiIgnore || (en.Targets & TypeTarget.OpenApi) == 0) continue;
            if (!first) sb.AppendLine(",");
            first = false;
            EmitEnumJson(sb, en, "      ");
        }
        foreach (var cls in model.Classes)
        {
            if (cls.Crud is null || (cls.Crud.Operations & CrudOperations.GetList) == 0) continue;
            if (cls.OpenApiIgnore || (cls.Targets & TypeTarget.OpenApi) == 0) continue;
            if (!first) sb.AppendLine(",");
            first = false;
            EmitPaginatedSchemaJson(sb, cls, "      ");
        }
        if (!first) sb.AppendLine();

        sb.AppendLine("    }");
        sb.AppendLine("  }");
        sb.AppendLine("}");
        return sb.ToString();
    }

    private static void EmitClassJson(StringBuilder sb, SchemaClass cls, IReadOnlyDictionary<string, string> nameByCSharp, string indent)
    {
        // Inheritance: emit allOf[{$ref: base}, {type: object, ...}] when the base is
        // in the model. Otherwise fall back to a single {type: object, ...} block.
        var baseRef = cls.BaseClassFullName is { } bfn && nameByCSharp.TryGetValue(bfn, out var bn) ? bn : null;
        var emittedProps = cls.Properties.Where(p => !p.OpenApiIgnore).ToList();
        var required = emittedProps.Where(p => !IsEffectivelyNullable(p)).ToList();

        sb.AppendLine($"{indent}{JsonString(cls.EmittedName)}: {{");
        if (baseRef is not null)
        {
            sb.AppendLine($"{indent}  \"allOf\": [");
            sb.AppendLine($"{indent}    {{ \"$ref\": \"#/components/schemas/{baseRef}\" }},");
            sb.Append($"{indent}    {{ \"type\": \"object\"");
            WriteRequiredAndPropsJson(sb, emittedProps, required, nameByCSharp, indent + "    ");
            sb.AppendLine();
            sb.AppendLine($"{indent}    }}");
            sb.AppendLine($"{indent}  ]");
        }
        else
        {
            sb.Append($"{indent}  \"type\": \"object\"");
            WriteRequiredAndPropsJson(sb, emittedProps, required, nameByCSharp, indent + "  ");
            sb.AppendLine();
        }
        sb.Append($"{indent}}}");
    }

    private static void WriteRequiredAndPropsJson(StringBuilder sb, List<SchemaProperty> emittedProps, List<SchemaProperty> required, IReadOnlyDictionary<string, string> nameByCSharp, string indent)
    {
        if (required.Count > 0)
        {
            sb.AppendLine(",");
            sb.Append($"{indent}\"required\": [");
            sb.Append(string.Join(", ", required.Select(r => JsonString(r.OpenApiNameOverride ?? r.SourceName))));
            sb.Append(']');
        }
        if (emittedProps.Count > 0)
        {
            sb.AppendLine(",");
            sb.AppendLine($"{indent}\"properties\": {{");
            for (int i = 0; i < emittedProps.Count; i++)
            {
                EmitPropertyJson(sb, emittedProps[i], nameByCSharp, indent + "  ");
                if (i < emittedProps.Count - 1) sb.AppendLine(",");
                else sb.AppendLine();
            }
            sb.Append($"{indent}}}");
        }
    }

    private static void EmitPropertyJson(StringBuilder sb, SchemaProperty prop, IReadOnlyDictionary<string, string> nameByCSharp, string indent)
    {
        var name = prop.OpenApiNameOverride ?? prop.SourceName;
        sb.Append($"{indent}{JsonString(name)}: {{ ");
        var (typeKey, fmtKey, refTarget, items) = ResolvePropertyShape(prop, nameByCSharp);
        var nullable = IsEffectivelyNullable(prop);
        if (refTarget != null)
        {
            if (nullable)
                sb.Append($"\"nullable\": true, \"allOf\": [{{ \"$ref\": \"#/components/schemas/{refTarget}\" }}]");
            else
                sb.Append($"\"$ref\": \"#/components/schemas/{refTarget}\"");
        }
        else if (typeKey == "array")
        {
            sb.Append("\"type\": \"array\"");
            if (nullable) sb.Append(", \"nullable\": true");
            sb.Append(", \"items\": { ");
            if (items?.RefTarget != null)
                sb.Append($"\"$ref\": \"#/components/schemas/{items.RefTarget}\"");
            else
            {
                sb.Append($"\"type\": \"{items?.TypeKey ?? "object"}\"");
                if (items?.FmtKey != null) sb.Append($", \"format\": \"{items.FmtKey}\"");
            }
            sb.Append(" }");
        }
        else if (typeKey == "dict")
        {
            sb.Append("\"type\": \"object\"");
            if (nullable) sb.Append(", \"nullable\": true");
            sb.Append(", \"additionalProperties\": { ");
            if (items?.RefTarget != null)
                sb.Append($"\"$ref\": \"#/components/schemas/{items.RefTarget}\"");
            else
            {
                sb.Append($"\"type\": \"{items?.TypeKey ?? "object"}\"");
                if (items?.FmtKey != null) sb.Append($", \"format\": \"{items.FmtKey}\"");
            }
            sb.Append(" }");
        }
        else
        {
            sb.Append($"\"type\": \"{typeKey}\"");
            if (fmtKey != null) sb.Append($", \"format\": \"{fmtKey}\"");
            if (nullable) sb.Append(", \"nullable\": true");
        }
        if (prop.OpenApiDescription != null)
            sb.Append($", \"description\": {JsonString(prop.OpenApiDescription)}");
        if (prop.MinLength is int minLen) sb.Append($", \"minLength\": {minLen}");
        if (prop.MaxLength is int maxLen) sb.Append($", \"maxLength\": {maxLen}");
        if (prop.Minimum is double minVal) sb.Append($", \"minimum\": {FormatNumber(minVal)}");
        if (prop.Maximum is double maxVal) sb.Append($", \"maximum\": {FormatNumber(maxVal)}");
        if (prop.Pattern is string pat) sb.Append($", \"pattern\": {JsonString(pat)}");
        sb.Append(" }");
    }

    private static void EmitPaginatedSchemaJson(StringBuilder sb, SchemaClass cls, string indent)
    {
        sb.AppendLine($"{indent}{JsonString(PaginatedSchemaName(cls))}: {{");
        sb.AppendLine($"{indent}  \"type\": \"object\",");
        sb.AppendLine($"{indent}  \"required\": [\"items\", \"totalCount\", \"page\", \"pageSize\", \"totalPages\", \"hasNextPage\", \"hasPreviousPage\"],");
        sb.AppendLine($"{indent}  \"properties\": {{");
        sb.AppendLine($"{indent}    \"items\": {{ \"type\": \"array\", \"items\": {{ \"$ref\": \"#/components/schemas/{cls.EmittedName}\" }} }},");
        sb.AppendLine($"{indent}    \"totalCount\": {{ \"type\": \"integer\", \"format\": \"int32\" }},");
        sb.AppendLine($"{indent}    \"page\": {{ \"type\": \"integer\", \"format\": \"int32\" }},");
        sb.AppendLine($"{indent}    \"pageSize\": {{ \"type\": \"integer\", \"format\": \"int32\" }},");
        sb.AppendLine($"{indent}    \"totalPages\": {{ \"type\": \"integer\", \"format\": \"int32\" }},");
        sb.AppendLine($"{indent}    \"hasNextPage\": {{ \"type\": \"boolean\" }},");
        sb.AppendLine($"{indent}    \"hasPreviousPage\": {{ \"type\": \"boolean\" }}");
        sb.AppendLine($"{indent}  }}");
        sb.Append($"{indent}}}");
    }

    private static void EmitEnumJson(StringBuilder sb, SchemaEnum en, string indent)
    {
        sb.AppendLine($"{indent}{JsonString(en.EmittedName)}: {{");
        sb.AppendLine($"{indent}  \"type\": \"string\",");
        sb.Append($"{indent}  \"enum\": [");
        sb.Append(string.Join(", ", en.Members.Select(m => JsonString(m.Name))));
        sb.AppendLine("]");
        sb.Append($"{indent}}}");
    }

    /// <summary>
    /// JSON paths emitter. Mirrors <see cref="EmitPathsYaml"/> but produces compact
    /// JSON. Schema content is intentionally minimal: <c>$ref</c> + <c>responses</c> +
    /// <c>requestBody</c> + path parameters. Keeps in lockstep with YAML.
    /// </summary>
    private static void EmitPathsJson(StringBuilder sb, SchemaModel model)
    {
        var crudClasses = model.Classes.Where(c => c.Crud is not null && (c.Targets & TypeTarget.OpenApi) != 0).ToList();
        if (crudClasses.Count == 0) { sb.AppendLine("  \"paths\": {},"); return; }

        sb.AppendLine("  \"paths\": {");
        var pathEntries = new List<(string Path, List<(string Verb, string Body)> Verbs)>();
        foreach (var cls in crudClasses)
        {
            var route = "/" + ResolveRoute(cls).TrimStart('/');
            var keyType = ResolveKeyParamType(cls);
            var itemPath = route + "/{" + LowerFirst(cls.Crud!.KeyProperty) + "}";
            var ops = cls.Crud.Operations;

            var coll = new List<(string, string)>();
            var item = new List<(string, string)>();
            if ((ops & CrudOperations.GetList) != 0) coll.Add(("get", BuildListJson(cls)));
            if ((ops & CrudOperations.Create) != 0) coll.Add(("post", BuildCreateJson(cls)));
            if ((ops & CrudOperations.GetById) != 0) item.Add(("get", BuildGetByIdJson(cls, keyType)));
            if ((ops & CrudOperations.Update) != 0) item.Add(("patch", BuildUpdateJson(cls, keyType)));
            if ((ops & CrudOperations.Delete) != 0) item.Add(("delete", BuildDeleteJson(cls, keyType)));

            if (coll.Count > 0) pathEntries.Add((route, coll));
            if (item.Count > 0) pathEntries.Add((itemPath, item));

            if ((ops & CrudOperations.BulkCreate) != 0)
                pathEntries.Add((route + "/bulk", new() { ("post", BuildBulkCreateJson(cls)) }));
            if ((ops & CrudOperations.BulkDelete) != 0)
                pathEntries.Add((route + "/bulk-delete", new() { ("post", BuildBulkDeleteJson(cls, keyType)) }));
        }

        for (int i = 0; i < pathEntries.Count; i++)
        {
            var (path, verbs) = pathEntries[i];
            sb.Append($"    {JsonString(path)}: {{ ");
            for (int j = 0; j < verbs.Count; j++)
            {
                sb.Append($"\"{verbs[j].Verb}\": {verbs[j].Body}");
                if (j < verbs.Count - 1) sb.Append(", ");
            }
            sb.Append(" }");
            if (i < pathEntries.Count - 1) sb.Append(',');
            sb.AppendLine();
        }
        sb.AppendLine("  },");
    }

    private static string BuildListJson(SchemaClass cls) =>
        $"{{ \"tags\": [\"{cls.EmittedName}\"], \"operationId\": \"list{cls.EmittedName}\", " +
        $"\"parameters\": [{BuildListQueryParamsJson(cls)}], " +
        $"\"responses\": {{ \"200\": {{ \"description\": \"OK\", \"content\": {{ \"application/json\": " +
        $"{{ \"schema\": {{ \"$ref\": \"#/components/schemas/{PaginatedSchemaName(cls)}\" }} }} }} }} }} }}";

    private static string BuildListQueryParamsJson(SchemaClass cls)
    {
        var parts = new List<string>
        {
            QueryParamJson("page", "integer", "int32", "Page number (1-based)."),
            QueryParamJson("pageSize", "integer", "int32", "Items per page."),
        };
        if (cls.QueryDsl)
        {
            parts.Add(QueryParamJson("filter", "string", null, "Filter DSL."));
            parts.Add(QueryParamJson("sort", "string", null, "Sort DSL."));
            parts.Add(QueryParamJson("select", "string", null, "Projection."));
            parts.Add(QueryParamJson("count", "boolean", null, "Returns only { count } when true."));
        }
        return string.Join(", ", parts);
    }

