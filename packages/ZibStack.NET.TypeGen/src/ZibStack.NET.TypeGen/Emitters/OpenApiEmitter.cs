    // ── shared mapping ──────────────────────────────────────────────────────

    /// <summary>
    /// Resolves the emitted shape for one property. Fluent <c>OpenApiRef</c> wins
    /// (emit <c>$ref</c> to the named schema, ignore inferred type entirely);
    /// fluent <c>OpenApiType</c> overrides the primary type but keeps inferred
    /// items / format unless those are also overridden. With no overrides this is
    /// just <see cref="MapCSharpToOpenApi"/> — backwards-compatible default.
    /// </summary>
    private static (string TypeKey, string? FmtKey, string? RefTarget, OpenApiTypeMapping? Items) ResolvePropertyShape(
        SchemaProperty prop, IReadOnlyDictionary<string, string> nameByCSharp)
    {
        if (prop.OpenApiRefOverride is { } refName)
            return ("$ref", null, refName, null);
        // `[UseType<T>]` — cross-target override. If T is in the model, emit a
        // $ref; if not, fall through (the target lives outside the schema — user
        // should supply OpenApiRefOverride or OpenApiTypeOverride explicitly).
        if (prop.TargetTypeCSharpFqn is { } useTypeFqn
            && nameByCSharp.TryGetValue(useTypeFqn, out var useTypeName))
            return ("$ref", null, useTypeName, null);
        var inferred = MapCSharpToOpenApi(prop.CSharpTypeFullName, nameByCSharp);
        if (prop.OpenApiTypeOverride is { } typeOverride)
            // Drop the inferred format — it was chosen to match the inferred type
            // (e.g. "double" for number) and doesn't carry over to the override.
            // The user's explicit OpenApiFormat (if any) is appended later by the caller.
            return (typeOverride, null, null, null);
        return inferred;
    }

    private sealed record OpenApiTypeMapping(string TypeKey, string? FmtKey, string? RefTarget);

    private static (string TypeKey, string? FmtKey, string? RefTarget, OpenApiTypeMapping? Items) MapCSharpToOpenApi(
        string cSharpType,
        IReadOnlyDictionary<string, string> nameByCSharp)
    {
        var t = cSharpType.TrimEnd('?');

        // PatchField<T> is ZibStack.NET.Dto's tri-state wrapper for Create/Update DTOs.
        // OpenAPI doesn't have a wire notion of tri-state — unwrap to the underlying
        // type so the schema reads like a plain field.
        var patchInner = ExtractGeneric(t, "PatchField");
        if (patchInner != null) return MapCSharpToOpenApi(patchInner, nameByCSharp);

        if (nameByCSharp.TryGetValue(t, out var refName))
            return ("$ref", null, refName, null);

        // Arrays / lists → type: array, items: ...
        if (t.EndsWith("[]"))
        {
            var inner = MapCSharpToOpenApi(t.Substring(0, t.Length - 2), nameByCSharp);
            return ("array", null, null, new OpenApiTypeMapping(inner.TypeKey, inner.FmtKey, inner.RefTarget));
        }
        var listInner = ExtractGeneric(t, "List", "IList", "ICollection", "IEnumerable", "IReadOnlyList", "IReadOnlyCollection");
        if (listInner != null)
        {
            var inner = MapCSharpToOpenApi(listInner, nameByCSharp);
            return ("array", null, null, new OpenApiTypeMapping(inner.TypeKey, inner.FmtKey, inner.RefTarget));
        }

        // Dictionary<K, V> → type: object with additionalProperties describing V.
        // OpenAPI only supports string keys, so K is ignored (same constraint as
        // JSON itself). Non-string key dicts still emit valid OpenAPI — the $ref
        // simply won't round-trip exact typing, but the shape is accurate enough
        // for consumers that serialize dicts to JSON objects.
        var dict = ExtractTwoGenericArgs(t, "Dictionary", "IDictionary", "IReadOnlyDictionary");
        if (dict != null)
        {
            var valueInner = MapCSharpToOpenApi(dict.Value.V, nameByCSharp);
            return ("dict", null, null, new OpenApiTypeMapping(valueInner.TypeKey, valueInner.FmtKey, valueInner.RefTarget));
        }

        return t switch
        {
            "string" => ("string", null, null, null),
            "bool" or "System.Boolean" => ("boolean", null, null, null),
            "byte" or "sbyte" => ("integer", "int32", null, null),
            "short" or "ushort" or "int" or "uint" or "System.Int32" => ("integer", "int32", null, null),
            "long" or "ulong" or "System.Int64" => ("integer", "int64", null, null),
            "float" or "System.Single" => ("number", "float", null, null),
            "double" or "System.Double" => ("number", "double", null, null),
            "decimal" or "System.Decimal" => ("number", "double", null, null),
            "System.Guid" or "Guid" => ("string", "uuid", null, null),
            "System.DateTime" or "DateTime" or "System.DateTimeOffset" or "DateTimeOffset" => ("string", "date-time", null, null),
            "System.DateOnly" or "DateOnly" => ("string", "date", null, null),
            "System.TimeOnly" or "TimeOnly" or "System.TimeSpan" or "TimeSpan" => ("string", "time", null, null),
            _ => ("object", null, null, null),
        };
    }

    private static (string K, string V)? ExtractTwoGenericArgs(string typeName, params string[] names)
    {
        var inner = ExtractGeneric(typeName, names);
        if (inner is null) return null;
        int depth = 0;
        for (int i = 0; i < inner.Length; i++)
        {
            if (inner[i] == '<') depth++;
            else if (inner[i] == '>') depth--;
            else if (inner[i] == ',' && depth == 0)
                return (inner.Substring(0, i).Trim(), inner.Substring(i + 1).Trim());
        }
        return null;
    }

    private static string? ExtractGeneric(string typeName, params string[] names)
    {
        foreach (var n in names)
        {
            var idx = typeName.IndexOf(n + "<", System.StringComparison.Ordinal);
            if (idx < 0) continue;
            if (idx > 0 && char.IsLetterOrDigit(typeName[idx - 1])) continue;
            int depth = 1;
            for (int i = idx + n.Length + 1; i < typeName.Length; i++)
            {
                if (typeName[i] == '<') depth++;
                else if (typeName[i] == '>') { depth--; if (depth == 0) return typeName.Substring(idx + n.Length + 1, i - idx - n.Length - 1); }
            }
        }
        return null;
    }

    private static bool IsEffectivelyNullable(SchemaProperty p) =>
        p.OpenApiNullableOverride ?? p.IsNullable;

    // ── YAML/JSON scalar formatters ──

    private static string YamlString(string s) =>
        s.Contains(':') || s.Contains('#') || s.Contains('\n') || s.StartsWith("'") || s.StartsWith("\"")
            ? $"\"{s.Replace("\"", "\\\"")}\""
            : s;

    private static string YamlScalar(object value) => value switch
    {
        string s => YamlString(s),
        bool b => b ? "true" : "false",
        null => "null",
        _ => System.Convert.ToString(value, CultureInfo.InvariantCulture) ?? "",
    };

    private static string JsonString(string s)
    {
        var sb = new StringBuilder("\"");
        foreach (var c in s)
        {
            switch (c)
            {
                case '"': sb.Append("\\\""); break;
                case '\\': sb.Append("\\\\"); break;
                case '\n': sb.Append("\\n"); break;
                case '\r': sb.Append("\\r"); break;
                case '\t': sb.Append("\\t"); break;
                default:
                    if (c < 0x20) sb.Append($"\\u{(int)c:x4}");
                    else sb.Append(c);
                    break;
            }
        }
        sb.Append('"');
        return sb.ToString();
    }
