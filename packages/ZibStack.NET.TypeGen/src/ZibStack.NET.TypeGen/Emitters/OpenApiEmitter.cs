using System.Collections.Generic;
using System.Globalization;
using System.Linq;
using System.Text;

namespace ZibStack.NET.TypeGen.Generator;

/// <summary>
/// Produces an OpenAPI 3.1 document from a <see cref="SchemaModel"/>. MVP coverage:
/// <c>info</c>, <c>components/schemas</c> populated from <c>[GenerateTypes]</c>
/// classes/enums. <c>paths</c> are out of scope until the <c>[CrudApi]</c>
/// integration ships in a follow-up.
///
/// <para>
/// Format follows the file extension on <see cref="OpenApiSettings.OutputPath"/>:
/// <c>.json</c> emits JSON, anything else emits YAML.
/// </para>
/// </summary>
internal static class OpenApiEmitter
{
    public static IReadOnlyList<EmittedFile> Emit(SchemaModel model, GlobalSettings settings)
    {
        var oa = settings.OpenApi;
        var emitJson = oa.OutputPath.EndsWith(".json", System.StringComparison.OrdinalIgnoreCase);

        // Stamp global flags onto classes so emitter helpers can read them without
        // threading settings through every call.
        foreach (var cls in model.Classes) cls.QueryDsl = settings.HasQueryDsl;

        // Resolve emitted schema names — ['Order' if no override, otherwise OpenApiSchemaName].
        // Stored back so $ref resolution works across classes referencing each other.
        foreach (var cls in model.Classes)
        {
            if (cls.OpenApiIgnore || (cls.Targets & TypeTarget.OpenApi) == 0) continue;
            cls.EmittedName = cls.OpenApiNameOverride ?? cls.SourceName;
        }
        foreach (var en in model.Enums)
        {
            if (en.OpenApiIgnore || (en.Targets & TypeTarget.OpenApi) == 0) continue;
            en.EmittedName = en.OpenApiNameOverride ?? en.SourceName;
        }

        var nameByCSharp = new Dictionary<string, string>();
        foreach (var c in model.Classes)
            if (!c.OpenApiIgnore && (c.Targets & TypeTarget.OpenApi) != 0)
                nameByCSharp[c.CSharpFullName] = c.EmittedName;
        foreach (var e in model.Enums)
            if (!e.OpenApiIgnore && (e.Targets & TypeTarget.OpenApi) != 0)
                nameByCSharp[e.CSharpFullName] = e.EmittedName;

        var content = emitJson
            ? EmitJson(model, oa, nameByCSharp)
            : EmitYaml(model, oa, nameByCSharp);

        // Split path into dir + filename. If the user gave only a filename, route it
        // under the first emitting class's OutputDir so `[GenerateTypes(OutputDir="generated")]`
        // keeps the schema next to its TypeScript siblings instead of dumping in the root.
        var path = oa.OutputPath.Replace('\\', '/');
        var lastSlash = path.LastIndexOf('/');
        string dir;
        string file;
        if (lastSlash >= 0)
        {
            dir = path.Substring(0, lastSlash);
            file = path.Substring(lastSlash + 1);
        }
        else
        {
            var firstCls = model.Classes.FirstOrDefault(c => !c.OpenApiIgnore && (c.Targets & TypeTarget.OpenApi) != 0);
            var firstEn = model.Enums.FirstOrDefault(e => !e.OpenApiIgnore && (e.Targets & TypeTarget.OpenApi) != 0);
            dir = firstCls?.OutputDir ?? firstEn?.OutputDir ?? ".";
            file = path;
        }

        return new[] { new EmittedFile(TypeTarget.OpenApi, dir, file, content) };
    }

    // ── YAML emitter (default) ──────────────────────────────────────────────

    private static string EmitYaml(SchemaModel model, OpenApiSettings oa, IReadOnlyDictionary<string, string> nameByCSharp)
    {
        var sb = new StringBuilder();
        sb.AppendLine("# @generated by ZibStack.NET.TypeGen — do not edit");
        sb.AppendLine($"openapi: {oa.OpenApiVersion}");
        sb.AppendLine("info:");
        sb.AppendLine($"  title: {YamlString(oa.Title)}");
        sb.AppendLine($"  version: {YamlString(oa.Version)}");
        if (!string.IsNullOrEmpty(oa.Description))
            sb.AppendLine($"  description: {YamlString(oa.Description!)}");
        EmitPathsYaml(sb, model, nameByCSharp);
        sb.AppendLine("components:");
        sb.AppendLine("  schemas:");

        foreach (var cls in model.Classes)
        {
            if (cls.OpenApiIgnore || (cls.Targets & TypeTarget.OpenApi) == 0) continue;
            EmitClassYaml(sb, cls, nameByCSharp, indent: "    ");
        }
        foreach (var en in model.Enums)
        {
            if (en.OpenApiIgnore || (en.Targets & TypeTarget.OpenApi) == 0) continue;
            EmitEnumYaml(sb, en, indent: "    ");
        }

        // PaginatedResponseOf{Class} auxiliary schemas for every GetList-enabled CRUD class.
        EmitPaginatedWrappersYaml(sb, model, indent: "    ");

        return sb.ToString();
    }

    // ── paths emission (driven by [CrudApi]) ─────────────────────────────────

    /// <summary>
    /// Emits the full <c>paths:</c> block from any class carrying <c>[CrudApi]</c>.
    /// Convention: response shape is the class itself (<c>$ref</c> by emitted name);
    /// request bodies are <c>Create{Class}Request</c> / <c>Update{Class}Request</c> —
    /// users are responsible for ensuring those exist as schemas (typically by also
    /// putting <c>[GenerateTypes]</c> on the request DTOs).
    /// </summary>
    private static void EmitPathsYaml(StringBuilder sb, SchemaModel model, IReadOnlyDictionary<string, string> nameByCSharp)
    {
        var crudClasses = model.Classes.Where(c => c.Crud is not null && (c.Targets & TypeTarget.OpenApi) != 0).ToList();
        if (crudClasses.Count == 0) { sb.AppendLine("paths: {}"); return; }

        sb.AppendLine("paths:");
        foreach (var cls in crudClasses)
        {
            var route = ResolveRoute(cls);
            var keyType = ResolveKeyParamType(cls);
            var collectionPath = "/" + route.TrimStart('/');
            var itemPath = collectionPath + "/{" + LowerFirst(cls.Crud!.KeyProperty) + "}";
            var ops = cls.Crud.Operations;

            // Group by path so multiple verbs share a single key.
            var collectionVerbs = new List<(string Verb, System.Action<StringBuilder> Emit)>();
            var itemVerbs = new List<(string Verb, System.Action<StringBuilder> Emit)>();

            if ((ops & CrudOperations.GetList) != 0)
                collectionVerbs.Add(("get", b => EmitListOpYaml(b, cls)));
            if ((ops & CrudOperations.Create) != 0)
                collectionVerbs.Add(("post", b => EmitCreateOpYaml(b, cls)));
            if ((ops & CrudOperations.GetById) != 0)
                itemVerbs.Add(("get", b => EmitGetByIdOpYaml(b, cls, keyType)));
            if ((ops & CrudOperations.Update) != 0)
                itemVerbs.Add(("patch", b => EmitUpdateOpYaml(b, cls, keyType)));
            if ((ops & CrudOperations.Delete) != 0)
                itemVerbs.Add(("delete", b => EmitDeleteOpYaml(b, cls, keyType)));

            if (collectionVerbs.Count > 0) EmitPathBlockYaml(sb, collectionPath, collectionVerbs);
            if (itemVerbs.Count > 0) EmitPathBlockYaml(sb, itemPath, itemVerbs);

            // Bulk ops sit on separate sub-paths — the Dto generator puts them under
            // /bulk and /bulk-delete. Emit only if their flag bit is set.
            if ((ops & CrudOperations.BulkCreate) != 0)
                EmitPathBlockYaml(sb, collectionPath + "/bulk",
                    new() { ("post", b => EmitBulkCreateOpYaml(b, cls)) });
            if ((ops & CrudOperations.BulkDelete) != 0)
                EmitPathBlockYaml(sb, collectionPath + "/bulk-delete",
                    new() { ("post", b => EmitBulkDeleteOpYaml(b, cls, keyType)) });
        }
    }

    private static void EmitPathBlockYaml(StringBuilder sb, string path, List<(string Verb, System.Action<StringBuilder> Emit)> verbs)
    {
        sb.AppendLine($"  {path}:");
        foreach (var (verb, emit) in verbs)
        {
            sb.AppendLine($"    {verb}:");
            emit(sb);
        }
    }

    private static void EmitListOpYaml(StringBuilder sb, SchemaClass cls)
    {
        sb.AppendLine($"      tags: [{cls.EmittedName}]");
        sb.AppendLine($"      operationId: list{cls.EmittedName}");
        EmitListQueryParamsYaml(sb, cls);
        sb.AppendLine("      responses:");
        sb.AppendLine("        '200':");
        sb.AppendLine("          description: OK");
        sb.AppendLine("          content:");
        sb.AppendLine("            application/json:");
        sb.AppendLine($"              schema: {{ $ref: '#/components/schemas/{PaginatedSchemaName(cls)}' }}");
    }

    private static string PaginatedSchemaName(SchemaClass cls) => $"PaginatedResponseOf{cls.EmittedName}";

    private static void EmitListQueryParamsYaml(StringBuilder sb, SchemaClass cls)
    {
        // page + pageSize are always emitted by the Dto generator — match them unconditionally.
        // filter/sort/select/count only exist when ZibStack.NET.Query is referenced.
        sb.AppendLine("      parameters:");
        EmitQueryParamYaml(sb, "page", "integer", "int32", "Page number (1-based).");
        EmitQueryParamYaml(sb, "pageSize", "integer", "int32", "Items per page.");
        if (cls.QueryDsl)
        {
            EmitQueryParamYaml(sb, "filter", "string", null, "Filter DSL — 'Field>value,Field2=*text'.");
            EmitQueryParamYaml(sb, "sort", "string", null, "Sort DSL — '-Field,Field2' (prefix '-' for desc).");
            EmitQueryParamYaml(sb, "select", "string", null, "Projection — 'Field1,Field2'.");
            EmitQueryParamYaml(sb, "count", "boolean", null, "When true, returns only { count: int }.");
        }
    }

    private static void EmitQueryParamYaml(StringBuilder sb, string name, string typeKey, string? fmtKey, string description)
    {
        sb.AppendLine($"        - name: {name}");
        sb.AppendLine("          in: query");
        sb.AppendLine($"          description: {YamlString(description)}");
        sb.AppendLine("          schema:");
        sb.AppendLine($"            type: {typeKey}");
        if (fmtKey != null) sb.AppendLine($"            format: {fmtKey}");
    }

    private static void EmitBulkCreateOpYaml(StringBuilder sb, SchemaClass cls)
    {
        var req = $"Create{cls.SourceName}Request";
        sb.AppendLine($"      tags: [{cls.EmittedName}]");
        sb.AppendLine($"      operationId: bulkCreate{cls.EmittedName}");
        sb.AppendLine("      requestBody:");
        sb.AppendLine("        required: true");
        sb.AppendLine("        content:");
        sb.AppendLine("          application/json:");
        sb.AppendLine("            schema:");
        sb.AppendLine("              type: array");
        sb.AppendLine($"              items: {{ $ref: '#/components/schemas/{req}' }}");
        sb.AppendLine("      responses:");
        sb.AppendLine("        '201':");
        sb.AppendLine("          description: Created");
        sb.AppendLine("          content:");
        sb.AppendLine("            application/json:");
        sb.AppendLine("              schema:");
        sb.AppendLine("                type: array");
        sb.AppendLine($"                items: {{ $ref: '#/components/schemas/{cls.EmittedName}' }}");
    }

    private static void EmitBulkDeleteOpYaml(StringBuilder sb, SchemaClass cls, (string TypeKey, string? FmtKey) keyType)
    {
        sb.AppendLine($"      tags: [{cls.EmittedName}]");
        sb.AppendLine($"      operationId: bulkDelete{cls.EmittedName}");
        sb.AppendLine("      requestBody:");
        sb.AppendLine("        required: true");
        sb.AppendLine("        content:");
        sb.AppendLine("          application/json:");
        sb.AppendLine("            schema:");
        sb.AppendLine("              type: array");
        sb.AppendLine($"              items: {{ type: {keyType.TypeKey}{(keyType.FmtKey is null ? "" : $", format: {keyType.FmtKey}")} }}");
        sb.AppendLine("      responses:");
        sb.AppendLine("        '204':");
        sb.AppendLine("          description: No Content");
    }

    /// <summary>
    /// For each CRUD class with GetList enabled, emits a <c>PaginatedResponseOf{Class}</c>
    /// schema whose shape mirrors <c>ZibStack.NET.Dto.PaginatedResponse&lt;T&gt;</c> at runtime.
    /// Called from the components/schemas block so list endpoints can <c>$ref</c> to it.
    /// </summary>
    private static void EmitPaginatedWrappersYaml(StringBuilder sb, SchemaModel model, string indent)
    {
        foreach (var cls in model.Classes)
        {
            if (cls.Crud is null || (cls.Crud.Operations & CrudOperations.GetList) == 0) continue;
            if ((cls.Targets & TypeTarget.OpenApi) == 0 || cls.OpenApiIgnore) continue;
            EmitPaginatedSchemaYaml(sb, cls, indent);
        }
    }

    private static void EmitPaginatedSchemaYaml(StringBuilder sb, SchemaClass cls, string indent)
    {
        sb.AppendLine($"{indent}{PaginatedSchemaName(cls)}:");
        sb.AppendLine($"{indent}  type: object");
        sb.AppendLine($"{indent}  required: [items, totalCount, page, pageSize, totalPages, hasNextPage, hasPreviousPage]");
        sb.AppendLine($"{indent}  properties:");
        sb.AppendLine($"{indent}    items:");
        sb.AppendLine($"{indent}      type: array");
        sb.AppendLine($"{indent}      items: {{ $ref: '#/components/schemas/{cls.EmittedName}' }}");
        foreach (var (n, t, f) in new[] {
            ("totalCount", "integer", "int32"),
            ("page", "integer", "int32"),
            ("pageSize", "integer", "int32"),
            ("totalPages", "integer", "int32"),
        })
        {
            sb.AppendLine($"{indent}    {n}: {{ type: {t}, format: {f} }}");
        }
        sb.AppendLine($"{indent}    hasNextPage: {{ type: boolean }}");
        sb.AppendLine($"{indent}    hasPreviousPage: {{ type: boolean }}");
    }

