using System.Collections.Generic;
using System.Globalization;
using System.Linq;
using System.Text;

namespace ZibStack.NET.TypeGen.Generator;

/// <summary>
/// Produces an OpenAPI 3.1 document from a <see cref="SchemaModel"/>. MVP coverage:
/// <c>info</c>, <c>components/schemas</c> populated from <c>[GenerateTypes]</c>
/// classes/enums. <c>paths</c> are out of scope until the <c>[CrudApi]</c>
/// integration ships in a follow-up.
///
/// <para>
/// Format follows the file extension on <see cref="OpenApiSettings.OutputPath"/>:
/// <c>.json</c> emits JSON, anything else emits YAML.
/// </para>
/// </summary>
internal static class OpenApiEmitter
{
    public static IReadOnlyList<EmittedFile> Emit(SchemaModel model, GlobalSettings settings)
    {
        var oa = settings.OpenApi;
        var emitJson = oa.OutputPath.EndsWith(".json", System.StringComparison.OrdinalIgnoreCase);

        // Resolve emitted schema names — ['Order' if no override, otherwise OpenApiSchemaName].
        // Stored back so $ref resolution works across classes referencing each other.
        foreach (var cls in model.Classes)
        {
            if (cls.OpenApiIgnore || (cls.Targets & TypeTarget.OpenApi) == 0) continue;
            cls.EmittedName = cls.OpenApiNameOverride ?? cls.SourceName;
        }
        foreach (var en in model.Enums)
        {
            if (en.OpenApiIgnore || (en.Targets & TypeTarget.OpenApi) == 0) continue;
            en.EmittedName = en.OpenApiNameOverride ?? en.SourceName;
        }

        var nameByCSharp = new Dictionary<string, string>();
        foreach (var c in model.Classes)
            if (!c.OpenApiIgnore && (c.Targets & TypeTarget.OpenApi) != 0)
                nameByCSharp[c.CSharpFullName] = c.EmittedName;
        foreach (var e in model.Enums)
            if (!e.OpenApiIgnore && (e.Targets & TypeTarget.OpenApi) != 0)
                nameByCSharp[e.CSharpFullName] = e.EmittedName;

        var content = emitJson
            ? EmitJson(model, oa, nameByCSharp)
            : EmitYaml(model, oa, nameByCSharp);

        // Split path into dir + filename. If the user gave only a filename, route it
        // under the first emitting class's OutputDir so `[GenerateTypes(OutputDir="generated")]`
        // keeps the schema next to its TypeScript siblings instead of dumping in the root.
        var path = oa.OutputPath.Replace('\\', '/');
        var lastSlash = path.LastIndexOf('/');
        string dir;
        string file;
        if (lastSlash >= 0)
        {
            dir = path.Substring(0, lastSlash);
            file = path.Substring(lastSlash + 1);
        }
        else
        {
            var firstCls = model.Classes.FirstOrDefault(c => !c.OpenApiIgnore && (c.Targets & TypeTarget.OpenApi) != 0);
            var firstEn = model.Enums.FirstOrDefault(e => !e.OpenApiIgnore && (e.Targets & TypeTarget.OpenApi) != 0);
            dir = firstCls?.OutputDir ?? firstEn?.OutputDir ?? ".";
            file = path;
        }

        return new[] { new EmittedFile(TypeTarget.OpenApi, dir, file, content) };
    }

    // ── YAML emitter (default) ──────────────────────────────────────────────

    private static string EmitYaml(SchemaModel model, OpenApiSettings oa, IReadOnlyDictionary<string, string> nameByCSharp)
    {
        var sb = new StringBuilder();
        sb.AppendLine("# @generated by ZibStack.NET.TypeGen — do not edit");
        sb.AppendLine($"openapi: {oa.OpenApiVersion}");
        sb.AppendLine("info:");
        sb.AppendLine($"  title: {YamlString(oa.Title)}");
        sb.AppendLine($"  version: {YamlString(oa.Version)}");
        if (!string.IsNullOrEmpty(oa.Description))
            sb.AppendLine($"  description: {YamlString(oa.Description!)}");
        sb.AppendLine("paths: {}");
        sb.AppendLine("components:");
        sb.AppendLine("  schemas:");

        foreach (var cls in model.Classes)
        {
            if (cls.OpenApiIgnore || (cls.Targets & TypeTarget.OpenApi) == 0) continue;
            EmitClassYaml(sb, cls, nameByCSharp, indent: "    ");
        }
        foreach (var en in model.Enums)
        {
            if (en.OpenApiIgnore || (en.Targets & TypeTarget.OpenApi) == 0) continue;
            EmitEnumYaml(sb, en, indent: "    ");
        }

        return sb.ToString();
    }

    private static void EmitClassYaml(StringBuilder sb, SchemaClass cls, IReadOnlyDictionary<string, string> nameByCSharp, string indent)
    {
        sb.AppendLine($"{indent}{cls.EmittedName}:");
        sb.AppendLine($"{indent}  type: object");

        var emittedProps = cls.Properties.Where(p => !p.OpenApiIgnore).ToList();
        var required = emittedProps.Where(p => !IsEffectivelyNullable(p)).ToList();
        if (required.Count > 0)
        {
            sb.AppendLine($"{indent}  required:");
            foreach (var p in required)
                sb.AppendLine($"{indent}    - {p.OpenApiNameOverride ?? p.SourceName}");
        }

        if (emittedProps.Count > 0)
        {
            sb.AppendLine($"{indent}  properties:");
            foreach (var p in emittedProps)
                EmitPropertyYaml(sb, p, nameByCSharp, indent + "    ");
        }
    }

    private static void EmitPropertyYaml(StringBuilder sb, SchemaProperty prop, IReadOnlyDictionary<string, string> nameByCSharp, string indent)
    {
        var name = prop.OpenApiNameOverride ?? prop.SourceName;
        sb.AppendLine($"{indent}{name}:");
        var (typeKey, fmtKey, refTarget, items) = MapCSharpToOpenApi(prop.CSharpTypeFullName, nameByCSharp);
        if (refTarget != null)
        {
            // OpenAPI 3.0: nullable $ref needs allOf wrapping — a bare $ref can't carry siblings.
            if (IsEffectivelyNullable(prop))
            {
                sb.AppendLine($"{indent}  nullable: true");
                sb.AppendLine($"{indent}  allOf:");
                sb.AppendLine($"{indent}    - $ref: '#/components/schemas/{refTarget}'");
            }
            else
                sb.AppendLine($"{indent}  $ref: '#/components/schemas/{refTarget}'");
        }
        else
        {
            if (typeKey == "array")
            {
                sb.AppendLine($"{indent}  type: array");
                if (IsEffectivelyNullable(prop))
                    sb.AppendLine($"{indent}  nullable: true");
                sb.AppendLine($"{indent}  items:");
                if (items?.RefTarget != null)
                    sb.AppendLine($"{indent}    $ref: '#/components/schemas/{items.RefTarget}'");
                else
                {
                    sb.AppendLine($"{indent}    type: {items?.TypeKey ?? "object"}");
                    if (items?.FmtKey != null) sb.AppendLine($"{indent}    format: {items.FmtKey}");
                }
            }
            else
            {
                sb.AppendLine($"{indent}  type: {typeKey}");
                if (fmtKey != null) sb.AppendLine($"{indent}  format: {fmtKey}");
                if (IsEffectivelyNullable(prop))
                    sb.AppendLine($"{indent}  nullable: true");
            }
        }

        // OpenApiProperty extras override / supplement the inferred shape.
        if (prop.OpenApiFormat != null)
            sb.AppendLine($"{indent}  format: {prop.OpenApiFormat}");
        if (prop.OpenApiDescription != null)
            sb.AppendLine($"{indent}  description: {YamlString(prop.OpenApiDescription)}");
        if (prop.OpenApiExample != null)
            sb.AppendLine($"{indent}  example: {YamlScalar(prop.OpenApiExample)}");
    }

    private static void EmitEnumYaml(StringBuilder sb, SchemaEnum en, string indent)
    {
        sb.AppendLine($"{indent}{en.EmittedName}:");
        sb.AppendLine($"{indent}  type: string");
        sb.AppendLine($"{indent}  enum:");
        foreach (var m in en.Members)
            sb.AppendLine($"{indent}    - {m.Name}");
    }

    // ── JSON emitter (when OutputPath ends with .json) ──────────────────────

    private static string EmitJson(SchemaModel model, OpenApiSettings oa, IReadOnlyDictionary<string, string> nameByCSharp)
    {
        // Minimal hand-rolled JSON to avoid a Newtonsoft / System.Text.Json dependency in
        // the analyzer DLL. Generator analyzers run inside the compiler — pulling in big
        // libraries here is risky, so we serialize manually for the small surface area.
        var sb = new StringBuilder();
        sb.AppendLine("{");
        sb.AppendLine($"  \"openapi\": {JsonString(oa.OpenApiVersion)},");
        sb.AppendLine("  \"info\": {");
        sb.AppendLine($"    \"title\": {JsonString(oa.Title)},");
        sb.Append($"    \"version\": {JsonString(oa.Version)}");
        if (!string.IsNullOrEmpty(oa.Description))
        {
            sb.AppendLine(",");
            sb.AppendLine($"    \"description\": {JsonString(oa.Description!)}");
        }
        else sb.AppendLine();
        sb.AppendLine("  },");
        sb.AppendLine("  \"paths\": {},");
        sb.AppendLine("  \"components\": {");
        sb.AppendLine("    \"schemas\": {");

        var first = true;
        foreach (var cls in model.Classes)
        {
            if (cls.OpenApiIgnore || (cls.Targets & TypeTarget.OpenApi) == 0) continue;
            if (!first) sb.AppendLine(",");
            first = false;
            EmitClassJson(sb, cls, nameByCSharp, "      ");
        }
        foreach (var en in model.Enums)
        {
            if (en.OpenApiIgnore || (en.Targets & TypeTarget.OpenApi) == 0) continue;
            if (!first) sb.AppendLine(",");
            first = false;
            EmitEnumJson(sb, en, "      ");
        }
        if (!first) sb.AppendLine();

        sb.AppendLine("    }");
        sb.AppendLine("  }");
        sb.AppendLine("}");
        return sb.ToString();
    }

    private static void EmitClassJson(StringBuilder sb, SchemaClass cls, IReadOnlyDictionary<string, string> nameByCSharp, string indent)
    {
        sb.AppendLine($"{indent}{JsonString(cls.EmittedName)}: {{");
        sb.Append($"{indent}  \"type\": \"object\"");
        var emittedProps = cls.Properties.Where(p => !p.OpenApiIgnore).ToList();
        var required = emittedProps.Where(p => !IsEffectivelyNullable(p)).ToList();
        if (required.Count > 0)
        {
            sb.AppendLine(",");
            sb.Append($"{indent}  \"required\": [");
            sb.Append(string.Join(", ", required.Select(r => JsonString(r.OpenApiNameOverride ?? r.SourceName))));
            sb.Append(']');
        }
        if (emittedProps.Count > 0)
        {
            sb.AppendLine(",");
            sb.AppendLine($"{indent}  \"properties\": {{");
            for (int i = 0; i < emittedProps.Count; i++)
            {
                EmitPropertyJson(sb, emittedProps[i], nameByCSharp, indent + "    ");
                if (i < emittedProps.Count - 1) sb.AppendLine(",");
                else sb.AppendLine();
            }
            sb.AppendLine($"{indent}  }}");
        }
        else sb.AppendLine();
        sb.Append($"{indent}}}");
    }

    private static void EmitPropertyJson(StringBuilder sb, SchemaProperty prop, IReadOnlyDictionary<string, string> nameByCSharp, string indent)
    {
        var name = prop.OpenApiNameOverride ?? prop.SourceName;
        sb.Append($"{indent}{JsonString(name)}: {{ ");
        var (typeKey, fmtKey, refTarget, items) = MapCSharpToOpenApi(prop.CSharpTypeFullName, nameByCSharp);
        var nullable = IsEffectivelyNullable(prop);
        if (refTarget != null)
        {
            if (nullable)
                sb.Append($"\"nullable\": true, \"allOf\": [{{ \"$ref\": \"#/components/schemas/{refTarget}\" }}]");
            else
                sb.Append($"\"$ref\": \"#/components/schemas/{refTarget}\"");
        }
        else if (typeKey == "array")
        {
            sb.Append("\"type\": \"array\"");
            if (nullable) sb.Append(", \"nullable\": true");
            sb.Append(", \"items\": { ");
            if (items?.RefTarget != null)
                sb.Append($"\"$ref\": \"#/components/schemas/{items.RefTarget}\"");
            else
            {
                sb.Append($"\"type\": \"{items?.TypeKey ?? "object"}\"");
                if (items?.FmtKey != null) sb.Append($", \"format\": \"{items.FmtKey}\"");
            }
            sb.Append(" }");
        }
        else
        {
            sb.Append($"\"type\": \"{typeKey}\"");
            if (fmtKey != null) sb.Append($", \"format\": \"{fmtKey}\"");
            if (nullable) sb.Append(", \"nullable\": true");
        }
        if (prop.OpenApiDescription != null)
            sb.Append($", \"description\": {JsonString(prop.OpenApiDescription)}");
        sb.Append(" }");
    }

    private static void EmitEnumJson(StringBuilder sb, SchemaEnum en, string indent)
    {
        sb.AppendLine($"{indent}{JsonString(en.EmittedName)}: {{");
        sb.AppendLine($"{indent}  \"type\": \"string\",");
        sb.Append($"{indent}  \"enum\": [");
        sb.Append(string.Join(", ", en.Members.Select(m => JsonString(m.Name))));
        sb.AppendLine("]");
        sb.Append($"{indent}}}");
    }

    // ── shared mapping ──────────────────────────────────────────────────────

    private sealed record OpenApiTypeMapping(string TypeKey, string? FmtKey, string? RefTarget);

    private static (string TypeKey, string? FmtKey, string? RefTarget, OpenApiTypeMapping? Items) MapCSharpToOpenApi(
        string cSharpType,
        IReadOnlyDictionary<string, string> nameByCSharp)
    {
        var t = cSharpType.TrimEnd('?');

        if (nameByCSharp.TryGetValue(t, out var refName))
            return ("$ref", null, refName, null);

        // Arrays / lists → type: array, items: ...
        if (t.EndsWith("[]"))
        {
            var inner = MapCSharpToOpenApi(t.Substring(0, t.Length - 2), nameByCSharp);
            return ("array", null, null, new OpenApiTypeMapping(inner.TypeKey, inner.FmtKey, inner.RefTarget));
        }
        var listInner = ExtractGeneric(t, "List", "IList", "ICollection", "IEnumerable", "IReadOnlyList", "IReadOnlyCollection");
        if (listInner != null)
        {
            var inner = MapCSharpToOpenApi(listInner, nameByCSharp);
            return ("array", null, null, new OpenApiTypeMapping(inner.TypeKey, inner.FmtKey, inner.RefTarget));
        }

        return t switch
        {
            "string" => ("string", null, null, null),
            "bool" or "System.Boolean" => ("boolean", null, null, null),
            "byte" or "sbyte" => ("integer", "int32", null, null),
            "short" or "ushort" or "int" or "uint" or "System.Int32" => ("integer", "int32", null, null),
            "long" or "ulong" or "System.Int64" => ("integer", "int64", null, null),
            "float" or "System.Single" => ("number", "float", null, null),
            "double" or "System.Double" => ("number", "double", null, null),
            "decimal" or "System.Decimal" => ("number", "double", null, null),
            "System.Guid" or "Guid" => ("string", "uuid", null, null),
            "System.DateTime" or "DateTime" or "System.DateTimeOffset" or "DateTimeOffset" => ("string", "date-time", null, null),
            "System.DateOnly" or "DateOnly" => ("string", "date", null, null),
            "System.TimeOnly" or "TimeOnly" or "System.TimeSpan" or "TimeSpan" => ("string", "time", null, null),
            _ => ("object", null, null, null),
        };
    }

    private static string? ExtractGeneric(string typeName, params string[] names)
    {
        foreach (var n in names)
        {
            var idx = typeName.IndexOf(n + "<", System.StringComparison.Ordinal);
            if (idx < 0) continue;
            if (idx > 0 && char.IsLetterOrDigit(typeName[idx - 1])) continue;
            int depth = 1;
            for (int i = idx + n.Length + 1; i < typeName.Length; i++)
            {
                if (typeName[i] == '<') depth++;
                else if (typeName[i] == '>') { depth--; if (depth == 0) return typeName.Substring(idx + n.Length + 1, i - idx - n.Length - 1); }
            }
        }
        return null;
    }

    private static bool IsEffectivelyNullable(SchemaProperty p) =>
        p.OpenApiNullableOverride ?? p.IsNullable;

    // ── YAML/JSON scalar formatters ──

    private static string YamlString(string s) =>
        s.Contains(':') || s.Contains('#') || s.Contains('\n') || s.StartsWith("'") || s.StartsWith("\"")
            ? $"\"{s.Replace("\"", "\\\"")}\""
            : s;

    private static string YamlScalar(object value) => value switch
    {
        string s => YamlString(s),
        bool b => b ? "true" : "false",
        null => "null",
        _ => System.Convert.ToString(value, CultureInfo.InvariantCulture) ?? "",
    };

    private static string JsonString(string s)
    {
        var sb = new StringBuilder("\"");
        foreach (var c in s)
        {
            switch (c)
            {
                case '"': sb.Append("\\\""); break;
                case '\\': sb.Append("\\\\"); break;
                case '\n': sb.Append("\\n"); break;
                case '\r': sb.Append("\\r"); break;
                case '\t': sb.Append("\\t"); break;
                default:
                    if (c < 0x20) sb.Append($"\\u{(int)c:x4}");
                    else sb.Append(c);
                    break;
            }
        }
        sb.Append('"');
        return sb.ToString();
    }
}
