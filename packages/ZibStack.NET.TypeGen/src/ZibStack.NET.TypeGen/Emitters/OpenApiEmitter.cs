using System.Collections.Generic;
using System.Globalization;
using System.Linq;
using System.Text;

namespace ZibStack.NET.TypeGen.Generator;

/// <summary>
/// Produces an OpenAPI 3.1 document from a <see cref="SchemaModel"/>. Emits
/// <c>info</c>, <c>paths</c>, and <c>components/schemas</c>. Paths come from the
/// unified <see cref="SchemaModel.Endpoints"/> list — populated by
/// <see cref="EndpointDiscovery"/> which scans hand-written
/// <c>[ApiController]</c> classes natively AND synthesizes entries from
/// <c>[CrudApi]</c>-annotated classes (since Roslyn doesn't let us see Dto's
/// generated endpoint source in the same compilation pass).
///
/// <para>
/// Format follows the file extension on <see cref="OpenApiSettings.OutputPath"/>:
/// <c>.json</c> emits JSON, anything else emits YAML.
/// </para>
/// </summary>
internal static class OpenApiEmitter
{
    public static IReadOnlyList<EmittedFile> Emit(SchemaModel model, GlobalSettings settings)
    {
        var oa = settings.OpenApi;
        var emitJson = oa.OutputPath.EndsWith(".json", System.StringComparison.OrdinalIgnoreCase);

        // Stamp global flags onto classes so emitter helpers can read them without
        // threading settings through every call.
        foreach (var cls in model.Classes) cls.QueryDsl = settings.HasQueryDsl;

        // Resolve emitted schema names — ['Order' if no override, otherwise OpenApiSchemaName].
        // Stored back so $ref resolution works across classes referencing each other.
        foreach (var cls in model.Classes)
        {
            if (cls.OpenApiIgnore || (cls.Targets & TypeTarget.OpenApi) == 0) continue;
            cls.EmittedName = cls.OpenApiNameOverride ?? cls.SourceName;
        }
        foreach (var en in model.Enums)
        {
            if (en.OpenApiIgnore || (en.Targets & TypeTarget.OpenApi) == 0) continue;
            en.EmittedName = en.OpenApiNameOverride ?? en.SourceName;
        }

        var nameByCSharp = new Dictionary<string, string>();
        foreach (var c in model.Classes)
            if (!c.OpenApiIgnore && (c.Targets & TypeTarget.OpenApi) != 0)
                nameByCSharp[c.CSharpFullName] = c.EmittedName;
        foreach (var e in model.Enums)
            if (!e.OpenApiIgnore && (e.Targets & TypeTarget.OpenApi) != 0)
                nameByCSharp[e.CSharpFullName] = e.EmittedName;

        // Ensure synthesized endpoints are materialized even when a caller built
        // the model directly (tests / non-generator hosts). Idempotent — already-
        // populated endpoints aren't duplicated.
        EndpointDiscovery.SynthesizeFromCrudApi(model);

        var content = emitJson
            ? EmitJson(model, oa, nameByCSharp, settings.HasQueryDsl)
            : EmitYaml(model, oa, nameByCSharp, settings.HasQueryDsl);

        // Split path into dir + filename. If the user gave only a filename, route it
        // under the first emitting class's OutputDir so `[GenerateTypes(OutputDir="generated")]`
        // keeps the schema next to its TypeScript siblings instead of dumping in the root.
        var path = oa.OutputPath.Replace('\\', '/');
        var lastSlash = path.LastIndexOf('/');
        string dir;
        string file;
        if (lastSlash >= 0)
        {
            dir = path.Substring(0, lastSlash);
            file = path.Substring(lastSlash + 1);
        }
        else
        {
            var firstCls = model.Classes.FirstOrDefault(c => !c.OpenApiIgnore && (c.Targets & TypeTarget.OpenApi) != 0);
            var firstEn = model.Enums.FirstOrDefault(e => !e.OpenApiIgnore && (e.Targets & TypeTarget.OpenApi) != 0);
            dir = firstCls?.OutputDir ?? firstEn?.OutputDir ?? ".";
            file = path;
        }

        return new[] { new EmittedFile(TypeTarget.OpenApi, dir, file, content) };
    }

    // ── YAML emitter (default) ──────────────────────────────────────────────

    private static string EmitYaml(SchemaModel model, OpenApiSettings oa, IReadOnlyDictionary<string, string> nameByCSharp, bool hasQueryDsl)
    {
        var sb = new StringBuilder();
        sb.AppendLine("# @generated by ZibStack.NET.TypeGen — do not edit");
        sb.AppendLine($"openapi: {oa.OpenApiVersion}");
        sb.AppendLine("info:");
        sb.AppendLine($"  title: {YamlString(oa.Title)}");
        sb.AppendLine($"  version: {YamlString(oa.Version)}");
        if (!string.IsNullOrEmpty(oa.Description))
            sb.AppendLine($"  description: {YamlString(oa.Description!)}");
        EmitPathsYaml(sb, model, nameByCSharp, hasQueryDsl, oa.EmitPaths);
        sb.AppendLine("components:");
        sb.AppendLine("  schemas:");

        foreach (var cls in model.Classes)
        {
            if (cls.OpenApiIgnore || (cls.Targets & TypeTarget.OpenApi) == 0) continue;
            EmitClassYaml(sb, cls, nameByCSharp, indent: "    ", model);
        }
        foreach (var en in model.Enums)
        {
            if (en.OpenApiIgnore || (en.Targets & TypeTarget.OpenApi) == 0) continue;
            EmitEnumYaml(sb, en, indent: "    ");
        }

        // PaginatedResponseOf{X} auxiliary schemas for every unique X across all
        // list endpoints returning PaginatedResponse<X>. Deduped across endpoints.
        EmitPaginatedWrappersYaml(sb, model, nameByCSharp, indent: "    ");

        return sb.ToString();
    }

    // ── paths emission (driven by model.Endpoints) ───────────────────────────

    /// <summary>
    /// Emits the full <c>paths:</c> block from <see cref="SchemaModel.Endpoints"/>.
    /// Source-agnostic: every endpoint (native controller or [CrudApi] synth)
    /// goes through the same code path. Groups endpoints by URL pattern so
    /// multiple verbs share one YAML key.
    /// </summary>
    private static void EmitPathsYaml(StringBuilder sb, SchemaModel model, IReadOnlyDictionary<string, string> nameByCSharp, bool hasQueryDsl, bool emitPaths)
    {
        if (!emitPaths || model.Endpoints.Count == 0) { sb.AppendLine("paths: {}"); return; }
        sb.AppendLine("paths:");
        foreach (var group in model.Endpoints.GroupBy(e => e.Pattern))
        {
            sb.AppendLine($"  {group.Key}:");
            foreach (var ep in group)
            {
                sb.AppendLine($"    {ep.Verb}:");
                EmitEndpointBodyYaml(sb, ep, nameByCSharp, hasQueryDsl);
            }
        }
    }

    private static void EmitEndpointBodyYaml(StringBuilder sb, EndpointInfo ep,
        IReadOnlyDictionary<string, string> nameByCSharp, bool hasQueryDsl)
    {
        if (ep.Tag is not null) sb.AppendLine($"      tags: [{ep.Tag}]");
        sb.AppendLine($"      operationId: {ep.OperationId}");

        // Route + query params merged into one `parameters:` block. List endpoints
        // get page/pageSize unconditionally; filter/sort/select/count added when
        // ZibStack.NET.Query is in the compilation (matches runtime binding).
        var allParams = new List<EndpointParameter>(ep.Parameters);
        if (ep.IsListEndpoint)
        {
            allParams.Add(QP("page", "int", "Page number (1-based)."));
            allParams.Add(QP("pageSize", "int", "Items per page."));
            if (hasQueryDsl)
            {
                allParams.Add(QP("filter", "string", "Filter DSL — 'Field>value,Field2=*text'."));
                allParams.Add(QP("sort", "string", "Sort DSL — '-Field,Field2' (prefix '-' for desc)."));
                allParams.Add(QP("select", "string", "Projection — 'Field1,Field2'."));
                allParams.Add(QP("count", "bool", "When true, returns only { count: int }."));
            }
        }
        if (allParams.Count > 0)
        {
            sb.AppendLine("      parameters:");
            foreach (var p in allParams) EmitParameterYaml(sb, p, nameByCSharp);
        }

        // Request body: either a single $ref (RequestBodyCSharpType) or an array
        // of $refs (RequestBodyArrayItemCSharpType — bulk ops shape).
        if (ep.RequestBodyCSharpType is not null)
        {
            sb.AppendLine("      requestBody:");
            sb.AppendLine("        required: true");
            sb.AppendLine("        content:");
            sb.AppendLine("          application/json:");
            sb.Append("            schema: ");
            EmitSchemaInlineYaml(sb, ep.RequestBodyCSharpType, nameByCSharp);
            sb.AppendLine();
        }
        else if (ep.RequestBodyArrayItemCSharpType is not null)
        {
            sb.AppendLine("      requestBody:");
            sb.AppendLine("        required: true");
            sb.AppendLine("        content:");
            sb.AppendLine("          application/json:");
            sb.AppendLine("            schema:");
            sb.AppendLine("              type: array");
            sb.Append("              items: ");
            EmitSchemaInlineYaml(sb, ep.RequestBodyArrayItemCSharpType, nameByCSharp);
            sb.AppendLine();
        }

        sb.AppendLine("      responses:");
        sb.AppendLine($"        '{ep.SuccessStatusCode}':");
        sb.AppendLine($"          description: {HttpStatusDescription(ep.SuccessStatusCode)}");
        if (ep.ResponseCSharpType is not null)
        {
            sb.AppendLine("          content:");
            sb.AppendLine("            application/json:");
            sb.Append("              schema: ");
            EmitSchemaInlineYaml(sb, ep.ResponseCSharpType, nameByCSharp);
            sb.AppendLine();
        }
        else if (ep.ResponseArrayItemCSharpType is not null)
        {
            sb.AppendLine("          content:");
            sb.AppendLine("            application/json:");
            sb.AppendLine("              schema:");
            sb.AppendLine("                type: array");
            sb.Append("                items: ");
            EmitSchemaInlineYaml(sb, ep.ResponseArrayItemCSharpType, nameByCSharp);
            sb.AppendLine();
        }
        if (ep.HasNotFoundResponse)
        {
            sb.AppendLine("        '404':");
            sb.AppendLine("          description: Not Found");
        }
    }

    private static EndpointParameter QP(string name, string cSharpType, string description) =>
        new() { Name = name, Location = ParamLocation.Query, CSharpType = cSharpType, Required = false, Description = description };

    private static void EmitParameterYaml(StringBuilder sb, EndpointParameter p,
        IReadOnlyDictionary<string, string> nameByCSharp)
    {
        sb.AppendLine($"        - name: {p.Name}");
        sb.AppendLine($"          in: {ParamLocationToOpenApi(p.Location)}");
        if (p.Required || p.Location == ParamLocation.Route) sb.AppendLine("          required: true");
        if (p.Description is not null) sb.AppendLine($"          description: {YamlString(p.Description)}");
        sb.AppendLine("          schema:");
        var mapped = MapCSharpToOpenApi(p.CSharpType, nameByCSharp);
        if (mapped.RefTarget is not null)
            sb.AppendLine($"            $ref: '#/components/schemas/{mapped.RefTarget}'");
        else
        {
            sb.AppendLine($"            type: {mapped.TypeKey}");
            if (mapped.FmtKey is not null) sb.AppendLine($"            format: {mapped.FmtKey}");
        }
    }

    private static string ParamLocationToOpenApi(ParamLocation loc) => loc switch
    {
        ParamLocation.Route => "path",
        ParamLocation.Query => "query",
        ParamLocation.Header => "header",
        ParamLocation.Body => "body",  // not valid in OpenAPI 3 — filtered before here
        _ => "query",
    };

    /// <summary>
    /// Emits a <c>{ $ref: '#/components/schemas/X' }</c> or <c>{ type: …, items: … }</c>
    /// inline. <c>cSharpType</c> may be <c>PaginatedResponse&lt;X&gt;</c> — recognised
    /// and expanded to <c>$ref: PaginatedResponseOf{X}</c>. Array types are
    /// emitted as <c>type: array, items: ...</c> inline. Primitives map through
    /// <see cref="MapCSharpToOpenApi"/>. Unknown class-shaped identifiers
    /// (<c>CreateOrderRequest</c>, likely a Dto-generated companion we can't
    /// see in this compilation) fall back to <c>$ref</c> under the assumption
    /// that the named schema materializes via another pass / generator.
    /// </summary>
    private static void EmitSchemaInlineYaml(StringBuilder sb, string cSharpType,
        IReadOnlyDictionary<string, string> nameByCSharp)
    {
        var paged = ExtractGenericArg(cSharpType, "PaginatedResponse");
        if (paged is not null)
        {
            var innerName = nameByCSharp.TryGetValue(paged, out var n) ? n : paged;
            sb.Append($"{{ $ref: '#/components/schemas/PaginatedResponseOf{innerName}' }}");
            return;
        }
        var mapped = MapCSharpToOpenApi(cSharpType, nameByCSharp);
        if (mapped.RefTarget is not null)
            sb.Append($"{{ $ref: '#/components/schemas/{mapped.RefTarget}' }}");
        else if (mapped.Items is not null)
        {
            sb.Append("{ type: array, items: ");
            if (mapped.Items.RefTarget is not null)
                sb.Append($"{{ $ref: '#/components/schemas/{mapped.Items.RefTarget}' }}");
            else
                sb.Append($"{{ type: {mapped.Items.TypeKey}{(mapped.Items.FmtKey is null ? "" : $", format: {mapped.Items.FmtKey}")} }}");
            sb.Append(" }");
        }
        else if (mapped.TypeKey == "object" && LooksLikeSchemaRef(cSharpType, out var shortName))
        {
            // Unknown user-type name — emit $ref assuming the named schema
            // exists elsewhere (Dto-generated companion, hand-authored schema, etc.).
            // Use the short name (strip namespace) since OpenAPI schema keys don't
            // carry dotted namespaces.
            sb.Append($"{{ $ref: '#/components/schemas/{shortName}' }}");
        }
        else
        {
            var fmt = mapped.FmtKey is null ? "" : $", format: {mapped.FmtKey}";
            sb.Append($"{{ type: {mapped.TypeKey}{fmt} }}");
        }
    }

    /// <summary>
    /// Heuristic for "this looks like a user DTO name" — PascalCase identifier,
    /// optionally namespace-qualified (dots allowed). Returns the short name
    /// (last segment, stripped of namespace) when matched — OpenAPI schema keys
    /// don't carry dotted namespaces. Used for the fallback <c>$ref</c> path
    /// when a type isn't in our model (Dto-generated companions, Roslyn cross-
    /// generator wall cases).
    /// </summary>
    private static bool LooksLikeSchemaRef(string name, out string shortName)
    {
        shortName = name;
        if (string.IsNullOrEmpty(name)) return false;
        // No angle brackets (generics) — those should have been unwrapped earlier;
        // a remaining `<` signals a shape we don't recognise.
        if (name.IndexOf('<') >= 0) return false;
        var lastDot = name.LastIndexOf('.');
        if (lastDot >= 0) shortName = name.Substring(lastDot + 1);
        if (shortName.Length == 0 || !char.IsUpper(shortName[0])) return false;
        foreach (var c in shortName)
            if (!char.IsLetterOrDigit(c) && c != '_') return false;
        return true;
    }

    /// <summary>
    /// Extract <c>T</c> from <c>Foo&lt;T&gt;</c> (non-nested). Returns <c>null</c>
    /// when the type isn't the expected generic shape. Used to recognise
    /// <c>PaginatedResponse&lt;Order&gt;</c> in return-type inference.
    /// </summary>
    private static string? ExtractGenericArg(string cSharpType, string outerName)
    {
        var t = cSharpType.TrimEnd('?');
        var needle = outerName + "<";
        var idx = t.IndexOf(needle, System.StringComparison.Ordinal);
        if (idx < 0) return null;
        if (idx > 0 && char.IsLetterOrDigit(t[idx - 1])) return null;
        var start = idx + needle.Length;
        int depth = 1;
        for (int i = start; i < t.Length; i++)
        {
            if (t[i] == '<') depth++;
            else if (t[i] == '>')
            {
                depth--;
                if (depth == 0) return t.Substring(start, i - start).Trim();
            }
        }
        return null;
    }

    private static string HttpStatusDescription(int code) => code switch
    {
        200 => "OK",
        201 => "Created",
        202 => "Accepted",
        204 => "No Content",
        _ => "Response",
    };

    /// <summary>
    /// For every list endpoint returning <c>PaginatedResponse&lt;X&gt;</c>, emit
    /// one <c>PaginatedResponseOf{X}</c> schema under <c>components/schemas</c>.
    /// Dedups by X so multiple list endpoints on the same resource don't
    /// double-emit. Shape mirrors the runtime <c>PaginatedResponse&lt;T&gt;</c>
    /// from ZibStack.NET.Dto.
    /// </summary>
    private static void EmitPaginatedWrappersYaml(StringBuilder sb, SchemaModel model,
        IReadOnlyDictionary<string, string> nameByCSharp, string indent)
    {
        var seen = new HashSet<string>(System.StringComparer.Ordinal);
        foreach (var ep in model.Endpoints)
        {
            var responseType = ep.ResponseCSharpType;
            if (responseType is null) continue;
            var inner = ExtractGenericArg(responseType, "PaginatedResponse");
            if (inner is null) continue;
            var innerEmittedName = nameByCSharp.TryGetValue(inner, out var n) ? n : inner;
            if (!seen.Add(innerEmittedName)) continue;
            EmitPaginatedSchemaYaml(sb, innerEmittedName, indent);
        }
    }

    private static void EmitPaginatedSchemaYaml(StringBuilder sb, string itemSchemaName, string indent)
    {
        sb.AppendLine($"{indent}PaginatedResponseOf{itemSchemaName}:");
        sb.AppendLine($"{indent}  type: object");
        sb.AppendLine($"{indent}  required: [items, totalCount, page, pageSize, totalPages, hasNextPage, hasPreviousPage]");
        sb.AppendLine($"{indent}  properties:");
        sb.AppendLine($"{indent}    items:");
        sb.AppendLine($"{indent}      type: array");
        sb.AppendLine($"{indent}      items: {{ $ref: '#/components/schemas/{itemSchemaName}' }}");
        foreach (var (n, t, f) in new[] {
            ("totalCount", "integer", "int32"),
            ("page", "integer", "int32"),
            ("pageSize", "integer", "int32"),
            ("totalPages", "integer", "int32"),
        })
        {
            sb.AppendLine($"{indent}    {n}: {{ type: {t}, format: {f} }}");
        }
        sb.AppendLine($"{indent}    hasNextPage: {{ type: boolean }}");
        sb.AppendLine($"{indent}    hasPreviousPage: {{ type: boolean }}");
    }

    private static void EmitClassYaml(StringBuilder sb, SchemaClass cls, IReadOnlyDictionary<string, string> nameByCSharp, string indent, SchemaModel model)
    {
        // Polymorphic base → `oneOf` with `discriminator.mapping`. Variants emit
        // as their own schemas (via ordinary EmitClassYaml on the variant); this
        // base schema just points at them.
        if (cls.PolymorphicVariants.Count > 0 && cls.PolymorphicDiscriminator is not null)
        {
            var variantRefs = cls.PolymorphicVariants
                .Select(v => (v, name: nameByCSharp.TryGetValue(v.CSharpFullName, out var n) ? n : null))
                .Where(x => x.name is not null)
                .ToList();
            if (variantRefs.Count > 0)
            {
                sb.AppendLine($"{indent}{cls.EmittedName}:");
                sb.AppendLine($"{indent}  oneOf:");
                foreach (var (_, name) in variantRefs)
                    sb.AppendLine($"{indent}    - $ref: '#/components/schemas/{name}'");
                sb.AppendLine($"{indent}  discriminator:");
                sb.AppendLine($"{indent}    propertyName: {cls.PolymorphicDiscriminator}");
                sb.AppendLine($"{indent}    mapping:");
                foreach (var (v, name) in variantRefs)
                    sb.AppendLine($"{indent}      {v.DiscriminatorValue}: '#/components/schemas/{name}'");
                return;
            }
        }

        // Base in the emit set → allOf [{ $ref: base }, { type: object, properties: new-only }].
        // BUT: if our base is a polymorphic union, DON'T allOf into it — the base
        // is an abstract oneOf, not a concrete object. We still stamp the
        // discriminator literal via the normal props path (see EmitPropertyYaml).
        var baseRef = cls.BaseClassFullName is { } bfn
            && nameByCSharp.TryGetValue(bfn, out var bn)
            && cls.PolymorphicDiscriminatorValue is null
            ? bn : null;

        // Each implemented interface that's visible for OpenAPI (in the model,
        // not OpenApiIgnored, targeted for OpenApi) contributes a $ref to the
        // allOf chain — same mechanism as base-class inheritance, just a list.
        // Also tracks the property names those interfaces cover, so the class
        // body skips redeclaring them in the inline `type: object` block.
        var ifaceRefs = new List<string>();
        var coveredByIfaces = new HashSet<string>(System.StringComparer.Ordinal);
        foreach (var ifaceFqn in cls.ImplementedInterfaces)
        {
            var ifaceCls = model.Classes.FirstOrDefault(c => c.CSharpFullName == ifaceFqn);
            if (ifaceCls is null) continue;
            if (ifaceCls.OpenApiIgnore || (ifaceCls.Targets & TypeTarget.OpenApi) == 0) continue;
            if (!nameByCSharp.TryGetValue(ifaceFqn, out var ifaceName)) continue;
            ifaceRefs.Add(ifaceName);
            foreach (var p in ifaceCls.Properties) coveredByIfaces.Add(p.SourceName);
        }

        if (baseRef is not null || ifaceRefs.Count > 0)
        {
            sb.AppendLine($"{indent}{cls.EmittedName}:");
            sb.AppendLine($"{indent}  allOf:");
            if (baseRef is not null)
                sb.AppendLine($"{indent}    - $ref: '#/components/schemas/{baseRef}'");
            foreach (var r in ifaceRefs)
                sb.AppendLine($"{indent}    - $ref: '#/components/schemas/{r}'");
            sb.AppendLine($"{indent}    - type: object");
            EmitClassBodyYaml(sb, cls, nameByCSharp, indent + "      ", coveredByIfaces);
            return;
        }
        sb.AppendLine($"{indent}{cls.EmittedName}:");
        sb.AppendLine($"{indent}  type: object");
        EmitClassBodyYaml(sb, cls, nameByCSharp, indent + "  ", coveredByIfaces);
    }

    private static void EmitClassBodyYaml(StringBuilder sb, SchemaClass cls, IReadOnlyDictionary<string, string> nameByCSharp, string indent, HashSet<string>? coveredByIfaces = null)
    {
        var ifaceCovered = coveredByIfaces ?? new HashSet<string>(System.StringComparer.Ordinal);
        var emittedProps = cls.Properties.Where(p => !p.OpenApiIgnore && !ifaceCovered.Contains(p.SourceName)).ToList();
        // Drop read-only properties from the `required` list: conventional OpenAPI
        // codegen treats `readOnly: true + required` as "present in response,
        // omitted from request". Since TypeGen emits a single schema per type,
        // leaving them required would force clients to include server-computed
        // fields when constructing request payloads — the exact footgun the
        // readOnly flag exists to prevent. Still required in spirit on the
        // response side; any codegen that generates split response/request types
        // will carry them over to the response schema.
        var required = emittedProps.Where(p => !IsEffectivelyNullable(p) && !p.IsReadOnly).ToList();
        // Polymorphic variant: discriminator is always present + required.
        var hasDiscProp = cls.PolymorphicDiscriminatorValue is not null
            && cls.PolymorphicDiscriminatorPropertyOnVariant is not null;

        if (required.Count > 0 || hasDiscProp)
        {
            sb.AppendLine($"{indent}required:");
            if (hasDiscProp) sb.AppendLine($"{indent}  - {cls.PolymorphicDiscriminatorPropertyOnVariant}");
            foreach (var p in required)
                sb.AppendLine($"{indent}  - {p.OpenApiNameOverride ?? p.SourceName}");
        }

        if (emittedProps.Count > 0 || hasDiscProp)
        {
            sb.AppendLine($"{indent}properties:");
            if (hasDiscProp)
            {
                // Discriminator as a string with single-value enum — OpenAPI 3.0
                // idiom for pinning a literal.
                sb.AppendLine($"{indent}  {cls.PolymorphicDiscriminatorPropertyOnVariant}:");
                sb.AppendLine($"{indent}    type: string");
                sb.AppendLine($"{indent}    enum:");
                sb.AppendLine($"{indent}      - {cls.PolymorphicDiscriminatorValue}");
            }
            foreach (var p in emittedProps)
                EmitPropertyYaml(sb, p, nameByCSharp, indent + "  ");
        }

        // [JsonExtensionData] property → additionalProperties on the parent schema
        // itself. Permissive (`true`) when the dictionary value is object / JsonElement,
        // typed schema otherwise.
        if (cls.AllowsAdditionalProperties)
        {
            if (cls.AdditionalPropertiesValueCSharpType is null)
            {
                sb.AppendLine($"{indent}additionalProperties: true");
            }
            else
            {
                var inner = MapCSharpToOpenApi(cls.AdditionalPropertiesValueCSharpType, nameByCSharp);
                sb.AppendLine($"{indent}additionalProperties:");
                if (inner.RefTarget is not null)
                    sb.AppendLine($"{indent}  $ref: '#/components/schemas/{inner.RefTarget}'");
                else
                {
                    sb.AppendLine($"{indent}  type: {inner.TypeKey}");
                    if (inner.FmtKey is not null) sb.AppendLine($"{indent}  format: {inner.FmtKey}");
                }
            }
        }
    }

    private static void EmitPropertyYaml(StringBuilder sb, SchemaProperty prop, IReadOnlyDictionary<string, string> nameByCSharp, string indent)
    {
        var name = prop.OpenApiNameOverride ?? prop.SourceName;
        sb.AppendLine($"{indent}{name}:");
        var (typeKey, fmtKey, refTarget, items) = ResolvePropertyShape(prop, nameByCSharp);
        if (refTarget != null)
        {
            // OpenAPI 3.0: nullable $ref needs allOf wrapping — a bare $ref can't carry siblings.
            if (IsEffectivelyNullable(prop))
            {
                sb.AppendLine($"{indent}  nullable: true");
                sb.AppendLine($"{indent}  allOf:");
                sb.AppendLine($"{indent}    - $ref: '#/components/schemas/{refTarget}'");
            }
            else
                sb.AppendLine($"{indent}  $ref: '#/components/schemas/{refTarget}'");
        }
        else
        {
            if (typeKey == "array")
            {
                sb.AppendLine($"{indent}  type: array");
                if (IsEffectivelyNullable(prop))
                    sb.AppendLine($"{indent}  nullable: true");
                sb.AppendLine($"{indent}  items:");
                if (items?.RefTarget != null)
                    sb.AppendLine($"{indent}    $ref: '#/components/schemas/{items.RefTarget}'");
                else
                {
                    sb.AppendLine($"{indent}    type: {items?.TypeKey ?? "object"}");
                    if (items?.FmtKey != null) sb.AppendLine($"{indent}    format: {items.FmtKey}");
                }
            }
            else if (typeKey == "dict")
            {
                sb.AppendLine($"{indent}  type: object");
                if (IsEffectivelyNullable(prop))
                    sb.AppendLine($"{indent}  nullable: true");
                sb.AppendLine($"{indent}  additionalProperties:");
                if (items?.RefTarget != null)
                    sb.AppendLine($"{indent}    $ref: '#/components/schemas/{items.RefTarget}'");
                else
                {
                    sb.AppendLine($"{indent}    type: {items?.TypeKey ?? "object"}");
                    if (items?.FmtKey != null) sb.AppendLine($"{indent}    format: {items.FmtKey}");
                }
            }
            else
            {
                sb.AppendLine($"{indent}  type: {typeKey}");
                if (fmtKey != null) sb.AppendLine($"{indent}  format: {fmtKey}");
                if (IsEffectivelyNullable(prop))
                    sb.AppendLine($"{indent}  nullable: true");
            }
        }

        // OpenApiProperty extras override / supplement the inferred shape.
        if (prop.OpenApiFormat != null)
            sb.AppendLine($"{indent}  format: {prop.OpenApiFormat}");
        if (prop.OpenApiDescription != null)
            sb.AppendLine($"{indent}  description: {YamlString(prop.OpenApiDescription)}");
        if (prop.OpenApiExample != null)
            sb.AppendLine($"{indent}  example: {YamlScalar(prop.OpenApiExample)}");
        // Server-computed fields: clients must not send them in requests. OpenAPI's
        // `readOnly: true` is understood by every mainstream codegen (orval,
        // openapi-typescript-codegen, openapi-generator) — they strip the field
        // from request bodies and keep it on response models.
        if (prop.IsReadOnly)
            sb.AppendLine($"{indent}  readOnly: true");

        // Validation-derived constraints — safe to emit even when not applicable
        // to the type (OpenAPI readers ignore minLength on integers, etc.).
        if (prop.MinLength is int minLen) sb.AppendLine($"{indent}  minLength: {minLen}");
        if (prop.MaxLength is int maxLen) sb.AppendLine($"{indent}  maxLength: {maxLen}");
        if (prop.Minimum is double minVal) sb.AppendLine($"{indent}  minimum: {FormatNumber(minVal)}");
        if (prop.Maximum is double maxVal) sb.AppendLine($"{indent}  maximum: {FormatNumber(maxVal)}");
        // Regex patterns commonly contain YAML-significant chars (|, *, ?, [, ~, &).
        // Always double-quote them — safer than relying on YamlString's heuristic.
        if (prop.Pattern is string pat)
            sb.AppendLine($"{indent}  pattern: \"{pat.Replace("\\", "\\\\").Replace("\"", "\\\"")}\"");
    }

    /// <summary>Trim ".0" from whole-double values to keep YAML tidy (e.g. 100, not 100.0).</summary>
    private static string FormatNumber(double v) =>
        v == System.Math.Floor(v) && !double.IsInfinity(v)
            ? ((long)v).ToString(CultureInfo.InvariantCulture)
            : v.ToString(CultureInfo.InvariantCulture);

    private static void EmitEnumYaml(StringBuilder sb, SchemaEnum en, string indent)
    {
        sb.AppendLine($"{indent}{en.EmittedName}:");
        sb.AppendLine($"{indent}  type: string");
        sb.AppendLine($"{indent}  enum:");
        foreach (var m in en.Members)
            sb.AppendLine($"{indent}    - {m.Name}");
    }

    // ── JSON emitter (when OutputPath ends with .json) ──────────────────────

    private static string EmitJson(SchemaModel model, OpenApiSettings oa, IReadOnlyDictionary<string, string> nameByCSharp, bool hasQueryDsl)
    {
        // Minimal hand-rolled JSON to avoid a Newtonsoft / System.Text.Json dependency in
        // the analyzer DLL. Generator analyzers run inside the compiler — pulling in big
        // libraries here is risky, so we serialize manually for the small surface area.
        var sb = new StringBuilder();
        sb.AppendLine("{");
        sb.AppendLine($"  \"openapi\": {JsonString(oa.OpenApiVersion)},");
        sb.AppendLine("  \"info\": {");
        sb.AppendLine($"    \"title\": {JsonString(oa.Title)},");
        sb.Append($"    \"version\": {JsonString(oa.Version)}");
        if (!string.IsNullOrEmpty(oa.Description))
        {
            sb.AppendLine(",");
            sb.AppendLine($"    \"description\": {JsonString(oa.Description!)}");
        }
        else sb.AppendLine();
        sb.AppendLine("  },");
        EmitPathsJson(sb, model, nameByCSharp, hasQueryDsl, oa.EmitPaths);
        sb.AppendLine("  \"components\": {");
        sb.AppendLine("    \"schemas\": {");

        var first = true;
        foreach (var cls in model.Classes)
        {
            if (cls.OpenApiIgnore || (cls.Targets & TypeTarget.OpenApi) == 0) continue;
            if (!first) sb.AppendLine(",");
            first = false;
            EmitClassJson(sb, cls, nameByCSharp, "      ", model);
        }
        foreach (var en in model.Enums)
        {
            if (en.OpenApiIgnore || (en.Targets & TypeTarget.OpenApi) == 0) continue;
            if (!first) sb.AppendLine(",");
            first = false;
            EmitEnumJson(sb, en, "      ");
        }
        // PaginatedResponseOf{X} auxiliary schemas — one per unique X across all
        // list endpoints returning PaginatedResponse<X>. Deduped across sources.
        var seenPaginated = new HashSet<string>(System.StringComparer.Ordinal);
        foreach (var ep in model.Endpoints)
        {
            if (ep.ResponseCSharpType is null) continue;
            var inner = ExtractGenericArg(ep.ResponseCSharpType, "PaginatedResponse");
            if (inner is null) continue;
            var innerName = nameByCSharp.TryGetValue(inner, out var n) ? n : inner;
            if (!seenPaginated.Add(innerName)) continue;
            if (!first) sb.AppendLine(",");
            first = false;
            EmitPaginatedSchemaJson(sb, innerName, "      ");
        }
        if (!first) sb.AppendLine();

        sb.AppendLine("    }");
        sb.AppendLine("  }");
        sb.AppendLine("}");
        return sb.ToString();
    }

    private static void EmitClassJson(StringBuilder sb, SchemaClass cls, IReadOnlyDictionary<string, string> nameByCSharp, string indent, SchemaModel model)
    {
        // Inheritance: emit allOf[{$ref: base}, {$ref: iface…}, {type: object, ...}]
        // when base or interfaces are in the model. Otherwise fall back to a
        // single {type: object, ...} block.
        var baseRef = cls.BaseClassFullName is { } bfn && nameByCSharp.TryGetValue(bfn, out var bn) ? bn : null;
        var ifaceRefs = new List<string>();
        var coveredByIfaces = new HashSet<string>(System.StringComparer.Ordinal);
        foreach (var ifaceFqn in cls.ImplementedInterfaces)
        {
            var ifaceCls = model.Classes.FirstOrDefault(c => c.CSharpFullName == ifaceFqn);
            if (ifaceCls is null) continue;
            if (ifaceCls.OpenApiIgnore || (ifaceCls.Targets & TypeTarget.OpenApi) == 0) continue;
            if (!nameByCSharp.TryGetValue(ifaceFqn, out var ifaceName)) continue;
            ifaceRefs.Add(ifaceName);
            foreach (var p in ifaceCls.Properties) coveredByIfaces.Add(p.SourceName);
        }

        var emittedProps = cls.Properties.Where(p => !p.OpenApiIgnore && !coveredByIfaces.Contains(p.SourceName)).ToList();
        // See EmitClassBodyYaml for why IsReadOnly is excluded from required —
        // single-schema emission + readOnly convention means forcing them would
        // break client-side payload construction.
        var required = emittedProps.Where(p => !IsEffectivelyNullable(p) && !p.IsReadOnly).ToList();

        sb.AppendLine($"{indent}{JsonString(cls.EmittedName)}: {{");
        if (baseRef is not null || ifaceRefs.Count > 0)
        {
            sb.AppendLine($"{indent}  \"allOf\": [");
            if (baseRef is not null)
                sb.AppendLine($"{indent}    {{ \"$ref\": \"#/components/schemas/{baseRef}\" }},");
            foreach (var r in ifaceRefs)
                sb.AppendLine($"{indent}    {{ \"$ref\": \"#/components/schemas/{r}\" }},");
            sb.Append($"{indent}    {{ \"type\": \"object\"");
            WriteRequiredAndPropsJson(sb, emittedProps, required, nameByCSharp, indent + "    ");
            sb.AppendLine();
            sb.AppendLine($"{indent}    }}");
            sb.AppendLine($"{indent}  ]");
        }
        else
        {
            sb.Append($"{indent}  \"type\": \"object\"");
            WriteRequiredAndPropsJson(sb, emittedProps, required, nameByCSharp, indent + "  ");
            sb.AppendLine();
        }
        sb.Append($"{indent}}}");
    }

    private static void WriteRequiredAndPropsJson(StringBuilder sb, List<SchemaProperty> emittedProps, List<SchemaProperty> required, IReadOnlyDictionary<string, string> nameByCSharp, string indent)
    {
        if (required.Count > 0)
        {
            sb.AppendLine(",");
            sb.Append($"{indent}\"required\": [");
            sb.Append(string.Join(", ", required.Select(r => JsonString(r.OpenApiNameOverride ?? r.SourceName))));
            sb.Append(']');
        }
        if (emittedProps.Count > 0)
        {
            sb.AppendLine(",");
            sb.AppendLine($"{indent}\"properties\": {{");
            for (int i = 0; i < emittedProps.Count; i++)
            {
                EmitPropertyJson(sb, emittedProps[i], nameByCSharp, indent + "  ");
                if (i < emittedProps.Count - 1) sb.AppendLine(",");
                else sb.AppendLine();
            }
            sb.Append($"{indent}}}");
        }
    }

    private static void EmitPropertyJson(StringBuilder sb, SchemaProperty prop, IReadOnlyDictionary<string, string> nameByCSharp, string indent)
    {
        var name = prop.OpenApiNameOverride ?? prop.SourceName;
        sb.Append($"{indent}{JsonString(name)}: {{ ");
        var (typeKey, fmtKey, refTarget, items) = ResolvePropertyShape(prop, nameByCSharp);
        var nullable = IsEffectivelyNullable(prop);
        if (refTarget != null)
        {
            if (nullable)
                sb.Append($"\"nullable\": true, \"allOf\": [{{ \"$ref\": \"#/components/schemas/{refTarget}\" }}]");
            else
                sb.Append($"\"$ref\": \"#/components/schemas/{refTarget}\"");
        }
        else if (typeKey == "array")
        {
            sb.Append("\"type\": \"array\"");
            if (nullable) sb.Append(", \"nullable\": true");
            sb.Append(", \"items\": { ");
            if (items?.RefTarget != null)
                sb.Append($"\"$ref\": \"#/components/schemas/{items.RefTarget}\"");
            else
            {
                sb.Append($"\"type\": \"{items?.TypeKey ?? "object"}\"");
                if (items?.FmtKey != null) sb.Append($", \"format\": \"{items.FmtKey}\"");
            }
            sb.Append(" }");
        }
        else if (typeKey == "dict")
        {
            sb.Append("\"type\": \"object\"");
            if (nullable) sb.Append(", \"nullable\": true");
            sb.Append(", \"additionalProperties\": { ");
            if (items?.RefTarget != null)
                sb.Append($"\"$ref\": \"#/components/schemas/{items.RefTarget}\"");
            else
            {
                sb.Append($"\"type\": \"{items?.TypeKey ?? "object"}\"");
                if (items?.FmtKey != null) sb.Append($", \"format\": \"{items.FmtKey}\"");
            }
            sb.Append(" }");
        }
        else
        {
            sb.Append($"\"type\": \"{typeKey}\"");
            if (fmtKey != null) sb.Append($", \"format\": \"{fmtKey}\"");
            if (nullable) sb.Append(", \"nullable\": true");
        }
        if (prop.OpenApiDescription != null)
            sb.Append($", \"description\": {JsonString(prop.OpenApiDescription)}");
        if (prop.IsReadOnly) sb.Append(", \"readOnly\": true");
        if (prop.MinLength is int minLen) sb.Append($", \"minLength\": {minLen}");
        if (prop.MaxLength is int maxLen) sb.Append($", \"maxLength\": {maxLen}");
        if (prop.Minimum is double minVal) sb.Append($", \"minimum\": {FormatNumber(minVal)}");
        if (prop.Maximum is double maxVal) sb.Append($", \"maximum\": {FormatNumber(maxVal)}");
        if (prop.Pattern is string pat) sb.Append($", \"pattern\": {JsonString(pat)}");
        sb.Append(" }");
    }

