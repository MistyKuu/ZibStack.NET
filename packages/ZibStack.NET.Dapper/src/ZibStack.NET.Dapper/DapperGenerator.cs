using Microsoft.CodeAnalysis;

namespace ZibStack.NET.Dapper;

[Generator]
public class DapperGenerator : IIncrementalGenerator
{
    private const string DapperCrudStoreSource = @"// <auto-generated />
#nullable enable

namespace ZibStack.NET.Dapper
{
    /// <summary>
    /// Base Dapper implementation of <see cref=""ZibStack.NET.Dto.ICrudStore{TEntity,TKey}""/>.
    /// Override <see cref=""TableName""/> and <see cref=""KeyColumn""/> to customise mapping.
    /// </summary>
    public abstract class DapperCrudStore<TEntity, TKey> : ZibStack.NET.Dto.ICrudStore<TEntity, TKey>
        where TEntity : class
    {
        protected System.Data.IDbConnection Db { get; }

        /// <summary>Table name used in SQL queries. Defaults to the entity type name + ""s"".</summary>
        protected virtual string TableName => typeof(TEntity).Name + ""s"";

        /// <summary>Primary key column name. Defaults to ""Id"".</summary>
        protected virtual string KeyColumn => ""Id"";

        protected DapperCrudStore(System.Data.IDbConnection db) => Db = db;

        public virtual async System.Threading.Tasks.ValueTask<TEntity?> GetByIdAsync(TKey id, System.Threading.CancellationToken ct = default)
        {
            var sql = $""SELECT * FROM {TableName} WHERE {KeyColumn} = @Id"";
            var result = await Dapper.SqlMapper.QueryFirstOrDefaultAsync<TEntity>(Db, new Dapper.CommandDefinition(sql, new { Id = id }, cancellationToken: ct));
            return result;
        }

        public virtual System.Linq.IQueryable<TEntity> Query()
        {
            var sql = $""SELECT * FROM {TableName}"";
            var results = Dapper.SqlMapper.Query<TEntity>(Db, sql);
            return results.AsQueryable();
        }

        public virtual async System.Threading.Tasks.ValueTask CreateAsync(TEntity entity, System.Threading.CancellationToken ct = default)
        {
            var props = typeof(TEntity).GetProperties(System.Reflection.BindingFlags.Public | System.Reflection.BindingFlags.Instance);
            var columns = new System.Collections.Generic.List<string>();
            var parameters = new System.Collections.Generic.List<string>();
            foreach (var p in props)
            {
                if (string.Equals(p.Name, KeyColumn, System.StringComparison.OrdinalIgnoreCase)) continue;
                columns.Add(p.Name);
                parameters.Add(""@"" + p.Name);
            }
            var sql = $""INSERT INTO {TableName} ({string.Join("", "", columns)}) VALUES ({string.Join("", "", parameters)})"";
            await Dapper.SqlMapper.ExecuteAsync(Db, new Dapper.CommandDefinition(sql, entity, cancellationToken: ct));
        }

        public virtual async System.Threading.Tasks.ValueTask UpdateAsync(TEntity entity, System.Threading.CancellationToken ct = default)
        {
            var props = typeof(TEntity).GetProperties(System.Reflection.BindingFlags.Public | System.Reflection.BindingFlags.Instance);
            var setClauses = new System.Collections.Generic.List<string>();
            foreach (var p in props)
            {
                if (string.Equals(p.Name, KeyColumn, System.StringComparison.OrdinalIgnoreCase)) continue;
                setClauses.Add($""{p.Name} = @{p.Name}"");
            }
            var sql = $""UPDATE {TableName} SET {string.Join("", "", setClauses)} WHERE {KeyColumn} = @{KeyColumn}"";
            await Dapper.SqlMapper.ExecuteAsync(Db, new Dapper.CommandDefinition(sql, entity, cancellationToken: ct));
        }

        public virtual async System.Threading.Tasks.ValueTask DeleteAsync(TEntity entity, System.Threading.CancellationToken ct = default)
        {
            var sql = $""DELETE FROM {TableName} WHERE {KeyColumn} = @{KeyColumn}"";
            await Dapper.SqlMapper.ExecuteAsync(Db, new Dapper.CommandDefinition(sql, entity, cancellationToken: ct));
        }
    }
}
";

    public void Initialize(IncrementalGeneratorInitializationContext context)
    {
        var canEmit = context.CompilationProvider.Select(static (compilation, _) =>
        {
            var hasDapper = compilation.GetTypeByMetadataName("Dapper.SqlMapper") is not null;
            var hasCrudStore = compilation.GetTypeByMetadataName("ZibStack.NET.Dto.ICrudStore`2") is not null;
            return hasDapper && hasCrudStore;
        });

        context.RegisterSourceOutput(canEmit, static (spc, can) =>
        {
            if (can)
                spc.AddSource("DapperCrudStore.g.cs", DapperCrudStoreSource);
        });
    }
}
