    /// <summary>
    /// ASP.NET Core Minimal API endpoint filter that auto-validates <see cref=""IValidatable""/> parameters.
    /// <para>Usage:</para>
    /// <code>
    /// app.MapPost(""/api/users"", (CreateUserRequest req) => ...)
    ///    .WithValidation();
    /// </code>
    /// </summary>
    public static class ValidationEndpointFilterExtensions
    {
        /// <summary>
        /// Adds automatic validation to a minimal API endpoint. Any parameter implementing
        /// <see cref=""IValidatable""/> is validated before the handler runs. Returns 400
        /// with structured errors if validation fails.
        /// </summary>
        public static Microsoft.AspNetCore.Builder.RouteHandlerBuilder WithValidation(
            this Microsoft.AspNetCore.Builder.RouteHandlerBuilder builder)
        {
            return builder.AddEndpointFilter(async (context, next) =>
            {
                foreach (var arg in context.Arguments)
                {
                    if (arg is IValidatable validatable)
                    {
                        var result = validatable.Validate();
                        if (!result.IsValid)
                        {
                            return Microsoft.AspNetCore.Http.Results.ValidationProblem(
                                result.ToDictionary()
                                    .ToDictionary(
                                        kv => kv.Key,
                                        kv => kv.Value.ToArray() as string[]));
                        }
                    }
                }
                return await next(context);
            });
        }

