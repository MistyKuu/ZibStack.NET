    // ─── Code generation ───────────────────────────────────────────────

    private static void EmitValidationAttributes(StringBuilder sb, List<string> attrs)
    {
        foreach (var attr in attrs)
            sb.AppendLine($"    {attr}");
    }

    /// <summary>Non-nullable reference types need ! when assigning from PatchField.Value.</summary>
    private static bool NeedsNullForgiving(UtilsPropertyInfo prop)
        => !prop.IsValueType && !prop.IsNullable;

    private static string GeneratePartialFromSource(PartialFromInfo info)
    {
        var sb = new StringBuilder();
        sb.AppendLine("// <auto-generated />");
        sb.AppendLine($"// Generated by ZibStack.NET.Utils from [PartialFrom] on {info.ClassName} (target: {info.TargetTypeName})");
        sb.AppendLine("#nullable enable");
        sb.AppendLine();
        sb.AppendLine("using System;");
        sb.AppendLine("using ZibStack.NET.Utils;");
        sb.AppendLine();

        if (info.Namespace is not null)
        {
            sb.AppendLine($"namespace {info.Namespace};");
            sb.AppendLine();
        }

        sb.AppendLine($"public partial {info.TypeKeyword} {info.ClassName}");
        sb.AppendLine("{");

        foreach (var prop in info.Properties)
        {
            EmitValidationAttributes(sb, prop.ValidationAttributes);
            sb.AppendLine($"    public PatchField<{prop.DisplayTypeName}> {prop.PropertyName} {{ get; init; }}");
        }

        sb.AppendLine();

        sb.AppendLine($"    public void ApplyTo({info.TargetTypeName} target)");
        sb.AppendLine("    {");
        foreach (var prop in info.Properties)
        {
            sb.AppendLine($"        if ({prop.PropertyName}.HasValue) target.{prop.PropertyName} = {prop.PropertyName}.Value{(NeedsNullForgiving(prop) ? "!" : "")};");
        }
        sb.AppendLine("    }");

        sb.AppendLine("}");
        return sb.ToString();
    }

    private static string GenerateIntersectSource(IntersectInfo info)
    {
        var sb = new StringBuilder();
        sb.AppendLine("// <auto-generated />");
        sb.AppendLine($"// Generated by ZibStack.NET.Utils from [IntersectFrom] on {info.ClassName}");
        sb.AppendLine("#nullable enable");
        sb.AppendLine();
        sb.AppendLine("using System;");
        sb.AppendLine("using ZibStack.NET.Utils;");
        sb.AppendLine();

        if (info.Namespace is not null)
        {
            sb.AppendLine($"namespace {info.Namespace};");
            sb.AppendLine();
        }

        sb.AppendLine($"public partial {info.TypeKeyword} {info.ClassName}");
        sb.AppendLine("{");

        // Properties — deduplicate by name (first wins)
        var seen = new HashSet<string>();
        foreach (var target in info.TargetTypes)
        {
            foreach (var prop in target.Properties)
            {
                if (seen.Add(prop.PropertyName))
                {
                    EmitValidationAttributes(sb, prop.ValidationAttributes);
            sb.AppendLine($"    public PatchField<{prop.DisplayTypeName}> {prop.PropertyName} {{ get; init; }}");
                }
            }
        }

        // ApplyTo per target type
        foreach (var target in info.TargetTypes)
        {
            sb.AppendLine();
            sb.AppendLine($"    public void ApplyTo({target.TypeName} target)");
            sb.AppendLine("    {");
            foreach (var prop in target.Properties)
            {
                sb.AppendLine($"        if ({prop.PropertyName}.HasValue) target.{prop.PropertyName} = {prop.PropertyName}.Value{(NeedsNullForgiving(prop) ? "!" : "")};");
            }
            sb.AppendLine("    }");
        }

        sb.AppendLine("}");
        return sb.ToString();
    }

    private static string GeneratePatchFieldSource(bool hasStj, bool hasNewtonsoft)
    {
        var sb = new StringBuilder();
        sb.AppendLine("// <auto-generated />");
        sb.AppendLine("#nullable enable");
        sb.AppendLine();
        sb.AppendLine("using System;");
        sb.AppendLine();
        sb.AppendLine("namespace ZibStack.NET.Utils");
        sb.AppendLine("{");

        sb.AppendLine(@"    /// <summary>
    /// Wraps a value that may or may not have been provided in a request.
    /// HasValue is true when the property was present in the JSON body.
    /// Register the appropriate converter in your serializer options.
    /// </summary>
    public readonly struct PatchField<T>
    {
        private readonly T _value;
        private readonly bool _hasValue;

        public bool HasValue => _hasValue;
        public T Value => _value;

        public PatchField(T value)
        {
            _value = value;
            _hasValue = true;
        }

        public static implicit operator PatchField<T>(T value) => new(value);
        public static implicit operator T(PatchField<T> field) => field.Value;

        public override string ToString()
            => _hasValue ? $""Value({_value})"" : ""Undefined"";
    }");

        if (hasStj)
        {
            sb.AppendLine();
            sb.AppendLine(@"    /// <summary>
    /// System.Text.Json converter for PatchField&lt;T&gt;.
    /// Register via: options.Converters.Add(new PatchFieldJsonConverterFactory());
    /// </summary>
    public sealed class PatchFieldJsonConverterFactory : System.Text.Json.Serialization.JsonConverterFactory
    {
        public override bool CanConvert(Type typeToConvert)
            => typeToConvert.IsGenericType
               && typeToConvert.GetGenericTypeDefinition() == typeof(PatchField<>);

        public override System.Text.Json.Serialization.JsonConverter CreateConverter(Type typeToConvert, System.Text.Json.JsonSerializerOptions options)
        {
            var innerType = typeToConvert.GetGenericArguments()[0];
            var converterType = typeof(PatchFieldJsonConverter<>).MakeGenericType(innerType);
            return (System.Text.Json.Serialization.JsonConverter)Activator.CreateInstance(converterType)!;
        }
    }

    public sealed class PatchFieldJsonConverter<T> : System.Text.Json.Serialization.JsonConverter<PatchField<T>>
    {
        public override PatchField<T> Read(ref System.Text.Json.Utf8JsonReader reader, Type typeToConvert, System.Text.Json.JsonSerializerOptions options)
        {
            var value = System.Text.Json.JsonSerializer.Deserialize<T>(ref reader, options)!;
            return new PatchField<T>(value);
        }

        public override void Write(System.Text.Json.Utf8JsonWriter writer, PatchField<T> value, System.Text.Json.JsonSerializerOptions options)
        {
            if (value.HasValue)
                System.Text.Json.JsonSerializer.Serialize(writer, value.Value, options);
            else
                writer.WriteNullValue();
        }
    }");
        }

        if (hasNewtonsoft)
        {
            sb.AppendLine();
            sb.AppendLine(@"    /// <summary>
    /// Newtonsoft.Json converter for PatchField&lt;T&gt;.
    /// Register via: settings.Converters.Add(new PatchFieldNewtonsoftConverter());
    /// </summary>
    public sealed class PatchFieldNewtonsoftConverter : Newtonsoft.Json.JsonConverter
    {
        public override bool CanConvert(Type objectType)
            => objectType.IsGenericType
               && objectType.GetGenericTypeDefinition() == typeof(PatchField<>);

        public override object? ReadJson(Newtonsoft.Json.JsonReader reader, Type objectType, object? existingValue, Newtonsoft.Json.JsonSerializer serializer)
        {
            var innerType = objectType.GetGenericArguments()[0];
            var value = serializer.Deserialize(reader, innerType);
            return Activator.CreateInstance(objectType, new[] { value });
        }

        public override void WriteJson(Newtonsoft.Json.JsonWriter writer, object? value, Newtonsoft.Json.JsonSerializer serializer)
        {
            if (value is null) { writer.WriteNull(); return; }
            var type = value.GetType();
            var hasValue = (bool)type.GetProperty(""HasValue"")!.GetValue(value)!;
            if (hasValue)
                serializer.Serialize(writer, type.GetProperty(""Value"")!.GetValue(value));
            else
                writer.WriteNull();
        }
    }");
        }

        sb.AppendLine();
        sb.AppendLine("}");
        return sb.ToString();
    }
