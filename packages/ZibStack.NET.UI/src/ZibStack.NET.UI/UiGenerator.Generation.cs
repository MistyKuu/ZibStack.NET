using System.Globalization;
using System.Text;

namespace ZibStack.NET.UI;

public partial class UiGenerator
{
    private static string GenerateFormDescriptor(FormClassInfo info)
    {
        var sb = new StringBuilder();
        sb.AppendLine("// <auto-generated />");
        sb.AppendLine($"// Generated by ZibStack.NET.UI for {info.ClassName}");
        sb.AppendLine("#nullable enable");
        sb.AppendLine();
        sb.AppendLine("using System.Collections.Generic;");
        sb.AppendLine("using ZibStack.NET.UI;");
        sb.AppendLine();

        if (info.Namespace is not null)
        {
            sb.AppendLine($"namespace {info.Namespace};");
            sb.AppendLine();
        }

        var keyword = info.IsRecord ? "record" : "class";
        sb.AppendLine($"partial {keyword} {info.ClassName} : IFormDescriptorProvider");
        sb.AppendLine("{");

        // Static descriptor
        sb.AppendLine("    private static readonly FormDescriptor _formDescriptor = new FormDescriptor(");
        sb.AppendLine($"        name: \"{EscapeString(info.FormName)}\",");
        sb.AppendLine($"        layout: \"{EscapeString(info.Layout)}\",");

        // Groups
        sb.AppendLine("        groups: new FormGroupDescriptor[]");
        sb.AppendLine("        {");
        foreach (var group in info.Groups)
        {
            var label = group.Label != null ? $"\"{EscapeString(group.Label)}\"" : "null";
            sb.AppendLine($"            new FormGroupDescriptor(\"{EscapeString(group.Name)}\", {label}, {group.Order}),");
        }
        sb.AppendLine("        },");

        // Fields
        sb.AppendLine("        fields: new FormFieldDescriptor[]");
        sb.AppendLine("        {");
        foreach (var field in info.Fields)
        {
            EmitFormField(sb, field);
        }
        if (info.Relations.Count > 0)
        {
            sb.AppendLine("        },");
            sb.AppendLine("        children: new FormRelationDescriptor[]");
            sb.AppendLine("        {");
            foreach (var rel in info.Relations)
            {
                var relationStr = rel.Kind == RelationKind.OneToMany ? "oneToMany" : "oneToOne";
                var schemaUrlArg = rel.SchemaUrl != null ? $", \"{EscapeString(rel.SchemaUrl)}\"" : ", null";
                var formSchemaUrlArg = rel.FormSchemaUrl != null ? $", \"{EscapeString(rel.FormSchemaUrl)}\"" : ", null";
                sb.AppendLine($"            new FormRelationDescriptor(\"{EscapeString(rel.PropertyName)}\", \"{EscapeString(rel.Label)}\", \"{EscapeString(rel.TargetTypeName)}\", \"{EscapeString(rel.ForeignKey)}\", \"{relationStr}\"{schemaUrlArg}{formSchemaUrlArg}),");
            }
            sb.AppendLine("        });");
        }
        else
        {
            sb.AppendLine("        });");
        }

        sb.AppendLine();
        sb.AppendLine("    public static FormDescriptor GetFormDescriptor() => _formDescriptor;");
        sb.AppendLine("}");

        return sb.ToString();
    }

    private static void EmitFormField(StringBuilder sb, FormFieldInfo field)
    {
        sb.AppendLine($"            new FormFieldDescriptor(");
        sb.AppendLine($"                name: \"{EscapeString(field.JsonName)}\",");
        sb.AppendLine($"                type: \"{EscapeString(field.FieldType)}\",");
        sb.AppendLine($"                uiHint: \"{EscapeString(field.UiHint)}\",");

        if (field.Label != null)
            sb.AppendLine($"                label: \"{EscapeString(field.Label)}\",");
        if (field.Placeholder != null)
            sb.AppendLine($"                placeholder: \"{EscapeString(field.Placeholder)}\",");
        if (field.HelpText != null)
            sb.AppendLine($"                helpText: \"{EscapeString(field.HelpText)}\",");
        if (field.Group != null)
            sb.AppendLine($"                group: \"{EscapeString(field.Group)}\",");

        sb.AppendLine($"                order: {field.Order},");

        if (field.IsRequired) sb.AppendLine("                isRequired: true,");
        if (field.IsHidden) sb.AppendLine("                isHidden: true,");
        if (field.IsReadOnly) sb.AppendLine("                isReadOnly: true,");
        if (field.IsDisabled) sb.AppendLine("                isDisabled: true,");
        if (field.IsCreateOnly) sb.AppendLine("                isCreateOnly: true,");
        if (field.IsUpdateOnly) sb.AppendLine("                isUpdateOnly: true,");

        // Options
        if (field.Options.Count > 0)
        {
            sb.AppendLine("                options: new SelectOptionDescriptor[]");
            sb.AppendLine("                {");
            foreach (var opt in field.Options)
            {
                sb.AppendLine($"                    new SelectOptionDescriptor(\"{EscapeString(opt.Value)}\", \"{EscapeString(opt.Label)}\"),");
            }
            sb.AppendLine("                },");
        }

        // Conditional
        if (field.Conditional != null)
        {
            sb.AppendLine($"                conditional: new ConditionalDescriptor(\"{EscapeString(field.Conditional.FieldName)}\", \"{EscapeString(field.Conditional.Operator)}\", \"{EscapeString(field.Conditional.Value)}\"),");
        }

        // Validation
        if (field.ValidationRules.Count > 0)
        {
            sb.AppendLine("                validation: new Dictionary<string, string>");
            sb.AppendLine("                {");
            foreach (var rule in field.ValidationRules)
            {
                switch (rule.Kind)
                {
                    case "required":
                        sb.AppendLine($"                    [\"required\"] = \"true\",");
                        break;
                    case "minLength":
                        sb.AppendLine($"                    [\"minLength\"] = \"{(int)(rule.MinValue ?? 0)}\",");
                        break;
                    case "maxLength":
                        sb.AppendLine($"                    [\"maxLength\"] = \"{(int)(rule.MaxValue ?? 0)}\",");
                        break;
                    case "range":
                        sb.AppendLine($"                    [\"min\"] = \"{(rule.MinValue ?? 0).ToString(CultureInfo.InvariantCulture)}\",");
                        sb.AppendLine($"                    [\"max\"] = \"{(rule.MaxValue ?? 0).ToString(CultureInfo.InvariantCulture)}\",");
                        break;
                    case "email":
                        sb.AppendLine($"                    [\"email\"] = \"true\",");
                        break;
                    case "url":
                        sb.AppendLine($"                    [\"url\"] = \"true\",");
                        break;
                    case "pattern":
                        if (rule.Pattern != null)
                            sb.AppendLine($"                    [\"pattern\"] = \"{EscapeString(rule.Pattern)}\",");
                        break;
                    case "notEmpty":
                        sb.AppendLine($"                    [\"notEmpty\"] = \"true\",");
                        break;
                }
            }
            sb.AppendLine("                },");
        }

        // Props
        if (field.Props.Count > 0)
        {
            sb.AppendLine("                props: new Dictionary<string, string>");
            sb.AppendLine("                {");
            foreach (var kv in field.Props)
            {
                sb.AppendLine($"                    [\"{EscapeString(kv.Key)}\"] = \"{EscapeString(kv.Value)}\",");
            }
            sb.AppendLine("                },");
        }

        // Close — remove trailing comma from last param by using closing paren
        sb.AppendLine($"                isNullable: {(field.IsNullable ? "true" : "false")}),");
    }

    private static string EscapeString(string s)
        => s.Replace("\\", "\\\\").Replace("\"", "\\\"");
}
