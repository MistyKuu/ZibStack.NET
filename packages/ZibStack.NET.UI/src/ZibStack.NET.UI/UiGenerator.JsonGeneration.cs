using System.Globalization;
using System.Linq;
using System.Text;

namespace ZibStack.NET.UI;

public partial class UiGenerator
{
    private static string GenerateFormJson(FormClassInfo info)
    {
        var sb = new StringBuilder();
        sb.AppendLine("// <auto-generated />");
        sb.AppendLine($"// Generated by ZibStack.NET.UI — JSON schema for {info.ClassName}");
        sb.AppendLine("#nullable enable");
        sb.AppendLine();

        if (info.Namespace is not null)
        {
            sb.AppendLine($"namespace {info.Namespace};");
            sb.AppendLine();
        }

        var keyword = info.IsRecord ? "record" : "class";
        sb.AppendLine($"partial {keyword} {info.ClassName}");
        sb.AppendLine("{");
        sb.AppendLine("    private static readonly string _formSchemaJson =");

        // Build JSON
        var json = BuildFormJsonString(info);
        sb.AppendLine($"        @\"{EscapeVerbatimString(json)}\";");
        sb.AppendLine();
        sb.AppendLine("    public static string GetFormSchemaJson() => _formSchemaJson;");
        sb.AppendLine("}");

        return sb.ToString();
    }

